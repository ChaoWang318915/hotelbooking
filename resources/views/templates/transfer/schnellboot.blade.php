<h1>Unsere Empfehlung: Transfer mit dem Schnellboot nur bis max. 45 Minuten Reisedauer</h1>
<p>
Manchmal bietet Ihnen das Resort unterschiedliche Transfermöglichkeiten an.
Zum Beispiel mit dem Wasserflugzeug und mit dem Schnellboot.
Wer die Wahl hat, sollte den Transfer mit dem Schnellboot nur dann wählen, wenn das Hotel max. 45 Minuten vom Flughafen in Malé entfernt ist.  
Viel weiter fahren die meisten Schnellboote eh nicht. Dennoch ...
<br><br>
Die Gewässer der Malediven sind nicht immer so friedlich und still wie man es von den Bildern her kennt.
Auch wenn die Sonne scheint, und das Meer ruhig aussieht, kann es auf dem Meer ziemlich ruppig zugehen.
Es gibt Tage mit heftigem Wellengang, obwohl es von Land aus nicht so aussieht, da möchte man überall sein, nur nicht an Bord eines Schnellbootes.
<br><br>
Es ist nicht nur der Wellengang der einen Seekrank werden lässt, es ist auch das der Transfer auf dem Boot ziemlich holperig, nass und laut sein kann, eigentlich keine angenehme Art zu reisen bei dieser Geschwindigkeit.
Im Gegensatz zu den Wasserflugzeugen können Schnellboote während des Tages und auch nachts verkehren.
Die meisten Transfers werden tagsüber durchgeführt.
</p>
<p>&nbsp;</p>
<figure><img src="../../../images/content-images/schnellboot_male.jpg" alt="Bild: Schnellboot auf den Malediven"></figure>

<h2>Einen Schnellboot Transfer buchen</h2>
<div class="row pt-4">
<div class="col-md-4"> <figure><img src="../../images/content-images/anlegestelle-male.jpg" alt="Bild: Anlegestelle Malé"></figure></div>
<div class="col-md-8">
<h3>Die Anlegestelle in Malé</h3>
<p>
Die Anlegestelle vom Schnellboot ist ca. 2 Minuten von der Ankunftshalle am Flughafen in Malé entfernt. 
Der Wartebereich auf dem Flughafengelände verfügt über einen öffentlichen Sitzbereich, dort gibt es auch mehrere Cafés, einen Raucherbereich und Toiletten.
<br><br>
Auf den Malediven ist es nicht möglich einen Transfer mit dem Schnellboot vor Ort bei Ankunft zu buchen. 
Die Hotels haben entweder selbst ein Schnellboot und bieten den Transfer ausschließlich mit ihren eigenen Schnellbooten an oder Sie chartern ein Schnellboot für Ihre Gäste.
Die Buchung des Transfers muss daher im Voraus erfolgen. 
Auf Wunsch und gegen Gebühr kümmern wir uns um Ihren Transfer, <a href"https://www.my-malydives.com/hotel-transfer/transferbuchung" title="Transferbuchung">hier</a> finden Sie mehr Informationen. 
Schnellboote sind eine beliebte und schnelle Beförderungsmöglichkeit, was daran liegt, das diese Transfermöglichkeit einfach billiger ist als der Transfer mit dem Wasserflugzeug oder die Inlandsflüge. 
</p>
</div></div>

<div class="row pt-4">
<div class="col-md-4"> <figure><img src="../../images/content-images/schnellboot2.jpg" alt="Bild: Das Boot"></figure></div>
<div class="col-md-8">
<h3>Das Boot</h3>
<p>
Schnellboote sind kleine wendige Boote mit rund 21 Sitzplätzen.
Es gibt auch größere Ausführungen mit mehr Sitzplätzen und einer kleinen Toilette.
Die meisten Schnellboote sind <b>Gulf Craft</b> aus der <b>Touring</b> Baureihe <b>36</b> und <b>40</b>.
Die Schnellboote können Geschwindigkeiten zwischen 60 km/h und  100 km/h erreichen.
<br><br>
Luxusyachten haben weniger, dafür aber viel bequemere Sitzplätze, sie werden innerhalb des Atolls eingesetzt und sind für längere Strecken ausgelegt.
Die werden nur auf Wunsch der Gäste eingesetzt oder von den luxuriösen Resorts wie zum Beispiel <b>One&Only</b> oder <b>Cheval Blanc</b>.
</p>
</div></div>
<p>&nbsp;</p>

<div class="row pt-4">
<div class="col-md-4"> <figure><img src="../../images/content-images/schnellboot-sicherheit.jpg" alt="Bild: Kleines Mädchen mit Rettungsweste"></figure></div>
<div class="col-md-8">
<h3>Sicherheit </h3>
<p>
An Board sollte man darauf achten, die Rettungswesten anzulegen.
Niemals auf ein Boot ohne Rettungswesten gehen!
Wir können uns gar nicht vorstellen, dass sowas jemals auf den Malediven passiert sein sollte.
Jedes Boot hat Rettungswesten. 
In den Malediven gelten die internationalen Seefahrregeln genauso wie in Deutschland.
<br><br>
Die Boote, die für den Transfer von Touristen eingesetzt werden, müssen jährlich durch die Behörden auf Sicherheit geprüft.
Die Kapitänslizenz wird ebenfalls jährlich geprüft. Das Bordpersonal ist gut ausgebildet und alle verfügen über sehr viel Erfahrung.
</p>
</div></div>
<p>&nbsp;</p>


<div class="row pt-4">
<div class="col-md-4"> <figure><img src="../../images/content-images/Schnellboot-Gepaeckregelung.jpg" alt="Bild: Gepäck"></figure></div>
<div class="col-md-8">
<h3>Gepäckregelung</h3>
<p>
Auf dem Boot gibt es keine Gewichtsbeschränkungen, was das Gepäck angeht.
Es gilt die erlaubte maximale Zuladung. 
Oft wird das Gepäck, wenn Sie mit dem Flugzeug fliegen, per Boot nachgeliefert.
Das passiert immer dann, wenn das Wasserflugzeug voll mit Passagieren und Gepäck ist.
<br><br>
Für den Transfer mit dem Schnellboot sind 1 Gepäckstück pro Person und 1 Handgepäckstück pro Person erlaubt.
Extra Gepäck ist oft gebührenpflichtig, die Preise variieren je nach Hotel.
Der Koffer darf nicht mehr wiegen als 30 Kg.
Wer ein Schnellboot gechartert hat, kann soviel Gepäck mitnehmen wie er will (natürlich nur bis zur max. erlaubten Zuladung) 
</p>
</div></div>
<p>&nbsp;</p>



<div class="row pt-4">
<div class="col-md-4"> <figure><img src="../../images/content-images/Schnellboot-Shared-Transfer.jpg" alt="Bild: Schnellboot"></figure></div>
<div class="col-md-8">
<h3>Shared Transfer</h3>
<p>
	Alle angebotenen Transfers mit dem Schnellboot sind sogenannte „shared transfers“.  
	Bei einem „shared transfer“ teilt man sich das Boot mit anderen Reisenden, die entweder auf die gleiche Insel wollen, oder auf eine andere Insel die auf den Weg liegt. 
	Ein „shared transfer“ muss daher nicht immer direkt sein. 
	Es kann also sein, dass unterwegs irgendwo angehalten wird, um andere Reisende zu Ihrer Urlaubsinsel zu bringen bzw. abzuholen. 
	<br><br>
	Man kann ein Schnellboot auch privat mieten. 
	Der Transfer geht dann natürlich direkt und man hat keine Wartezeiten, kostet aber um ein vielfaches mehr. 
	Das lohnt sich aber nur, wenn man mit einer größeren Familie oder Gruppe reist bzw. man das Geld hat. 
	Alle „shared transfers“ haben Wartezeiten.
	Diese können je nach Ankunft der anderen Gäste zwischen 1 Stunde und 4 Stunden betragen.
</p>
</div></div>
<p>&nbsp;</p>


<div class="row pt-4">
<div class="col-md-4"> <figure><img src="../../images/content-images/maldives-weather.jpg" alt="Bild: Wetter"></figure></div>
<div class="col-md-8">
<h3>Wetterbedingungen</h3>
<p>
Ein weiterer wichtiger Punkt, den Sie beim Transfer mit dem Schnellboot beachten sollten, sind die Wetterbedingungen.
Die Transfers mit dem Schnellboot oder dem Wasserflugzeug hängen von den Wetterbedingungen ab, nur der Inlandsflug findet auch bei schlechtem Wetter statt. 
Ist das Wetter so schlecht, das der Transfer nicht stattfinden kann, wird dieser auf den Vorabend verlegt. 
<br><br>
Schlimmsten Falls müssen Sie eine Nacht auf der Insel verbleiben und können erst am nächsten Tag abreisen. 
Gegen diesen Fall müssen Sie sich mit einer Reiseversicherung absichern.
Das Transfers wegen schlechten Wetter verschoben werden oder nicht stattfinden ist besonders häufig in den Monaten Juni und Juli.
</p>
</div></div>

<div class="row pt-4">
<div class="col-md-4"> <figure><img src="../../images/content-images/wartebereich-schnellboote.jpg" alt="Bild: Wartebereich Schnellboote"></figure></div>
<div class="col-md-8">
<h3>Wartezeiten</h3>
<p>
Wenn Sie einen Transfer mit dem Schnellboot buchen, müssen Sie sicherlich keine langen Wartezeiten am Flughafen bzw. Bootsanlegestelle in Kauf nehmen.
Das ist besonders angenehm nach einem Langstreckenflug.
Wenn man ankommt, geht es relativ schnell weiter, planen Sie eine Wartezeit von 30 bis 60 Minuten ein.
Unter gewissen Umständen kann die Wartezeit auch 2 Stunden betragen (z. Bsp. bei schlechten Wetterverhältnissen oder Wartezeiten wegen anderen Gästen.)
</p>
</div></div>


<p>&nbsp;</p>
<p>&nbsp;</p>


<div class="block">
<h3>Der Inlandsflug, bei jedem Wetter und zu jeder Uhrzeit</h3>
<p>
	Eine weitere Transfermöglichkeit auf den Malediven ist der Transfer mit dem Inlandsflug.
	Es sollte eine preiswertere Möglichkeit sein im Vergleich mit dem Wasserflugzeug, ist es aber leider nicht immer.
  Dafür landet und startet der Inlandsflug auch bei Dunkelheit und schlechtem Wetter.
	Bestens geeignet für lange Strecken.
</p>
