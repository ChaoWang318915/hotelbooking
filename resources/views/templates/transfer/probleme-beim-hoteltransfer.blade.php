<h1>Probleme beim Hoteltransfer</h1>
<figure><img src="../../images/content-images/Probleme-beim-Transfer-header.jpg" alt="Bild: Mann verpasst seinen Flug"></figure>
<p>
	Wie heißt es im Volksmund so schön, wo gehobelt wird, fallen Späne. Das trifft es auch für den Hoteltransfer auf den Malediven.
	Einige Probleme sind hausgemacht, gegen andere kann man nicht machen.
</p>

<ul class="accordion">
<li>
<header class="title-panel"><h3>Kein Empfang am Flughafen</h3></header>
<div class="collapse">
<h4>Keiner holt Sie vom Flughafen ab</h4>
<p>Ein Problem was sehr selten auftritt. Wenn es aber passiert, ist es kein großes Problem.
<br><br>
Jedes Hotel auf den Malediven hat entweder eigenes Personal am Flughafen, welches die Gäste begrüßt oder nimmt die Hilfe von Service Agenten in Anspruch. Weiterhin haben fast alle Hotels einen kleinen Stand in der Ausgangshalle des Flughafens.
Diese Stände sind nummeriert. Schauen Sie auf Ihre Buchungsbestätigung oder E-Mail nach, ob Ihnen das Resort die Standnummer mitgeteilt hat.
Gehen Sie direkt zu dem Stand. Haben Sie keine Standnummer erhalten.
Fragen Sie an einem der Stände, die Leute vor Ort kennen sich gut aus, sind kundenfreundlich und kompetent, man wird Ihnen den Stand oder den Verantwortlichen vor Ort zeigen. 
<br><br>
Auch Meine Malediven nutzt eine Agentur vor Ort. Sie erhalten mit Ihrer Buchung auch eine Telefonnummer die Sie 24 Stunden erreichen können.
<br><br>
Sollte alles nichts helfen, rufen Sie vor Ort im Hotel an.
Am besten ist, Sie bitten einen Mitarbeiter vor Ort von einem anderen Hotel.
Das wird kein Problem darstellen. Wie gesagt, dieses Problem tritt sehr selten auf.
Oft ist dann schon im Vorfeld was falsch gelaufen (zum Beispiel falsche Ankunftszeiten).

</p>
</div>
</li>

<li>
<header class="title-panel"><h3>Zusatzkosten in der VIP/Airport Lounge bei der Abreise</h3></header>
<div class="collapse">
<p>
Einige Hotels berechnen für die Wartezeit in der Flughafen-Lounge extra Geld.
Das gilt nicht für ankommende Gäste, sondern für Gäste die auf Ihren Abflug warten.
Die Lounges am Flughafen sind nur für ankommende Gäste kostenlos.
Wenn Sie wieder abfliegen von den Malediven, müssen Sie den  Aufenthalt in der Lounge bezahlen.
Meistens ist der Aufenthalt auch noch begrenzt auf einige Stunden. Bezahlt wird nach der Anzahl der Personen. 
<br><br>
Auch wenn der Hotelpreis den Aufenthalt bei Ankunft in der Flughafen-Lounge beinhaltet, einige Leistungen oder Angebote innerhalb der Lounge werden nur gegen Extra-Gebühr angeboten.
Am Flughafen gibt es darüber hinaus auch noch die Flughafen Lounge (Moonimaa Lounge), die um einiges preiswerter ist als die Resort VIP-Lounges.
</p>
</div>
</li>

<li>
<header class="title-panel"><h3>Das Stornieren eines Hoteltransfers ist mit Kosten verbunden</h3></header>
<div class="collapse">
<p>
Wenn der Transfer nicht angetreten werden kann, können bis zu 100 % Stornierungskosten entstehen.
Mit welchen Kosten Sie rechnen müssen, entnehmen Sie Ihrer Buchungsbestätigung.
Es sollte vor Buchung abgeklärt werden wie hoch diese Kosten sind.
Einige Anbieter berechnen keine Kosten, wenn die Stornierung rechtzeitig erfolgt. Genaue Angaben sind der Hotelbeschreibung zu entnehmen. 
<br><br>
Sollte Ihr Flug Verspätung haben und viel später auf den Malediven landen, kann es sein, dass Ihr Flug bereits weg ist und Sie diesen verpasst haben.
Das ist ein typischer „No-Show“. Hier storniert die Airline Ihren Hinflug und berechnet Ihnen die Kosten. 
<br><br>
Ansprüche müssen Sie gegenüber der Fluggesellschaft erheben, die Ihre Verspätung zu verantworten hat. Oft reicht die Zeit jedoch aus.
Selbst wenn Ihr Flug 1–2 Stunden verspätet ankommt, kann es dennoch rechtzeitig sein und der Flug ist noch nicht gestartet.
Die Anbieter verfolgen selbstverständlich die ankommenden Flüge und deren Flugzeiten.
Passiert es trotzdem, müssen Sie schlimmsten Falls ein Hotel in der Umgebung von Malé oder in Malé suchen für die eine Übernachtung, bis es am nächsten Tag dann in Ihr Hotel geht. 
<br><br>
Denken Sie daran, den Transfer immer separat auch zu stornieren, unabhängig vom Hotelaufenthalt.
</p>
</div>
</li>

<li>
<header class="title-panel"><h3>Wartezeiten</h3></header>
<div class="collapse">
<p>
Sowohl beim Wasserflugzeug und beim Inlandsflug als auch beim Schnellboot können Wartezeiten entstehen.
Ankommen und gleich weiter ins Hotel funktioniert nur, wenn Sie einen privaten Transfer gebucht haben.
<br><br>
Manchmal treffen nicht alle Gäste rechtzeitig ein, die für diesen Flug oder das Boot reserviert wurden.
Auch Verspätungen von internationalen Flügen kommen vor.
Oft sind es schlechte Wetterbedingungen, die dafür verantwortlich sind, dass die Abflug- oder Abfahrtzeiten von einem Flug oder einer Bootsfahrt geändert oder gar komplett abgesagt werden muss.
Manchmal sind es auch technische Probleme, die Verspätungen hervorrufen. Es kommt dann zu längeren Wartezeiten.
</p>
<h4>Wartezeiten durch Zwischenstopps</h4>
<p>
Flugzeuge fliegen nicht immer direkt oder nonstop. Manchmal sind Zwischenstopps nötig, um Gäste aus- oder zusteigen zu lassen.
Die Kommunikation klappt nicht immer reibungslos, sodass man oft nicht weiß, warum es eigentlich nicht weitergeht oder man warten muss.
Auch Boote müssen nicht unbedingt direkt ins Hotel fahren.
Einige machen einen Halt an anderen Inseln, um Gäste abzuholen oder hinzubringen.
All das bedeutet eine längere Anreise. Man darf halt nicht vergessen, dass die Transfers alle „shared transfers“ sind.
Also Transfers, die man sich mit anderen Reisenden teilt. So bleiben die Kosten einigermaßen im Rahmen, ansonsten muss man einen Privattransfer bestellen. Diese sind selbst für Boote relativ teuer.
</p>
</div>
</li>

<li>
<header class="title-panel">
<h3>Ankunft nach 15:30 Uhr in Malé oder verspätet </h3>
</header>
<div class="collapse">
<p>

Ein Transfer mit dem Wasserflugzeug und den meisten Booten ist bei Dunkelheit nicht möglich.
Einige Boote und der Inlandsflug bieten Ihren Service auch bei Dunkelheit an.
Das Wasserflugzeug aber fliegt nur bis max. 18 Uhr.
Das bedeutet, Sie müssen bis spätestens 15:30 Uhr in Malé gelandet sein, um den letzten Flug mit dem Wasserflugzeug noch zu bekommen.
<br><br>
Ansonsten muss eine zusätzliche Hotelnacht in Malé oder Umgebung reserviert und extra bezahlt werden.
Es ist sinnvoll zu prüfen, ob die Weiterreise eventuell mit einem Inlandsflug möglich ist.
</p>
</div>
</li>

<li>
<header class="title-panel">
<h3>Transfer wird storniert / abgesagt  </h3>
</header>
<div class="collapse">
<p>
Unter Berücksichtigung der Luft- und Seefahrtvorschriften, die auch auf den Malediven gelten, kann einzig und allein der Kapitän oder der Pilot entscheiden, ob ein Flug mit dem Wasserflugzeug oder eine Fahrt mit dem Boot stattfinden kann oder nicht.
Sicherheit geht bei allen vor.
Sollten die Wetterverhältnisse so schlecht sein, das ein Transfer nicht stattfinden kann, wird man alles tun, das Sie trotzdem an Ihr Ziel kommen. 
<br><br>
Dennoch kann es sein, dass ein Flug oder eine Bootsfahrt abgesagt wird. Bei der Anreise stellt das ein kleineres Problem dar.
Es ist zwar ärgerlich, aber entweder kommt es zu einer längeren Wartezeit und einer Übernachtung in Malé oder Umgebung und man wird am Folgetag zum Hotel gebracht.
Es ist ein großes bei der Abreise. Bestenfalls muss man am Vortag abreisen, um das schlechte Wetter zu umgehen, aber im schlimmsten Fall verpasst man seinen Rückflug nach Hause.
Gegen dieses Risiko gilt es sich abzusichern. Hier hilft eine Reiseabbruchversicherung. Achtung hier gibt es Versicherungstechnisch „Stolperfallen“.
Den normalerweise gilt eine Reiseversicherung immer nur bis zum Tag der Abreise, wenn Sie aber einen Tag länger bleiben müssen, sind Sie quasi nicht versichert, sei denn Sie haben eine Jahresversicherung.
Eine Jahresversicherung ist sinnvoll, wenn man mehrmals pro Jahr verreist oder jedes Jahr wieder verreist, denn erfahrungsgemäß sind diese Versicherungen preiswerter als eine Versicherung, die man nur für eine Reise abschließt. Bitte lassen Sie sich diesbezüglich beraten.
</p>
</div>
</li>

<li class="open">
<header class="title-panel">
<h3>Fehlende Flugdetails </h3>
</header>
<div class="collapse">
<p>
Damit ein Transfer reibungslos arrangiert werden kann, müssen Flugdetails (Datum, Uhrzeit, Flugnummern vom Anreise- und Abreisetag) unbedingt bei Buchung übermittelt werden.
Sollten die Flugdetails noch nicht vorliegen, müssen diese spätestens in der vom Hotel vorgegebenen Frist dem Hotel übermittelt werden.
Kommt man dem nicht oder zu spät nach kann es zu Probleme beim Transfer kommen und eventuell fallen extra Kosten an.
<br><br>
Im schlimmsten Fall kann der Transfer nicht am selben Tag durchgeführt werden.
In jedem Fall sollte das Hotel sofort darüber informiert werden, um möglichst einen Transfer noch am selben Tag zu organisieren.
Wichtig ist, dass man sich nach Übermittlung der Flugdetails den Transfer schriftlich bestätigen lässt.
</p>
</div>
</li>
</ul>

