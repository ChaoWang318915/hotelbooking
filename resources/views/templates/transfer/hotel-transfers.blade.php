<div class="collapse">
  <h3>Der Transfer mit der Fähre</h3>
<p>
Eine weitere Transfermöglichkeit, auf die wir jedoch nicht weiter eingehen möchten, ist der Transfer mit der Fähre.
Die Fähren werden selten von Touristen benutzt.
Nur wer ein Gästehaus bucht, benötigt den Transfer mit der Fähre. Und selbst dann würde man wahrscheinlich eher das Schnellboot nehmen.
Es dauert lange bis die Fähren ankommen. 
<br><br>
Die Fähren fahren landesweit so ziemlich alle einheimischen Inseln an.
Dies geschieht nach einem festen Fahrplan <small>(meist einmal morgens und einmal spät nachmittags)</small> und einer festen Route.
Ja nach Entfernung zahlt man pro Person und Strecke umgerechnet nur wenige US-Dollars, gezahlt wird in Rufiyaa (der Landewährung der Malediven).   
<br><br>
Manchmal muss man auf einer Insel auch umsteigen in die nächste Fähre.
Was die Anfahrt noch einmal erheblich verlängert.
Für Touristen ist die Fähre ein nicht so wichtiges Transportmittel.
</p>
</div>
</li>
</ul>
