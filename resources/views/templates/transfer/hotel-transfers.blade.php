<div class="col-md-7">
<h3>Domestic Flights (Inlandsflug)</h3>
<p>
Der <b>Inlandsflug</b> sollte preiswerter als das Wasserflugzeug sein. Diesen kann man auch selbst buchen.
Aber Achtung: Man muss aber dann den Transfer vom Flughafen zum Resort noch organisieren.
Das geht nur mit einem Schnellboot.
Wenn Sie den Inlandsflug nicht beim Hotel gebucht haben, ist der <b>Schnellboot Transfer</b> meistens kostenpflichtig.
Buchen Sie alles beim Hotel ist der Schnellboot Transfer inklusive. 
<br><br>
Ein Inlandsflug wird gern genommen, wenn der internationale Flug spät abends in Male ankommt, da das Wasserflugzeug nur bis 18 Uhr fliegt,
kann man mit dem Inlandsflug auch bei Dunkelheit fliegen.
Auch fliegen die Fluggesellschaften bei schlechtem Wetter.
Wenn man Geld sparen möchte oder wenn es keine andere Möglichkeit gibt, weil das Wasserflugzeug die Insel nicht anfliegt ist der Inlandsflug die beste Alternative.
Auch für Resorts, die mehr als 300 km vom Velana Flughafen entfernt liegen, ist der Inlandsflug eine gute Alternative.
Aber es gibt wie bei allen Flügen auch Regeln, die es zu beachten gilt.
</p>
