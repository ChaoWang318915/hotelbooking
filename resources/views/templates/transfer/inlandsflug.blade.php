<h1>Zu jeder Zeit und bei jedem Wetter, der Inlandsflug</h1>
<p>
Einige Hotels bieten Ihren Gästen unter anderen den Transfer mit einem Inlandsflug an.  
Für Gäste, die nach 15:30 Uhr in Malé landen, besteht nur die Möglichkeit mit dem Schnellboot zum Hotel zu fahren oder eben mit einem Inlandsflug bis in die Nähe der Resortinsel zu fliegen.
Anschließend mit dem Schnellboot vom Flughafen zur Resortinsel. Das jedoch wird, wenn überhaupt möglich, vom Hotel organisiert.  
</p>
<p>&nbsp;</p>
<figure><img src="../../../images/content-images/domestic-flight-2.jpg" alt="Bild: Inlandsflug auf den Malediven"></figure>

<h2>Einen Inlandsflug buchen</h2>
<div class="row pt-4">
<div class="col-md-4"> <figure><img src="../../images/content-images/Domestic-Flight-Content.jpg" alt="Bild: Inlandsflug"></figure></div>
<div class="col-md-8">
<h3>Maldivian ist die erste Wahl für Touristen!</h3>
<p>
Ein <b>Inlandsflug</b> bietet sich immer bei großen Entfernungen an, da die Flüge auch bei <b>schlechtem Wetter</b> fliegen und sowohl bei <b>Tages- und Nachtzeiten</b> eingesetzt werden.
 Inlandsflüge sind oft <b>preiswerter als Wasserflugzeuge</b>, jedoch nicht immer.
Inlandsflüge werden von Resort organisiert, man kann aber als Gast den Inlandsflug auch selbst buchen, muss dann aber noch den Transfer von der Flughafeninsel zur Resortinsel organisieren und auch extra bezahlen.  
<br><br>
Die Inlandsflüge sind mit den Hotels abgestimmt und richten sich vornehmlich an die An- und Abflugzeiten der Gäste. 
Einige Strecken haben feste Flugzeiten.
Es können Zwischenstopps erfolgen, somit sind nicht immer alle Inlandsflüge Nonstop-Flüge.
Der Inlandsflug ist sicher die schnellste Transfermöglichkeit, um von Malé auf eine Resortinsel zu kommen. 
<br><br>
Auf den Malediven gibt es zur Zeit 3 Fluggesellschaften:<br><br> a) <a href="https://maldivian.aero" target="_blank">Maldivian</a>, b) <a href="https://mantaair.mv/" target="_blank">Manta Air</a> und c) <a href="https://www.flyme.mv/" target="_blank">Flyme</a>
<br><br>
Maldivian dürfte für Touristen die beste Wahl sein, da <a data-toggle-popup="" href="#popup-01">alle Inlandsflughäfen</a> angeflogen werden.
<br><br>
Die beiden anderen Airlines sind nur eingeschränkt tätig. 
Damit ein Hoteltransfer mit dem Inlandsflug organisiert werden kann, benötigt man Ihre Flugdetails. 
Die Flugdetails müssen dem Resort spätestens 7 Tage vor Ankunft in Malé vorliegen, um einen reibungslosen Hoteltransfer gewährleisten zu können.
Wenn die Daten nicht rechtzeitig übermittelt werden, kann es zu Verzögerung beim Transfer kommen.
Das ist abhängig von der Verfügbarkeit von Flügen und Sitzplätzen. 
<br><br>
Zusammen mit den Flugdetails muss der vollständige Name und die Nationalität des Gastes, wie dieser im Reisepass steht, angegeben werden, inklusive aller Mittelnamen.
  Namensänderungen sind für ausgestellte Tickets nicht zulässig.
Es ist hier unbedingt auf die Rechtschreibung zu achten.
<br><br>
Fehler im Namen werden als Namensänderung (name change) betrachtet und kosten Geld.
 Das kann bis 100 % des Ticketpreises betragen.
Das Freigepäck bei Inlandsflügen beträgt, wenn nichts anderes angegeben, 20 kg pro Person plus 5 kg bzw. 7 kg bei einem Airbus 320/321 Handgepäck pro Passagier (ausgenommen Kleinkinder 0 bis 1.99 Jahre).  
Der Preis für Übergepäck beträgt zwischen 2 US-Dollar pro kg und 6 US-Dollar pro kg.
<br><br>
Handgepäck sollte die Maße 50,8 cm x 30,5 cm x 17,8 cm nicht überschreiten.  
Kleinkinder unter 2 Jahren sind kostenlos (auf dem Schoß der Eltern sitzend).
 Inlandflüge können eine längere Wartezeit haben.
Der letzte Flug startet gegen 23:00 Uhr aus Malé.
</p>
