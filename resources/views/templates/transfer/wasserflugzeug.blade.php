<h1>Der Transfer mit dem Wasserflugzeug <br>Wir sagen dir, was du bei deinem Flug beachten musst</h1>

<p>Wir würden Ihnen immer den Transfer mit dem Wasserflugzeug empfehlen.
	 Bei einigen Hotels wird dieser nicht angeboten. Ist das Resort innerhalb von wenigen Kilometern von Malé aus erreichbar, wird der Transfer mit dem Schnellboot angeboten.
	 Ist das Resort zu weit entfernt, wird der Inlandsflug die bessere Wahl sein.

</p>

<h2>Trans Maldivian Airways (TMA)</h2>
<figure><img src="../../../images/content-images/wasserflugzeug-tma.jpg" alt="#"></figure>
<p>
Mit einer Flotte von 57 de Havilland Twin Otters betreibt <b>Trans Maldivian Airways (TMA)</b> auf den Malediven den größten Wasserflugzeugbetrieb der Welt.
Nicht nur Transfers zu den Resorts  werden angeboten, sondern auch Ausflüge, Privat-Charter, VIP Flüge, Fotoflüge und im Notfall Passagier Evakuierungen. 
<br><br>
TMA fliegt über 80 Resorts auf den Malediven an.
Der Flugradius reicht von 312 km in nördlicher und knapp 160 km in südlicher Richtung (vom internationalen Flughafen Velana aus).
Das Wasserflugzeug ist das sicherste und schnellste Verkehrsmittel auf den Malediven und für 80 % aller Hotels auf den Malediven die beste Wahl.
Er ist bequem, aufregend, schnell und die Malediven aus der Luft, einfach nur ein Traum.
Es ist interessant vorne zu sitzen, um ein Blick ins Cockpit werfen zu können, wo die barfüßigen Piloten das Flugzeug fliegen.
Wenn man Fotos machen möchte, sollte man auf der Schattenseite des Flugzeuges sitzen. 
<br><br>
TMA bietet auch Rundflüge an, Fotoflüge oder private Charter-Flüge.
Allerdings sind die Preise sehr hoch und müssen im Voraus gebucht werden.
</p>

<h2>Endlich in Malé angekommen ... </h2>
<div class="row pt-4">
<div class="col-md-4"> <figure><img src="../../images/content-images/Wasserflugzeug-Male-Hauptstadtinsel.jpg" alt="Bild: Malé"></figure></div>
<div class="col-md-8">
<h3>Jetzt beginnt Ihr Urlaub</h3>
<p>
Nach der Immigration holen Sie Ihr Gepäck ab und gehen weiter in die Ankunftshalle.
Hier angekommen, werden Sie vom Hotelpersonal abgeholt.
<br><br>
Wir von <b>Meine Malediven</b> arbeiten mit einem ortsansässigen Anbieter zusammen, der unsere Gäste direkt in der Ausgangshalle abholt.
Andere Hotels haben Personal vor Ort, welches Sie begrüßt.
Sollte das Personal nicht auffindbar sein, gehen Sie zu den Schaltern in der Ausgangshalle und schauen Sie nach Ihrem Hotel.
Normalerweise schreiben die Hotelanbieter die Nummer des Schalters, wo Sie sich melden sollen, in die Buchungsbestätigung.
Wenn alles nichts hilft, einfach jemanden fragen, die Leute sind sehr freundlich und man wird Ihnen sofort den richtigen Schalter zeigen. 
Es gibt auch ein Informationsschalter in der Halle, hier kann man Ihnen garantiert weiterhelfen.
</p>
</div></div>
