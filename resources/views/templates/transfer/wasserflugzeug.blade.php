<h2>Informationen zum Transfer mit dem Wasserflugzeug</h2>

<ul class="accordion">


<li>
<header class="title-panel"> <h3>Ankunft in Malé / Abflug von Malé</h3></header>
<div class="collapse">
<h4>Ankunft in Malé</h4>
<p>
Bitte beachten Sie, dass die Wasserflugzeuge nur bei Tageslicht fliegen.
<br>
Die normalen Zeiten sind von Sonnenaufgang 6:00 Uhr bis Sonnenuntergang 18:00 Uhr.
Für Gäste, deren Flugzeug nach 15:30 Uhr in Malé landet, erfolgt der Transfer in der Regel erst am nächsten Tag.
Wenn Sie Ihr Resort also noch am selben Tag erreichen möchten, planen Sie bitte Ihre Ankunft auf den Malediven bis 15:30 Uhr.
Wenn Sie keinen internationalen Flug finden, der Sie rechtzeitig nach Malé bringt, können Sie auch eine Nacht in Malé oder Hulhumale verbringen und das Leben der Einheimischen kennenlernen.
</p>
<h4>Rückflug</h4>
<p>
Bei der Planung des Rückfluges nach Deutschland (Österreich oder der Schweiz) empfehlen wir, darauf zu achten, dass diese Flüge erst nach 9 Uhr von Malé aus starten,
um eine nahtlose Abreise vom Resort am selben Tag zu gewährleisten. 
Generell sind von den meisten Airlines die Flüge so ausgelegt, dass eine vernünftige An- und Abreise gewährleistet ist.
Allerdings kann es immer wieder zu  Verspätungen kommen. Es ist daher ratsam, immer ein wenig Zeit einzuplanen.
</p>
</div>
</li>


<li>
<header class="title-panel"><h3>Gepäckregelung</h3></header>
<div class="collapse">
<h4>Freigepäck – Übergepäck</h4>
<ul>
<li>Alle Transfers beinhalten 20 kg Freigepäck + 5 kg Handgepäck pro Passagier. <br>
Das Handgepäck darf die folgenden Maße nicht überschreiten: 50,8 x 30,5 x 17,8 cm.</li>
<br>
<li>Übergepäck wird mit ca. 2 bis 8 US-Dollar pro Kilogramm, zzgl. 12 % GST. berechnet. Die Fluggesellschaft TMA berechnet 5 US-Dollar pro Kilogramm.
Übergepäck muss vor Ort direkt bezahlt werden.</li>
<br>
<li>Es besteht kein Anspruch darauf, dass das Gepäck im selben Flug transportiert wird wie der Gast.
Abhängig von der Verfügbarkeit und in Übereinstimmung mit der Zivilluftfahrtbehörde der Malediven, kann das Gepäck mit einem Schnellboot transportiert werden.
Es wird noch am selben Tag im Resort eintreffen.
</li>
<br>
<li>Kinder unter 2 Jahren fliegen kostenlos. Es gibt kein Sitzplatzanspruch, demnach gibt es auch kein Freigepäck welches geltend gemacht werden kann.
Gepäck von Kindern unter 2 Jahren wird daher als Übergepäck behandelt.</li>
<br>
</ul>
</div>
</li>

<li>
<header class="title-panel"><h3>Wartezeiten / VIP Lounge</h3></header>
<div class="collapse">
<h4>Wartezeiten</h4>
<p>
Die Wartezeit am Flughafen bis man ins Resort befördert wird, kann bis zu 2 Stunden oder länger dauern.
Jedes Hotel ist bemüht die Kunden so schnell wie möglich ins Resort zu befördern.
Dennoch muss man auf Flüge von anderen Gästen warten.
Auch Wetterverhältnisse spielen oft eine Rolle, wenn es zu längeren Wartezeiten kommt.
</p>
<h4>Terminals</h4>
<p>
TMA verfügt über drei Terminalgebäude am Velana International Airport.
</p>

<ul>
<li>
<h4>Terminal A</h4>
<p>Ein komfortables, modernes, dreistöckiges Wartegebäude für Passagiere mit zwei Aufzügen.
Es verfügt über Satellitenfernsehen, kostenloses Wi-Fi, Klimaanlage, ein Echtzeit-Informationssystem für den Abflug und spezielle Lounges für Urlaubsgäste.</p>
</li>
<li>
<h4>Terminal B</h4>
<p>Besteht aus speziellen VIP-Lounges der Urlaubsresorts und einer gemeinsamen TMA-VIP-Lounge.</p>
</li>
<li>
<h4>Terminal C</h4>
<p>Besteht aus einem großen Abfertigungsbereich mit direktem Zugang zur Außenterrasse und den offenen Gates.
Außerdem gibt es hier ein Café und einen Souvenirladen, in dem die Passagiere essen und einkaufen können, während sie auf ihren Flug warten, sowie drei spezielle Lounges.
</p>
</li>
</ul>
<h4>VIP-Lounge</h4>
<p>Wenn man mit dem Wasserflugzeug fliegt, kann man entweder die Lounge des Resorts nutzen oder die Lounge der Wasserfluggesellschaft TMA.
An den TMA-Terminals finden die Reisenden spezielle VIP-Lounges für ausgewählte Resorts und eine gemeinsame TMA-VIP-Lounge, in der sie sich in höchstem Komfort entspannen können.
Lesen Sie ein Buch, erfrischen Sie sich mit einem kühlenden Handtuch, trinken Sie ein heißes/kaltes Getränk und genießen Sie einen Snack,
während Sie auf den unglaublichsten Teil der Reise warten - einen Flug über einige der schönsten Landschaften der Welt.
</p>
</div>
</li>



<li>
<header class="title-panel"><h3>Den Hoteltransfer buchen </h3></header>
<div class="collapse">
<h4>Wenn Sie Ihr Hotel bei einem anderen Anbieter gebucht haben</h4>
<p>
Die Hoteltransfers mit dem Wasserflugzeug werden immer vom Resort organisiert.
Wenn Sie Ihr Hotel bei einem anderen Anbieter gebucht haben, wenden Sie sich an diesen Anbieter.
Kann dieser Ihren Transfer nicht organisieren, müssen Sie sich an das Resort direkt wenden.
Hierzu müssen Sie anrufen und / oder eine E-Mail schreiben. Die Kommunikation wird in Englisch geführt.
</p>
<h4>Wir buchen den Transfer für Sie</h4>
<p>
Wenn Sie Ihr Hotel bei einem anderen Anbieter gebucht haben und Sie wollen sich nicht um den Transfer kümmern können Sie uns damit beauftragen, den Transfer für Sie
zu organisieren. Wir berechnen 25 € pro Person (inkl. 19 % MwSt.).
</p>
<p><a href="https://www.my-maldives.com/hotel-transfer/transferbuchung" class="link-arrow">Hier den Transfer buchen</a></p>
<p>&nbsp;</p>

<h4>Wenn Sie Ihr Hotel bei <b>Meine Malediven</b> gebucht haben</h4>
<p>
Wir bieten Ihnen auch immer den Transfer mit an. Am Ende der Hotelbuchung können Sie den Transfer auswählen.
Sollte das Hotel mehrere Transfermöglichkeiten anbieten, können Sie den gewünschten Transfer hinzufügen.
</p>
<h4>Flugdetails</h4>
<p>Wenn wir von Sie beauftragt werden den Hoteltransfer für Sie zu buchen oder wenn Sie das Hotel bei uns gebucht haben, benötigen wir Ihre Flugdetails.
Das ist nicht nötig, wenn wir Ihnen einen Flug vermittelt haben.
In der Regel benötigen wir Ihre Flugdetails spätestens ca. 7 Tage vor Anreise.
 Wenn Sie Ihre Flugdaten nicht oder zu spät übermitteln, kann entweder Ihr Transfer nicht wie geplant durchgeführt werden oder es kommt zu erheblichen Wartezeiten am Flughafen in Malé.
Ihre Flugdetails können Sie hier an uns übermitteln:
</p>
<p><a href="https://www.my-maldives.com/flugdetails-uebermitteln" class="link-arrow">Übermitteln Sie hier Ihre Flugdetails</a></p>
</div>
</li>



<li>
<header class="title-panel"><h3>Anreise / Abreise Resortinsel</h3></header>
<div class="collapse">
<h4>Zeitunterschied</h4>
<p>
Einige Resortinseln haben eine Zeitverschiebung eingeführt. Es ist auf den Inseln 1 Stunde später als in Malé.
Das ist gut bei der Anreise, da es 1 Stunde später ist als in Malé und so der Check-in 1 Stunde früher möglich ist.
Es hat auch eine psychologische Wirkung bei der Abreise, da es eine Stunde später erscheint, als es in wirklichkeit in Malé ist.
</p>
<h4>Rückflug von der Insel nach Malé</h4>
<p>Die Flugzeiten für den Rücktransfer vom Resort nach Malé, werden dem Kunden am Vorabend der Abreise vom Resort mitgeteilt.</p>
</div>
</li>


<li>
<header class="title-panel"><h3>Flugstornierungen / Probleme beim Transfer</h3></header>
<div class="collapse">
<h4>Flugstornierung wegen schlechtem Wetter</h4>
<p>
Das Wasserflugzeug fliegt aus Sicherheitsgründen nicht bei schlechten Wetter, hierzu zählen vor allen starker Wind, Orkan sowie lokale Gewitter.
Es kann daher zu Ausfällen oder Verspätungen kommen.
Eine Haftung gegenüber der Fluggesellschaft oder dem Hotel ist in diesem Fall ausgeschlossen. Um sich davor abzusichern müssen Sie eine Reiseversicherung abschließen.
Sollte der Wettervohersage so sein, das an Ihrem Abreisetag das Wetter so schlecht ist, das Sie nicht fliegen können, werden Sie am Vorabend nach Malé oder Umgebung befördert, damit Sie wie geplant
Ihren Rückflug antreten können. Das Resort kümmert sich um eine Unterbringung in einem anderen Hotel.
</p>


<h4>Flugstornierung wegen Verspätung</h4>
<p>
	Wer seinen Flug nicht rechtzeitig wahrnehmen kann, weil zum Beispiel  der Flug aus Deutschland, Österreich oder der Schweiz Verspätung hat, wird mit der  „No-Show“ Regel konfrontiert.
	Das bedeutet der Hinflug wurde storniert, da man zum Zeitpunkt des Abfluges nicht anwesend war. 
	<br><br>
	In diesem Fall muss man 50 % des Flugpreises ein weiteres Mal bezahlen, um in das Hotel befördert zu werden.
	Wer Glück hat, kann das noch am selben Tag. Wenn der letzte Flug bereits weg ist, ist eine Weiterreise erst am nächsten Tag möglich. 
	Ist das Resort in der Nähe des Flughafens, könnte eventuell ein Transfer ins Hotel mit dem Schnellboot möglich sein.
	Die Mitarbeiter werden diese Möglichkeit prüfen.
</p>
 <h4>Flugstornierung + Hotelstornierung</h4>
<p>	Wer seinen Hotelaufenthalt storniert, muss auch sein Flug extra stornieren, ansonsten werden 100 % des Flugpreises berechnet. 
	Beachten Sie die Stornierungsbedingungen des Fluges, die andere sein können als die Stornierungsbedingungen vom Hotel.
</p>
<h4>Flugplanung</h4>
<p>	Planen Sie Ihre Flüge immer zeitlich mit ein wenig „Luft“. Man fährt keine U-Bahn mit festen Zeitfahrplänen.
	Es kann immer hier und da zu Verspätungen kommen, sei es doch andere Gäste, durch Gepäck, etwas wurde vergessen oder es ist was verloren gegangen.
	Oft muss man nicht mal selbst Schuld sein. Deshalb lieber eine Stunde mehr einplanen als alles zu „eng“ planen. 
	<br><br>
	Sie können sich darauf verlassen, dass sowohl das Transferunternehmen, das Hotel, als auch das Personal genügend Erfahrung haben und genau wissen, worauf es ankommt, damit Sie pünktlich zum Flughafen kommen.
	Schließlich wird der Transfer Tausende Male pro Woche durchgeführt.
</p>
<a href="https://www.my-maldives.com/probleme-beim-transfer" class="link-arrow">Welche Probleme beim Transfer auftreten könnten</a>
</div>
</li>


<li>
<header class="title-panel"><h3>Treibstoffzuschläge</h3></header>
<div class="collapse">
<h4>Erhöhung der Treibstoffzuschläge</h4>
<p>
Das Luftfahrtunternehmen kann Treibstoffzuschläge in Abhängigkeit von den Marktpreisen für Kerosin erheben.
Kunden werden in diesem Fall umgehend informiert. Der Aufpreis ist vor Ort in Bar zu bezahlen.
</p>
</div>
</li>


<li class="open">
<header class="title-panel"><h3>Kleinkinder</h3></header>
<div class="collapse">
<h4>Kleinkinder fliegen kostenlos</h4>
<p>
Kleinkinder unter zwei Jahren fliegen kostenlos.
Aufgrund gesetzlicher Bestimmungen wird für Kleinkinder kein eigener Sitzplatz zugewiesen.
Dementsprechend gibt es auch kein zusätzliches Freigepäck für das Kleinkind.
</p>
</div>
</li>
</ul>
