	<h1>Seenu Atoll <small>(Addu Atholhu)</small> </h1>

<p>
Das <b>Addu Atoll</b> auch bekannt als Seenu Atoll, ist das südlichste Atoll der Malediven und ca. 541 km von der Malé entfernt.
Die Stadt Addu City ist die zweitgrößte bewohnte Insel der Malediven und die einzige bewohnte Insel in Bezug auf die Bevölkerung, die den Status einer anderen Landeshauptstadt, wie die der Hauptstadt Male, bekommen könnte.
Die Größe des Atolls beträgt ca. 11,55 km x 14,29 km <small>(Länge x Breite)</small> Addu City hat fünf Bezirksinseln: Hithadhoo, Maradhoo, Maradhoo Feydhoo, Hulhudhoo und Meedhoo.
Diese Bezirke sind gut miteinander verbunden und alle natürliche Inseln.
Das Addu Atoll (Seenu Atoll) hat dazu noch einige unbewohnte Inseln.
<br><br>
Die Hauptstadt Insel ist <b>Hithadhoo</b>. 35.000 Einwohner leben im Atoll, von denen mehr als 18.000 aus Hithadhoo kommen.
Im Addu Atoll wird der Dialekt Addu bas gesprochen, was ziemlich ungewöhnlich ist gegenüber der offiziellen Sprache Dhivehi.
Der Dialekt hat einige Ähnlichkeiten mit der Sprache der Fua Mulaku, Mulaku bas. Die Inseln im Addu Atoll haben einzigartige Eigenschaften.
Kokospalmen sind fast überall in der Lage auf den Inseln zu wachsen. 
<br><br>
Es gibt kleine Seen, Feuchtgebiete und sumpfige Taro-Felder, die einzigartig auf den Inseln sind.
Weiße Seeschwalben bekannt als Dhondheeni sind eine weitere Besonderheit, da sie nur im Addu Atoll auf den Malediven vorkommen. 
<br><br>
Das Addu Atoll (Seenu Atoll) besitzt auch eine besonders reiche Wal- und Delfin Fauna sowie eine Vielzahl anderer Arten.
Den größten Einfluss auf die moderne Geschichte des Addu Atoll kam durch die ersten britischen Militärbasen, die im Zweiten Weltkrieg auf der Insel Gan gegründet wurde, als Teil der Abwehrkräfte im Indischen Ozean. 
</p>
<div class="banner"><img src="../../images/content-images/image-seenu-atoll.jpg" alt="Bild: Frau am Strand"></div>
<p>
1956 wurde ein britischer Royal Air Force Flugplatz als strategischer Vorposten im Kalten Krieg stationiert.
Die Station hatte rund 600, an Spitzenzeiten bis zu 3000 Mitarbeiter vor Ort. 
<br><br>
Sie bauten auch einen Verbindungsdamm zu den Inseln <b>Feydhoo</b>, <b>Maradhoo</b> und <b>Hithadhoo</b> für die lokalen Mitarbeiter.
<br><br>
1976 verließen die Briten die Malediven, aber viele ihrer Mitarbeiter, die gut Englisch sprachen und gute Erfahrung in der Arbeit für den Westen hatten, fanden meistens neue Aufgaben in der Hauptstadt Malé.
Einige engagierten sich als qualifiziert Mitarbeiter im Staats- und im Tourismussektor.
Die Regierung hat den Bewohnern ein Krankenhaus zur Verfügung gestellt für regionale Hilfen mit einer Rund-um-die-Uhr Betreuung.
<br><br>
Die Tourismusentwicklung in <b>Addu City</b> bekam nach den Präsidentschaftswahlen 2008 freie Möglichkeiten in die Tourismusbranche zu investieren.
Der vierte Präsident der Malediven, Mohamed Nasheed, gab Bürgerinnen und Bürger vom Addu Atoll und ausländischen Investoren auf lokalen Inseln die Möglichkeit, Gästehäuser, Hotels, Tauchbasen, Wassersportzentren und anderen Tourismus bezogenen Unternehmen zu gründen.
Das erste Gästehaus <b>Stellar Maradhoo</b> eröffnete 2013.
Die erste Tauchbasis Aquaventure 1 und die ersten internationalen Touristen Dive Center aus Belgien haben im Mai 2013 auf der lokalen Insel Maradhoo eröffnet.
Im November 2011 fand das 17. SAA RC-Gipfel statt.
Gastgeber waren <b>Addu City</b> und die Nachbarinsel <b>Fuvahmulah</b>.
Die Planung für den Gipfel brachten neue Entwicklungen in die Stadt, darunter den Bau eines Kongresszentrums, Ausbau von Straßen und öffentlichen Plätzen, die Renovierung des Flughafen Gan International Airport, dem regionalen Krankenhaus, und mehr Beschäftigungsmöglichkeiten. 
<br><br>
Der Flughafen <b>Gan International Airport</b> von <b>Addu City</b> befindet sich auf nächstliegenden Insel <b>Gan</b>.
Der Flughafen wurde nach internationalen Standards für internationale Flüge erweitert, in Erwartung mehr Touristen in der Umgebung zu befördern.
</p>

