<h1>Noonu Atoll <small>(Miladhunmadulu Dhekunuburi)</small></h1>
<p>
Das <b>Noonu Atoll (Miladhunmadulu Dhekunuburi)</b> ist ein Verwaltungsatoll der Malediven und umgrenzt den südlichsten Teil des Miladhunmadulu Atoll.
Die Größe des Atolls beträgt ca.43 km x 40 km <small>(Länge x Breite)</small>.
Der Kanal westlich von diesem Atoll heißt <b>Baraveli Kandu</b>.
Baraveli bedeutet so viel wie Einsiedlerkrebs in lokalen Sprache Dhivehi.
<br><br>
Die Hauptstadt Insel im Noonu Atoll ist <b>Manadhoo</b>.
Die Gesamtbevölkerung im Atoll liegt bei ca. 14500 und besteht aus 68 Inseln, von denen 13 bewohnt sind.
<b>Manadhoo</b> ist die Hauptstadt Insel. Sie ist ca. 92,2 Hektar groß und umgeben von einem Hafen mit natürlichen Deichen.
Die Insel liegt im Südosten des Atolls.
Unter allen Inseln des Atolls hat Manadhoo die drittgrößte Bevölkerungsdichte und viele Regierungsbüros haben sich auf der Insel etabliert.
<br><br>
Die Bewohner des Atolls verdienen Ihren Lebensunterhalt mit Bau, Tischlerarbeiten, öffentlicher Dienst, Tourismus, Fischerei und der Privatwirtschaft.
Allerdings ist die Haupteinnahmequelle weiterhin die Fischerei.
</p>
<div class="banner"><img src="../../images/content-images/image-noonu-atoll.jpg" alt="Bild: Sonnenuntergang"></div>
<p>
Es gibt 8 Resortinseln in Noonu Atoll, darunter das etablierte Luxushotel <b>Cheval Blanc Randheli</b> von LVMH Hotelmanagement.
Das 45 Villen Hotel liegt im Noonu Atoll und verbindet modernes Design mit der natürlichen Umgebung der Insel.
Die üppige Vegetation und der Blick auf die Lagune tun ihr Übriges. Einige Hotelinseln sind noch im Bau und werden bald eröffnet.
Malé ist ca. 165 km entfernt.
</p>
