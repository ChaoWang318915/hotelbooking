<h1>Haa Dhaalu Atoll <small>(Thiladhunmathi Dhekunuburi)</small></h1>
<p>
<b>Haa Dhaalu</b> (Thiladhunmathi Dhekunuburi) ist der südliche Abschnitt des <b>Thiladhunmathi Atolls.</b>
Das Haa Dhaalu Atoll liegt im Süden vom <b>Haa Alifu Atoll</b> das sich im Norden der Malediven befinden.
Die Entfernung vom Haa Dhaalu Atoll nach Male beträgt ca. 276 km.
Das Atoll hat eine Größe von ca. 74,6 km x 48 km. <small>(Länge x Breite)</small>.
</p>
<div class="banner"><img src="../../images/content-images/image-haa-dhaalu.jpg" alt="Bild: Beach"></div>
<p>
Das <b>Haa Dhaalu Atoll</b> besteht aus 33 Inseln, von denen 16 bewohnt und 16 Inseln unbewohnt sind.
Die Hauptstadtinsel des Atolls ist <b>Kulhudhuffushi.</b>
Insgesamt leben ca. 23.875 Personen im Atoll, wovon Kulhudhuffushi mit 9500 Personen die Mehrheit der Bevölkerung des Atolls ausmacht.
Es gibt einen nationalen Flughafen auf der Insel <b>Hanimaadhoo</b> ca. 15 km von der Hauptstadt Kulhudhuffushi entfernt.
Vor Kulhudhuffushi war bis 6. Mai 1992 die Insel Nolhivaranfaru die Hauptstadtinsel des Atolls.
</p>

