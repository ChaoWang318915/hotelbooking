<h1>Alifu Dhaalu Atoll <small>(Ari Atoll Dhekunuburi)</small></h1>

 <p>
	 Das<b> Süd-Ari-Atoll</b> (auch als <b>Alifu Dhaalu</b> bekannt)</small> ist der südliche Teil des <b>Ari Atolls</b>.
	 Das Süd-Ari-Atoll wurde verwaltungstechnisch vom Nord Ari Atoll getrennt.
	 Aufgrund seiner malerischen Landschaften und schillernden Unterwasserwelt mit den schönsten Tauchplätzen, insbesondere für <b>Walhaie</b>, <b>Haie und Mantas</b>, ist das Atoll ein beliebtes Urlaubsziel auf den Malediven.
<br><br>
Das <b>Alifu Dhaalu Atoll</b> liegt südlich der Grenze zwischen den Kanälen <b>Himendhoo Dhekunukandu</b> und <b>Genburugau Kandu</b>.
Es leben etwa 9.396 Einwohner im Atoll. Insgesamt gibt es 92 Inseln im Süd Ari Atoll, von denen 9 bewohnt und  67 unbewohnt sind.
Die Hauptstadtinsel des Atolls ist <b>Mahibadhoo</b>. Die Entfernung zu Malé beträgt ca. 75 km und die Atoll Größe ist ca. 46 km x 29 km <small>(Länge x Breite)</small>.
<br><br>
Auf der Insel Mahibadhoo befinden sich ein Krankenhaus, das Atoll Ausbildungszentrum, Polizeirevier, Mahibadhoo Sozialzentrum, Bank of Maldives Mahibadhoo, eine traditionelle Moschee, Masgid al-Ihsan Moschee und ein Fußballplatz.
Die Insel hat 2.080 Einwohner, die ihren Lebensunterhalt mit Angeln, Bootsbau, Bau, Kunsthandwerk, Tischlerei und dem Tourismus verbundene Arbeiten bestreiten.
Die große Mehrheit der männlichen Jugend arbeitet in der Nähe von Resortinseln.
Einnahmen werden daher meistens durch den Tourismus generiert.
Weitere Dienstleistungen wie Schnellboot Vermietung, Hotel- und Catering-Service, Wireless Internet-Anschluss, Bankgeschäfte, Geldautomat und Kabel-TV-Dienste gehören ebenfalls dazu.
Die Einheimischen sind sehr geschickt in der Fischerei und Haijagd.
</p>
<div class="banner"><img src="../../images/content-images/image-alifu-dhaalu-atoll.jpg" alt="Paar am Strand"></div>
<p>
Das Alifu Dhaalu Education Centre ist die größte Schule im Atoll.
Die schulische Ausbildung umfasst die Klasse 1 bis 5, die Sekundarschulbildung die Klassen 6 bis 10 und die Hochschulbildung bis zur elften und zwölften Klasse.
Zudem gibt es ein Bildungszentrum für Wirtschaftslehre.
<br><br>
Im Atoll Krankenhaus werden verschiedene Dienstleistungen wie Bluttransfusionen, EKG, Laboruntersuchungen, Gynäkologie, Röntgen und Ultraschall angeboten.
<br><br>
Die zahlreichen Tauchplätze im Süd Ari Atoll sind weltberühmt unter Tauchern für<b> Walhaie</b> und berühmt, die eine vielfältige atemberaubende Unterwasserwelt erleben und fotografieren wollen.
Das Alifu Dhaalu Atoll verfügt über <b>24 Tauchspots</b>.
Einige davon sind perfekt, um Walhaie zu beobachten. Der beste Ort, um Walhaie zu beobachten, ist der Süden vom Alifu Dhaalu Atoll.
An insgesamt 7 Spots können Sie mit ihm tauchen. Wir empfehlen den Dezember. Die Tiere sind jedoch ganzjährig zu sehen.
Am besten in der Zeit von Anfang August bis Ende November.
</p>
