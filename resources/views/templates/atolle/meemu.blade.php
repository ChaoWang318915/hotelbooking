<h1>Meemu Atoll <small> (Mulaku Atoll)</small></h1>
<p>
Das <b>Meemu Atoll</b> ist eine Verwaltungseinheit der Malediven.
Das natürliche Atoll ist auch als <b>Mulak Atoll</b> oder <b>Mulakatolhu Atoll</b> bekannt.
Am Ende des südlichen Riffs im Meemu Atoll, führt ein berühmter Kanal mit dem Name <b>Vattaru Kandu</b>.
Die Hauptstadt Insel <b>Muli</b> war eine der Inseln im Indischen Ozean, die durch den Tsunami 2004 betroffen waren.
<br><br>
Das Atoll verfügt über insgesamt 33 Inseln, darunter sind 9 Inseln bewohnt und 24 Inseln unbewohnt.
6459 Einwohner leben im Atoll. Das Meemu Atoll hat zwei Resortinseln und weitere vier Resortinseln befinden sich im Aufbau.
</p>
<div class="banner"><img src="../../images/content-images/image-meemu-atoll.jpg" alt="Bild: Altes Schiff am Strand"></div>
<p>
Auf der Hauptstadtinsel des Atolls befindet sich das <b>Roashanee Guest House</b>.
Das <b>Gästehaus </b> verfügt über eine Reihe von Annehmlichkeiten wie Tagungsräume, Familienzimmer und eine Café-Bar. 
Außerdem gibt es ein Schalter für Touren, Baby- / Kinderbetreuung und ein Fahrradverleih.
<br><br>
Die Gäste können die ganze Insel umrunden und so das Leben der Einheimischen kennenlernen.<br>
Im Meemu Atoll sind einige Inseln von historischer Bedeutung. So ist eine Moschee von historischem Wert auf der Insel Kolhufushi zu finden
<br><br>
Auch von historischer Bedeutung ist das Wrack Kalhuohfummi von 1573 am südöstlichen Riff vor der Insel Kolhuvaariyaafushi.
</p>

