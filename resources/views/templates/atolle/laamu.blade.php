<h1>Laamu Atoll <small>(Haddhunmathi)</small></h1>
<p>
Das <b>Laamu Atoll</b> (Haddhunmathi) ist ein Verwaltungsbezirk der Malediven und ist größtenteils von Korallenriffen umrandet.
Entlang der östlichen und südlichen Grenze gibt es sehr viele Inseln, bewohnte wie unbewohnte.
Das Atoll hat 14.313 Einwohner und hat eine Größe von 29 km x 35 km <small>(Länge x Breite)</small>.
Die Entfernung zu Malé beträgt ca. 229 km. Die Hauptstadtinsel des Atolls ist <b>Fonadhoo</b>, auf der rund 1.773 Einwohner leben.
<br><br>
Aufgrund seiner typischen Atoll Form unterscheidet sich das Atoll in nichts von den Atollen im Pazifischen Ozean, abgesehen von seiner größeren Größe.
Die Riffe sind in der Regel von geringer Breite.
Die Lagune ist vergleichsweise offen, der Boden schlammig mit einer durchschnittlichen Tiefe von 55 bis 62 Meter.
Das Laamu Atoll bildet die südliche Grenze im Zentrum der Malediven.
Der <b>Huvadhu Kandu Kanal</b> ist der breiteste Kanal zwischen den Atollen der Malediven und trennt die nördlichen Atolle vom Zentrum der Malediven sowie die südlichen Atolle.
</p>
<div class="banner"><img src="../../images/content-images/image-laamu-atoll.jpg" alt="Bild: Pärchen auf einer Sandbank"></div>
<p>
Viele der Inseln am östlichen Riff des Laamu Atolls liegen an wichtigen archäologische Stätten wie <b>Dhanbidhoo</b>, <b>Mundoo</b>, <b>Gan Island</b> und <b>Isdhoo</b>.
Es sind Überreste von Klöstern, Viharas und buddhistische Bauwerke von großem Ausmaß.
Ein Buddha Kopf ist während seiner Ausgrabung in diesem Atoll verschwunden.
<br><br>
Die Insel <b>Gan Island</b> im Laamu Atoll ist die größte Insel im Archipel, bestehend aus den längsten Stränden der Malediven (7,8 km lange, weißen Sandstrand).
Gan hat einen Süßwassersee in der Mitte der Insel (der Red Lake, sogenannt wegen seiner roten Farbe von Baumblättern, die ins Wasser fallen).
Die Insel Gan Island ist die am weitesten entwickelte Insel im Atoll und besitzt auch einige alte buddhistische Ruinen.
<br><br>
Der Inlandsflughafen der Insel ist <b>Kadhdhoo KDO</b> und sollte nicht mit Kaadedhdhoo-KDM verwechselt werden.
Die Inseln Gan nach <b>Funadhoo</b> (einschließlich der Flughafeninsel) sind durch Brücken miteinander verbunden.
</p>

