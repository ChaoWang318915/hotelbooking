<h1>Gaafu Dhaalu Atoll <small>(Huvadhu Atholhu Dhekunuburi)</small></h1>
<p>
Das <b>Gaafu Dhaalu Atoll </b> <b>(Huvadhu Atholhu Dhekunuburi)</b> entstand am 8. Februar 1962 als das <b>Huvadhu Atoll</b> in zwei Bezirke unterteilt wurde.
Das Atoll befindet sich ca. 385 km südlich von der Hauptstadt <b>Malé</b> entfernt.
Es zählt zum südlichen Abschnitt dieses riesigen Atolls und wird südlich durch die Kanäle <b>Footukandu</b> und <b>Vaarulu Kandu</b> flankiert.  
<br><br>
Das Atoll besteht aus 153 Inseln, davon  10 bewohnte und 143 unbewohnte Inseln. Die Größe des Atolls beträgt ca.	63 km x 61 km <small>(Länge x Breite)</smalL>
<b>Thinadhoo</b> ist die Hauptstadtinsel des Atolls.
Die Gesamtbevölkerung beträgt 18.485 Einwohner, ein Großteil der Bevölkerung <small>(ca. 10.000 Einwohner)</smalL> ist auf der Insel <b>Thinadhoo</b> ansässig.
</p>
<div class="banner"><img src="../../images/content-images/image-gaafu-dhaalu.jpg" alt="Bild: Strand auf den Malediven"></div>
<p>
Drei der südlichen Atolle, Huvadhu, <b>Fuvahmulah</b> und <b>Addu,</b> waren im Januar 1959 beim Aufbau einer abtrünnigen Republik mit dem Namen <b>Suvadiva</b> beteiligt, die aber nur bis September 1963 überdauerte.
2004 kenterte das Boot Enamaa weil es mehr Gewichte transportierte als seine Kapazität es zuließ. Zwanzig Menschen starben bei dem Unglück, weil zwei Rettungsboote fehlten.  
<br><br>
2006 hat das Tourismusministerium der Malediven die Resortinsel <b>Vatavarrehaa</b> für den Tourismus geöffnet.
Der <b>Inlandsflughafen Kaadedhdhoo</b>, ehemals eine unbewohnte Insel, befindet sich auf Kaadedhdhoo und wurde 1993 eröffnet. 
</p>
