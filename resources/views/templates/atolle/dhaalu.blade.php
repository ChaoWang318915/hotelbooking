<h1>Dhaalu Atoll <small>(Nilandhe Atoll Dhekunuburi)</small></h1>
<p>
Das <b>Dhaalu Atoll</b> (Nilandhe Atoll Dhekunuburi) liegt im Süden der Malediven.
Das Atoll erstreckt sich 23 km von Ost nach West und 38 km von Nord nach Süd.
Es besteht aus 44 Inseln, von denen 7 bewohnt und 26 Inseln unbewohnt sind, weiterhin gibt es 11 Resortinseln.
Das Atoll hat eine Gesamtbevölkerung von ca. 6694 Einwohnern. Die Entfernung zu Malé beträgt ca. 143 km.
Die Hauptstadtinsel <b>Kudahuvadhoo</b> hat ca. 2300 Einwohner und liegt an der Südspitze des Atolls.
Auf der Insel befindet sich auch ein Krankenhaus.
<br><br>
Das <b>Dhaalu Atoll</b> ist berühmt für seine Handwerker, hauptsächlich für seine Juweliere.
Die Malediven waren einst berühmt in der arabischen Welt für die Herstellung feinster muslimischer Grabsteine.
Schöne Moscheen mit antiken Skulpturen aus Stein beweisen die exquisiten Fähigkeiten der Steinmetze des Atolls.
</p>
<div class="banner"><img src="../../images/content-images/image-dhaalu.jpg" alt="Bild: Tropical Island"></div>
<p>
Einige der Inseln in diesem Atoll haben Überreste aus der buddhistischen Zeit, aber bisher hat sich keiner um die Ausgrabungen gekümmert.
Geheimnisvolle Hügel, auch als Hawittas bekannt, sind auf der Insel Kudahuvadhoo gefunden worden.
Diese Hügel sind Ruinen buddhistischer Tempel aus der vorislamischen Zeit (vor dem 10. Jahrhundert), die noch nicht ausgegraben wurden.
<br><br>
Die alte Moschee auf der Insel Kudahuvadhoo besitzt eines der schönsten Mauerwerke des Landes.
Thor Heyerdahl, der die Insel in den frühen 1980er erforschte, schrieb das die alte Korallen-Steinmoschee auf Kudahuvadhoo das schönste Mauerwerk besitzt, dass er je in der Welt gesehen hat.
<br><br>
1998 wurde das Atoll für den Tourismus geöffnet und hat sich zu einem beliebten Tauchgebiet entwickelt.
Einige Hotelinseln im Dhaalu Atoll sind; <b>Angsana Velavaru Resort</b>  und das <b>Niyama Private Islands.</b>
</p>
