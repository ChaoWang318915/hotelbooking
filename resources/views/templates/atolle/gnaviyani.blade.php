<h1>Gnaviyani Atoll</h1>
<p>
<b>Gnaviyani</b> ist der kleinste Verwaltungsbezirk der Malediven, der sich parallel zum natürlichen Atoll <b>Fuvahmulah</b> befindet.
Fuvahmulah ist ein Ein-Insel Atoll. Die Insel ist etwa 4,5 km x 1,5 km groß mit einem Unterwasserriff (Rashikedefaro) von etwa drei Kilometer Länge. 
Bis nach  Malé sind es ca. 496 km. Das Atoll hat ca. 14.140 Einwohner.
Die Bewohner sprechen eine charakteristische Form der Dhivehi, bekannt als Mulaku bas. 
<br><br>
Die Insel ist Hauptproduzent von Mangos, Ananas und Orangen, die nirgend woanders auf den Malediven gefunden werden.
In den Feuchtgebieten von Fuvahmulah gibt es verschiedene Arten von Farnen. Das Schilf wird traditionell zum Weben für Matten verwendet, die ihren Nutzen auf den Feldern finden.
Auf Fuvahmulah besitzen Einheimische auch Ackerland auf denen Sie verschiedene  Gemüsesorten wie Gurken, Tomaten, Karotten, Kohl, Pfeffer, Paprika und Auberginen anbauen. 
</p>
<div class="banner"><img src="../../images/content-images/image-gnaviyani.jpg" alt="Bild: junges Mädchen aus den Malediven"></div>
<p>
Am nördlichsten Punkt der Insel gibt es einen breiten Sandstrand, <b>Thundi</b> genannt.
Dieser Sandstrand besteht aus weißen kleinen runden Kieselsteinen, die außergewöhnlich glatt und glänzend sind.
Diese Art von Kieselsteinen finden sich nur in den Malediven. 
<br><br>
Ein jährliches Phänomen namens Bissaaveli bilden ein Thundi. 
Die Bissaaveli Formen bilden eine neue Lagune, wenn ein Teil des Sandstrandes sich von der Küste weg bewegt und sich an der Grenze des Riffs ablagert.
Das ist nur auf der Insel Fuvahmulah zu beobachten. 
<br><br>
<b>Kalho-Akiri</b> (was schwarzer Kiesel bedeutet) findet man an der Südost-Küste der Insel.
Nur hier findet man schwarze Kiesel, was ziemlich bemerkenswert ist, zu den sonst üblichen weißen Korallensandstränden der <b>Malediven</b>.
</p>

