<h1>Gaafu Alifu Atoll <small>(Huvadhu Atholhu Uthuruburi)</small> </h1>

<p>
Das <b>Gaafu Alifu Atoll</b> (Huvadhu Atholhu Uthuruburi) ist ein Verwaltungsbezirk der Malediven.
Gaafu Alifu wurde am 8. Februar 1962 angelegt als das Huvadhu Atoll in zwei Bezirke unterteilt wurde.
In diesem großen natürlichen Atoll liegen im nordöstlichen Abschnitt <b>Gaafu Alifu</b> und <b>Gaafu Dhaalu</b>, in nördlicher Linie zwischen den Kanälen <b>Footukandu</b> und <b>Vaarulu Kandu</b>.
Die Hauptstadtinsel ist <b>Vilingili</b>.
Die Gesamtbevölkerung des Atolls beträgt 12.116 Einwohner.
Das Atoll hat eine Größe von 69 km x 21 km <small>(Länge x Breite)</small>.
Es gibt insgesamt 82 Inseln, darunter 9 bewohnte und 67 unbewohnte Inseln. Die Entfernung zu Malé beträgt ca. 336 km. 
</p>
<div class="banner">	<img src="../../images/content-images/image-gaafu-alifu.jpg" alt="Bild: 2 Delfine"></div>
<p>
Haupteinnahmequelle im Atoll ist die Fischerei.
Die Fischer von der Hauptstadtinsel Vilingili schmücken ihre Fischerboote mit abwechslungsreichen und dekorativen Mustern.
Am 10. September 2012 wurde der <b>Kooddoo Inlandsflughafen</b> auf der gleichnamigen Insel eröffnet (GKK).
<br><br>
Das Gaafu Alifu Atoll verfügt über 6 luxuriöse Resortinseln. Weitere Resortinseln befinden sich im Bau.
Die Inseln im Atoll sind reich an Naturschönheiten und bieten den perfekten Ort für Ruhesuchende.
</p>

