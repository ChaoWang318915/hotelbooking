<h1>Shaviyani Atoll <small>(Miladhunmathi Uthuruburi)</small></h1>
<p> Das <b>Shaviyani (Miladhunmathi Uthuruburi)</b> ist das 3. Atoll am nördlichen Rand der Malediven, es ist ca. 60 km lang.
Das Atoll besteht aus 51 Inseln, von denen 14 bewohnt und 35 Inseln unbewohnt sind.
Es gibt neun Inseln, die kleiner als ein Hektar sind. 
Die Größe des Atolls beträgt ca.	53,5 km x 43 km <small>(Länge x Breite)</small>.
Die Hauptstadtinsel des Atolls ist <b>Funadhoo</b>.
Fischerei und Landwirtschaft ist die Haupteinnahmequelle auf dem Atoll.
Die Gesamtbevölkerung im ganzen Atoll beträgt 16.542 Personen, Stand April 2012.
</p>
<div class="banner"><img src="../../images/content-images/image-shaviyani-atoll.jpg" alt="Bild: "></div>
<p>
Eine der historischen Inseln des Atolls ist <b>Maroshi.</b>
Die Insel war bekannt dafür, dass Bodu Thakurufaanu (Muhammad Al Thakurufaanu Auzam) das Schiff Kalhuohfummi in der Nacht hier versteckte während des Kampfes mit den Portugiesen.
<br><br>
Eine medizinische Grundversorgung wurde im Atoll mit der Errichtung eines Gesundheitszentrums eingeführt.
Aus dem Gesundheitszentrum wurde ein Atoll Krankenhaus, das ein gut ausgestattetes Labor und OP-Einrichtungen hat und von einem Frauenarzt, Anästhesist und Allgemeinarzt besetzt ist.
Neben dem Atoll Krankenhaus gibt es 13 Gesundheitszentren im Atoll mit Ärzten und Krankenschwestern.
<br><br>
Eine Grundschule gibt es auf allen bewohnten Inseln im <b>Shaviyani Atoll</b>.
Alle Schulen im Atoll bieten Ausbildungen bis zur 10. Klasse und im Atoll Bildungszentrum wird bis zur 12. Klasse unterrichtet.
Die Entfernung nach Malé beträgt ca. 222 Kilometer.
</p>


