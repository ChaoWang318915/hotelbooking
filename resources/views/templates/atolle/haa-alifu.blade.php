  <h1>Haa Alifu Atoll <small> (Thiladhunmathi Uthuruburi) </small></h1>
  <p>
  <b>Das Haa Alifu Atoll</b> <small>(offiziell auch Thiladhunmathi Uthuruburi)</small> ist das nördlichste und das drittgrößte Atoll der Malediven.
  Es liegt Sri Lanka und Indien am nächsten. 
  <br>Die Hauptstadtinsel des Atolls ist <b>Dhidhdhoo</b>.
  Mit 2.651 Einwohnern ist Dhidhdhoo auch die am dicht besiedelste Insel im <b>Haa Alifu Atoll</b>. 
  <br><br>
  Das <b>Haa Alifu Atoll</b> ist rund 303 km von Malé entfernt und hat eine Größe von ca.	31 km x 53 km.
  Das Atoll besteht aus 45 Inseln, davon 15 bewohnte und 30 unbewohnte Inseln, es gibt 1 Resortinsel im Atoll.
  Die Insel mit dem größten pro Kopf Einkommen in diesem Atoll ist Ihavandhoo. 
  </p>
  <div class="banner"><img src="../images/content-images/image-haa-alifu.jpg" alt="#"></div>
  <p>
  Eine der historischen Inseln im Haa Alifu Atoll ist <b>Utheemu</b>, der Geburtsort von Mohammed Al Thakurufaanu Auzam, einer der Helden der Malediven, wenn nicht gar der Held.
  Er regierte die Malediven von 1573 bis 1585 und besiegte 1573 die Portugiesen und gewann so die Unabhängigkeit der Malediven.
  Der Utheemu Dynastie folgten 3 weitere Sultane, die alle aus Utheemu kamen und deren Herrschaft bis 1692 andauerte. 
  Utheemu, <b>Thakandhoo </b>und <b>Baarah</b> sind die drei historischen Inseln im Atoll. 
  <br><br>
  Für Besucher gibt es zurzeit zwei Hotels in diesem Atoll: <br>
  <b>Hideaway Beach Resort & Spa</b> auf Dhonakulhi und <b>Ja Manafaru</b> (ehemals Beach House Iruveli) auf <b>Manafaru.</b>
  Auf einigen Inseln im Atoll sind auch Gästehäuser buchbar. 
  Kelaa, Baarah, Filladhoo und Maafahi sind Inseln im Haa Alifu Atoll auf denen Obst und Gemüse angebaut werden.
  Viele Vogelarten haben hier eine Heimat gefunden. 
  <br><br>
  Es sind keine öffentlichen Seetransportsysteme wie Fährterminals oder große Schiffe vorhanden, die Einwohner von einer Insel zur anderen bringen.
  Die Einwohner reisen mit eigenen Booten oder mieten Boote an.
  Einige Einwohner verdienen ihren Lebensunterhalt mit dem Bootsverleih.
  Heutzutage werden häufig Schnellboote verwendet, obwohl es noch immer traditionelle Boote gibt. 
  </p>
