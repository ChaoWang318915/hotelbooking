<h1>Raa Atoll <small>(Maalhosmadulu Uthuruburi) </small></h1>
<p>
Das etwas abgelegene <b>Raa Atoll</b> (Maalhosmadulu Uthuruburi) ist etwa 170 km von Malé entfernt und ist ca. 56 km lang und ca. 24 km breit.
Die westlichen Inseln im Atoll bestehen aus einer Kette von kreisförmigen Riffen, eine Eigenschaft, die man in den größeren nördlichen Atollen findet.<br>
Die Mitte des Atolls ist mit dichten Korallenflecken durchzogen.
Das Atoll besteht aus 90 Inseln, von denen 15 bewohnt und 61 Inseln unbewohnt sind.
Im Atoll gibt es zur Zeit 15 Resortinseln.<br>
<br>
Die Gesamtbevölkerung besteht aus ca. 24.500 Einwohnern und die machen 5,4 % der gesamten Bevölkerung der Malediven aus.
Die Hauptstadtinsel des Raa Atolls ist <b>Ungoofaaru</b>.<br>
Hier befindet sich das Ungoofaaru Regional Hospital, das größten Krankenhaus in der Nordprovinz. 
</p>
<div class="banner"><img src="../images/content-images/image-raa-atoll.jpg" alt="Bild: Sonnenuntergang"></div>
<p>
Das <b>Raa Atoll</b> wurde erst Ende der 90er-Jahre für den Tourismus freigegeben, was durch einen Wechsel in der Regierungspolitik möglich wurde.
Die wichtigste touristische Resortinsel im nördlichen Maalhosmadulu Atoll ist Meedhupparu.
Die Haupteinnahmequellen für das Atoll sind Fischerei, Landwirtschaft und die Bildungsbranche.
Einen <b>Inlandsflughafen</b> gibt es auf der Insel <b>Ifuru (IFU)</b>, der von Maldivian Air angeflogen wird.
</p>

