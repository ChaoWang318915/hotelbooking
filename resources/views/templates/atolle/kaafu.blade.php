<h1>Kaafu Atoll <small>(Male’atholhu)</small></h1>
<p>
Das Atoll besteht aus dem <b>Kaashidhoo Island</b>, dem <b>Gahaafaru Atoll</b> und <b>Malé Atoll </b>(welches das Nord-Malé-Atoll und das Süd-Malé-Atoll umfasst) sowie einige unbewohnte Inseln.
Thulusdhoo ist die Hauptstadtinsel im Kaafu Atoll.
Die Inselbewohner von <b>Rinbudhoo</b> aus dem <b>Dhaalu Atoll</b> immigrierten nach <b>Thulusdhoo</b> wegen des verheerenden Tsunamis 2004.
Im Atoll gibt es 9.886 Einwohner und 96 Inseln, von denen 12 Inseln bewohnt und 42 Inseln unbewohnt sind.
<br><br>
Zurzeit gibt es 42 Resortinseln.
Einige Inseln des Atolls werden auch für kommerzielle Zwecke verwendet.
Die Hauptstadt der Malediven <b>Malé</b> befindet sich zwar im Nord-Malé-Atoll, ist aber nicht Teil der Verwaltungseinheit Kaafu.
Die Hotels im <b>Süd-Male-Atoll</b> liegen sehr nah an der Hauptstadt Insel <b>Malé</b> und dem <b>internationalen Flughafen Velana</b>.
Das <b>Kaafu Atoll</b> verfügt über 42 Hotels und ständig kommen weitere hinzu.
</p>
<div class="banner"><img src="../../images/content-images/image-kaafu-atoll.jpg" alt="Bild: Blick auf die türkisfarbende Lagune"></div>
<p>
Der Tourismus hat sich schnell auf den Inseln im <b>Kaafu Atoll</b> verbreitet.
Einige lokale Inseln verfügen über Gästehäuser.
Die <b>Gästehäuser</b> sind eine Initiative der Regierung der Malediven aus dem Jahr 2009, um das wirtschaftliche Wachstum der Malediven zu erweitern.
Einheimische verdienen ihren Lebensunterhalt mit den Gästehäusern auf ihren Heimatinseln.
So wird ein ausgeglichener Tourismus und eine bessere Einkommensverteilung im Atoll ermöglicht.
Zuvor wurden touristischen Einrichtungen nur auf unbewohnten Inseln als Resortinsel genutzt.
<br><br>
Die meisten der verwalteten Hotels sind im Besitz von einer Gruppe oder lokalen Geschäftsleuten sowie von ausländischen Firmen.
Als Ergebnis sind die wahren Vorteile des Tourismus nicht bis an die lokalen Bürger der Malediven vorgedrungen.
Das Kaafu Atoll der Malediven heißt Sie herzlich willkommen auch die umliegenden Inseln zu besuchen und zu erleben.
Aufenthalte in den Gästehäusern ermöglichen verschiedene Ausflüge wie Schnorcheln, Angeln, Wassersport, Einblicke in das traditionelle Leben der Malediven und bieten einzigartige Fischgerichte der Region.
Die Insel <b>Maafushi</b> ist eine bewohnte Insel, mit den meisten Gästehäusern und Gästebetten der Malediven.
<br><br>
Mehr als 20 Gästehäuser bieten zahlreiche Vorteile für die Wirtschaft als auch für Familien.
Die meisten dieser Gästehäuser sind mithilfe von Direktinvestitionen von Anwohnern der Insel finanziert.
Allerdings haben auch Geschäftsleute aus Malé und verschiedenen anderen Inseln hier investiert.
</p>

