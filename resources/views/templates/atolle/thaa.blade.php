<h1>Thaa Atoll <small>(Kolhumadulu)</small></h1>
<p>
Das <b>Thaa Atoll </b> (Kolhumadulu Atoll) entspricht dem natürlichen Atoll gleichen Namens.
Während der Zeit des Königs Koimala, der alle Atolle der Malediven zum ersten Mal unter einem Königreich vereinte, war der Name des Atolls  <b>Kolhumaduva</b>.
Die Verwaltungsbezirke der Malediven haben nicht die richtigen Namen der natürlichen Atolle.
Einige Atolle sind in zwei Verwaltungsbezirke aufgeteilt, während andere aus zwei oder mehr natürlichen Atollen bestehen.
Die Namen der Verwaltungsbezirke haben sie sich unter den Touristen  eingebürgert, da sie leichter auszusprechen und zu merken sind als der richtige Name des Atolls in Dhivehi.
Das gilt bis auf wenige Ausnahmen, wie beim Ari Atoll. 
<br><br>
Die Hauptstadtinsel im Thaa Atoll ist die große Insel Veymundoo, wo mehr als 1.000 Menschen leben. 
Insgesamt leben rund 13.846 Einwohner im Thaa Atoll. 
<br>
<div class="banner"><img src="../../images/content-images/image-thaa-atoll.jpg" alt="Bild Frau in der Hängematte"></div>
<br>
Das Thaa Atoll besitzt insgesamt 61 Inseln. Eine davon ist die Resortinsel <b>Maalifushi</b>. Weiterhin gibt es 13 bewohnte und 47 unbewohnte Inseln.  
Die Entfernung zu Malé beträgt ca. 186 km. Die Hauptbeschäftigung der Einwohner im Thaa Atoll ist die Fischerei.
Es ist naheliegend, dass die Hotels der Region einzigartige Fischgerichte anbieten.
Wenn Sie die benachbarten Fischerdörfer besuchen, können Sie vielleicht selbst einen Thunfisch, Barrakuda oder Red Snapper fangen. 
<br><br>
Das <b>Thaa Atoll</b> hat einen inländischen Flughafen auf der Insel <b>Thimarafushi</b>, erstellt von der Maledivian Transport and Contracting Company (MTCC).
Dadurch können sowohl Einwohner des Thaa Atolls, als auch die Bewohner der Nachbar-Atolle leichter die Hauptstadt Malé erreichen.
</p>

