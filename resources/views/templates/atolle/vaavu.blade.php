<h1>Vaavu Atoll <small>(Felidhe Atholhu)</small></h1>
<p>
	Das <b>Vaavu Atoll</b> (Felidhe Atholhu) ist eine Verwaltungseinheit der Malediven und umfasst die natürlichen <b>Atolle Felidhu</b> und das kleine eiförmige <b>Vattaru Reef Atoll</b> mit der kleinen Insel Vattaru.
	Das Vaavu Atoll ist das Bevölkerungsärmste Atoll. Im Atoll leben ca. 2.144 Einwohner. Die Hauptstadtinsel ist Felidhoo.
	Die Größe des Atolls beträgt ca. 52 km x 55 km <small>(Länge x Breite)</small>. 
	Die Entfernung zu Malé beträgt ca. 65 km.
	Ein Schnellboot benötigt etwa 90 Minuten während die Fähre 5 Stunden benötigt, um ins Vaavu Atoll zu kommen.
	Das Atoll besteht aus 21 Inseln, von denen 5 bewohnt sind und 12 Inseln unbewohnt sind.
	Es gibt vier Resortinseln im Vaavu Atoll, darunter das <b>Nakai Alimathà Resort</b> und das <b>Cinnamon Velifushi Maldives.</b> 
</p>
<div class="banner"><img src="../../images/content-images/image-vaavu-atoll.jpg" alt="Bild: Einsame Inseln"></div>
<p>
Die Inseln faszinieren mit idyllischer Atmosphäre und zeigen das entspannte Inselleben der Malediven.
Das Vaavu Atoll ist ein echtes geografische Wunder, gebildet aus einem stiefelförmigen Atoll.
Die „Zehe“ <b>Fotteyo Muli</b> ist der östlichste Punkt des Malediven Archipel.
Weiterhin erstreckt sich ein 55 Kilometer langes ununterbrochenes Riff von der „Zehe“ bis zur „Ferse“ und ist somit das längste Riff der Malediven.
<br><br>
Im <b>Vaavu Atoll</b> hat der Tourismus bereits 1975 begonnen.
Das Atoll ist sehr beliebt bei Safari- und Kreuzfahrtunternehmen.
Durch die etwas isolierte Lage wird das Atoll weniger kommerziell genutzt als andere touristische Atolle.
</p>

