<h1>Lhaviyani Atoll <small>(Faadhippolhu)</small></h1>

<p>
Das <b>Lhaviyani Atoll</b> (Faadhippolhu) ist ca. 35 × 36 km groß (Länge x Breite). Die Entfernung nach Male beträgt 120 km.
Insgesamt gibt es 54 Inseln im Atoll, von denen nur 5 bewohnt und 40 Inseln unbewohnt sind.
Die Hauptstadt Insel des Atolls ist <b>Naifaru</b>. 
</p>
<div class="banner"><img src="../../images/content-images/image-lhaviyani-atoll.jpg" alt="#"></div>
<p>
Zur Gesamtbevölkerung des Atolls zählen 11.900 Einwohner, von denen ein Großteil <small>(ca. 5.044 Bewohner)</small> auf der Insel Naifaru wohnt.
Traditionelle Malediver identifizieren das Atoll einfach mit <b>Faadhippolhu</b>, ohne Hinzufügen des Wortes Atholhu am Ende.
Im frühen 12. Jahrhundert während der Regierungszeit von König Koimala Siri Mahaabarana Mahaa Radun, dem ersten König der über die gesamten Inseln der Malediven regierte, hieß das <b>Lhaviyani Atoll</b> Faadu Bur.
<br><br>
Im Atoll befinden sich 9 Resortinseln, darunter das Kuredhu, Komandoo, Kanuhuraa und das Palmbeach.
Auf der Insel Felivaru gibt es eine Konservenfabrik, die einzige im Atoll.
<b>Felivaru</b> liegt zwischen <b>Naifaru</b> und <b>Hinnavaru</b>.
</p>

