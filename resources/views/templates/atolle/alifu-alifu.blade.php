<h1>Alifu Alifu Atoll <small>(Ari Atoll Uthuruburi)</small></h1>
<p>
Das nördliche <b>Ari Atoll</b> <small>(auch <b>Alifu Alifu Atoll</b></small> genannt), ist eine Verwaltungseinheit der Malediven.
Das Atoll wurde 1984 aus dem nördlichen Teil des <b>Ari-Atolls</b>, dem kleinen <b>Rasdhukuramathi Atoll</b> und der isolierten Insel <b>Thoddoo</b>, zu einem Atoll, dem <b>Alifu Alifu Atoll</b> zusammengelegt.
Durch die Unterteilung des <b>Ari Atolls</b> (früher Alifu Atoll) in einen nördlichen und einen südlichen Abschnitt wurde das Alifu Alifu Atoll und das <b>Alifu Dhaalu Atoll</b> gebildet.
<br><br>
Das Ari Atoll hat ca. 6.146 Bewohner und besteht aus 105 Inseln, 33 davon im Alifu Alifu Atoll.
Das Ari Atoll ist bekannt für den Tourismus der Malediven. 
<p>
<figure><img src="../../images/content-images/image-alifu-alifu.jpg" alt="Bild: Paar am Strand"></figure>
<p>
Die Hauptstadt <b>Malé</b> ist etwa 30 Minuten bzw. 57 km mit dem Wasserflugzeug entfernt.
Das Atoll erstreckt sich über eine Strecke von 89 km x 30 km (Länge x Breite).  
Die Insel <b>Maalhos</b> ist geografisch die westlichste Insel des Atolls und die zweite westlichste bewohnte Insel der Malediven nach der <b>Resortinsel Makunudhoo</b> im Haa Dhaalu Atoll.
<br><br>
Viele der Inseln im Atoll sind seit der Antike bewohnt und man kann archäologische Überreste aus der buddhistischen Periode der Malediven entdecken.
Das Ari Atoll ist eines der natürlichen Atolle der Malediven und eines der größten Atolle im Westen des Archipels.
<br><br>
Es gibt 12 Resortinseln im </b>Alifu Alifu Atoll</b>.
Tauchen ist die beliebteste touristische Aktivität auf den Malediven. Im Alifu Alifu Atoll kommen Taucher voll auf Ihre Kosten, es gibt 24 Tauchspots.
Wer jedoch Walhaie beobachten möchte, sollte sich aber besser im <b>Alifu Dhaalu</b> (südliches Ari Atoll) ein Tauchspot suchen.
</p>
