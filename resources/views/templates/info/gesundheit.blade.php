<h1>Allgemeine Informationen zur Gesundheit auf den Malediven!</h1>
<p>Die medizinische Versorgung auf den Malediven entspricht in keiner Weise den europäischen Standards.
Generell sollte man für die Malediven einen entsprechenden Versicherungsschutz abschließen, dazu gehört eine Auslandskrankenversicherung mit Rückholservice.
(Diese Versicherung sollte Standard sein bei jedem, der mindestens einmal im Jahr verreist.)
</p>
<p>Ernsthafte Erkrankungen können nur in Singapur, Dubai oder Indien behandelt werden.
	Wobei Deutsche, Schweizer und Österreicher für die Einreise nach Indien ein Visum benötigen.
	Leichtere Erkrankungen können entweder auf der Insel durch einen Arzt/Assistenzarzt behandelt werden oder in einem der beiden Krankenhäuser in Male.
</p>

<h2>Alles gefährliche befindet sich unter Wasser</h2>
<figure><img src="../images/info/hai.jpg" alt="Bild: Hai"></figure>
<p>
	Die Malediven sind ein relativ sicheres Reiseland. Es gab bisher nur einen Tsunami (2004), es gibt keine Wirbelstürme oder Erdbeben. Politische Unruhen sind auf den Resortinseln nicht zu befürchten.
	Die größte Gefahr auf den Malediven befindet sich im und unter Wasser. 
<br><br>
	<b>Starke Strömungen</b> machen das Schwimmen, Tauchen und auch das Schnorcheln an vielen Stellen gefährlich, auch wenn diese nicht so aussehen.
	Im Hotel sind die Bereiche, die nicht zum Schwimmen, Schnorcheln oder Tauchen geeignet sind, ausgewiesen.
	Man sollte fragen, wo es am sichersten ist, ins Wasser zu gehen. 
<br><br>
	Innerhalb der Lagune ist das Wasser meist nur Knie- oder Hüfthoch, dennoch können im sandigen Meeresgrund Stachelrochen liegen.
	Tritt man auf sie, schießt der Schwanz mit seinem Widerhaken wie eine Peitsche in das Bein und führt zu einer schmerzhaften Wunde, die sich entzünden kann.
	Normalerweise gleiten Stachelrochen davon, wenn man ihnen zu nahe kommt.
	Wird man von einem Stachelrochen gestochen sollte man die Wunde sofort mit heißem Wasser ausspülen und den Arzt aufsuchen. 
<br><br>
	<b>Seeanemonen</b> sind zwar bunte aber giftige Geschöpfe die zustechen, wenn man sie berührt. Der Stachel (Nesseln) fügt einem empfindliche Schmerzen zu.
	Es sollte umgehend ein Arzt aufgesucht werden. 
<br><br>
	Verletzungen durch Korallen sind relativ häufig.
	Immer wieder werden trotz Verbots Korallen abgebrochen oder es wird darauf herumgetrampelt.
	Auch wenn man unachtsam ist, kann es passieren, dass man sich an einer Koralle verletzt.
	Die Verletzungen können Infektionen hervorrufen und sollten umgehend mit Desinfektionsmitteln gereinigt und desinfiziert werden.
	Einige Korallen sind darüber hinaus auch noch giftig. 
<br><br>
<figure><img src="../images/info/Stachelrochen.jpg" alt="Bild: Stachelrochen"></figure>
	<b>Seeigel</b> sind eine Plage, die meisten Hotels entfernen sie, auch sind sie gut im klaren Wasser zu sehen.
	Man darf nur nicht unachtsam sein und rauf treten. Die Stacheln tun höllisch weh und lassen sich nur mühsam entfernen. 
<br><br>
	<b>Haie</b> sind furchteinflößende Geschöpfe, in den Malediven gibt es quasi keine Angriffe von Haien, die meisten sind harmlos. 
<br><br>
	Der <b>Steinfisch</b> gehört zu den giftigsten Tieren auf der Welt.
	Sein Nervengift in den Stacheln seiner Rückenflossen kann einen Menschen problemlos töten, auch wenn das sehr selten passiert.
	Der Fisch ist gut getarnt unterwegs, meistens ist er an Felsriffen, Korallenriffen und auf sandigen Boden in flachen Gewässern zu finden.
	Der im Indischen Ozean vorkommende Steinfisch (lat. Synanceja verrucosa) wird auch der echte Steinfisch genannt.
	Dieser braun-rote Fisch (weitere Farben kommen vor), vergräbt sich gern in flachen Gewässern im Sand und tarnt sich als Stein.
	Das Gift verursacht starke Schmerzen und kann zu einem Schock oder Absterben des Gewebes führen.
	Besonders Menschen mit einem schwachen Immunsystem, Kinder oder ältere Menschen können daran sterben.
</p>

