								<div class="block">
									<h3>Kann man sterben wenn man ein zweites Mal gestochen wird?</h3>
									<p>
										Wer einmal von einer Tigermücke mit dem Dengue Virus infiziert wurde, ist anhaltend Immun nur gegen einen der insgesamt 4 Virustypen.
										Nur weil man bereits einmal infiziert war, heißt das also nicht, dass man nicht wieder infiziert werden kann.
										Die Gefahr bei einer zweiten Infizierung zu sterben oder schlimmere Folgen erleiden zu müssen ist genauso hoch wie bei der ersten Infizierung.
										Laut der Deutschen Gesellschaft für Tropenmedizin DTG heißt es: "... ist es nicht gerechtfertigt Reisenden, die ein Dengue Fieber durchgemacht haben, von weiteren Aufenthalten in Verbreitungsgebieten abzuraten." </p>
										<p>Zusammenfassend:</p>
										<ul>
								    	<li>Es gibt keine Impfung gegen Dengue</li>
  										<li>  Konsequenter Mückenschutz bei Reisen in die Tropen und Subtropen ist unerlässlich</li>
  										<li>  Besondere Aufmerksamkeit beim Mückenschutz ist bei Kindern besonders wichtig</li>
  										<li>  Konsultieren Sie Ihren Hausarzt vor Reisen in die Tropen</li>
  										<li>  Kaufen Sie ausreichend Mückenschutz (DEET) bereits in Deutschland, auch ein imprägniertes Mückennetz inkl. Schnur</li>
