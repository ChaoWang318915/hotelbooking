<h3>Medikamente</h3>
</header>
<div class="collapse">
<h4>Einfuhr von Medikamenten</h4>
<p>
Die Einfuhr von Medikamenten wird streng kontrolliert, bei der Mitnahme von verschreibungspflichtigen Medikamenten muss eine Bescheinigung vom Arzt vorliegen.
Die Bescheinigung muss auf Verlangen in englischer Sprache vorgelegt werden, Dauer und Dosierung müssen bescheinigt werden.
Bekannte, nicht verschreibungspflichtige Medikamente wie Tabletten gegen Kopfschmerzen oder Durchfall sind hingegen kein Problem. 
Nie einzelne Tabletten mit sich führen, sondern immer verpackt, am besten in Originalschachtel.
</p>
<h4>Medikamente für die Reiseapotheke</h4>
<p>
Medikamente gegen Durchfall, Kopf- und Zahnschmerzen etc. sollte man in der Reiseapotheke haben und die sollte auf Haltbarkeit und Vollständigkeit kontrolliert werden.
Eine Reiseapotheke für die Tropen sieht natürlich anders aus als für die Schweizer Berge. Richten Sie die Reiseapotheke daher entsprechend dem Reiseziel aus.
(<a data-toggle-popup href="#popup-01">Reiseapotheke für die Malediven</a>)
</p>
