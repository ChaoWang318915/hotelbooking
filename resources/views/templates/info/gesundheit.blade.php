<li>
<header class="title-panel"><h3>Krankenhäuser</h3></header>
<div class="collapse">
<h4>Krankenhäuser auf den Malediven</h4>
<p>
Auf den Resortinseln ist entweder ein Arzt oder ein/e Arzthelfer/in.
Wenn nicht vor Ort kann man diese jedoch kurzfristig telefonisch herbeigerufen werden.
Einige Inseln verfügen sogar über eine kleine Klinik. Kuramathi ist so eine Insel, sie verfügt über eine medizinische Klinik mit Druckkammer für Tauchunfälle.
Die Klinik wird auch von Einheimischen benutzt. Auch Bandos verfügt über eine Klinik mit Dekompressionskammer. 
<br><br>
Weiterhin gibt es auf einigen Inseln noch sogenannte Gesundheitszentren, diese sind meistens jedoch nur mit einer Krankenschwester besetzt.
Auf Male gibt es 2 Krankenhäuser, das ADK Hospital und das Indira Gandhi Memorial Hospital.
Bedenken Sie, dass eine Behandlung kostenpflichtig ist und sofort bezahlt werden muss.
</p>
</div>
</li>


<li>
<header class="title-panel"><h3>Korallen</h3></header>
<div class="collapse">
<p>
Korallen sehen über dem Wasser aus wie graue oder schwarze Steine am Meeresgrund.
Die Realität ist aber, dass es sich um eine Ansammlung von Millionen von Organismen handelt.
Diese Organismen spielen eine große Rolle im Schutz der Insel.
<br><br>
Sie sind es, die die Wellen brechen lassen, sie wachsen mit dem Meeresspiegel immer dem Licht nach, steigt der Meeresspiegel wachsen auch die Korallen.
Sie bieten dadurch der Insel einen Schutz vor Wellen und geben Ihr Stabilität.
Fast 1/4 der Meeresbewohner finden in Korallen Schutz und Nahrungsquellen.
<br><br>
Ohne Korallen/Korallenriffe keine Insel! Korallen kann man leicht beschädigen, die filigranen Verästelungen brechen einfach ab.
Tritt man auf eine Koralle hat man die Koralle zerstört und sie stirbt ab.
Jahrelanges Wachstum ist nötig um nur 1 cm zu wachsen, mit dem Zerstören von Korallen zerstört man ein über Jahrzehnte gewachsenes Ökosystem.
Am Urlaubsort können Korallen ziemlich gefährlich sein.
Korallen sind sehr scharf und es kann zu Schnittverletzungen kommen, die sich entzünden können.
Weiterhin verstecken sich Meeresbewohner zwischen den Korallen, einige von Ihnen sind giftig oder „bissig“ wenn man Ihnen zu nahe kommt.
</p>
</div>
</li>

<li>
<header class="title-panel"><h3>Taucherkrankheit</h3></header>
<div class="collapse">
<p>
Die Taucherkrankheit ist eine ernste Angelegenheit, die durch zu tiefes Tauchen, zu schnelles aufsteigen oder zu langes verweilen in der Tiefe entstehen kann.
Die Symptome sind vielseitig und gehen von starker Müdigkeit, Schmerzen in den Gliedern, Taubheitsgefühl oder Lähmung und Atemnot bis hin zu Hautausschlag, Hautjucken oder Bewusstlosigkeit.
Die einzige Behandlung ist die Dekompressionskammer, wo unter Druck die Stickstoffblasen im Blut wieder langsam absorbiert werden.
Man darf 24 Stunden vor Abreise nicht mehr tauchen gehen. Die maximale Tauchtiefe für Sporttaucher auf den Malediven beträgt 30 Meter, die maximale Tauchzeit darf 60 Minuten nicht überschreiten.
</p>
</div>
</li>


<li>
<header class="title-panel"><h3>Sonnenbrand</h3></header>
<div class="collapse">
<p>
Die Sonne in Äquatornähe ist ein nicht zu vernachlässigendes Problem.
Besonders zwischen 12:00 und 15:00 Uhr ist die Sonnenstrahlung so intensiv, dass man schnell einen Sonnenbrand bekommen kann.
In einer Reiseapotheke sollte daher ausreichend Sonnenschutz mit einem hohen UV-Schutzfaktor (≧ 50), wasserbeständig, vorhanden sein.
<br><br>
Man kann das in den Shops der Resorts kaufen, jedoch zu relativ hohen Preisen.
Beim Schwimmen im Meer sollte man ein T-Shirt tragen, da die Sonnenstrahlen auf dem Wasser reflektiert werden und man so noch schneller einen Sonnenbrand bekommt.
Bei der Zimmerwahl im Hotel darauf achten, dass schattige Plätze zur Verfügung stehen. Was komischerweise nicht bei allen Hotels der Fall ist.
</p>
</div>
</li>

<li>
<header class="title-panel"><h3>Hepatitis</h3></header>
<div class="collapse">
<p>
Hepatitis A, auch als Gelbsucht bekannt, ist eine Viruserkrankung der Leber.
Der Virus wird durch Wasser oder verunreinigte Lebensmittel übertragen.
Vor allem sollte vermieden werden rohe Lebensmittel zu verspeisen.
Der einzige Schutz vor Hepatitis A ist eine Impfung. Bitte lassen Sie sich bei Ihrem Hausarzt beraten.
Eine Impfung gegen Hepatitis A gehört zur Standard-Impfung eines Reisenden, besonders in tropische und subtropische Länder mit geringen Hygienestandards.
</p>
</div>
</li>

<li>
<header class="title-panel"><h3>Trinkwasser</h3></header>
<div class="collapse">
<p>
Es wird empfohlen, das Leitungswasser nicht zu trinken.
Mineralwasser ist in allen Bars oder Restaurants verfügbar. Hotels stellen kostenlos 1 Flasche Wasser pro Person und Tag zur Verfügung.
<br><br>
Das Wasser wird aus dem Meer entnommen, entsalzt und aufbereitet. Auch wenn die Betreiber behaupten, dass es Trinkwasserqualität hat, würden wir vom Konsum abraten.
Es ist gut für Zähneputzen, Händewaschen und Duschen, aber als Getränk ist es nicht schmackhaft, es schmeckt fade.
Natürlich ist das die einzige Wasserquelle für viele einheimischen Inseln.
</p>
</div>
</li>

<li class="open">
<header class="title-panel"><h3>Magen- & Darmerkrankungen</h3></header>
<div class="collapse">
<p>
Eine Reiseapotheke sollte auf jeden Fall Tabletten für Magen- und Darmerkrankungen sowie gegen Durchfall beinhalten.
Die Nahrungsmittel in den Resorts werden aus weiten Teilen der Welt importiert, vorrangig Sri Lanka, Indien und Europa.
Das Angebot ist reichhaltig und umfasst mehrere Geschmacksrichtungen. Man bekommt viele Dinge zu essen, die man sonst nicht auf dem Teller hat.
Das verträgt sich nicht immer mit den europäischen Mägen. Viele Speisen sind scharf, beinhalten rohe Zutaten oder sind aus leicht verderblichen Zutaten.
Eine schlechte oder unzureichende Kühlung oder Erwärmung der Speisen kann übriges dazu beitragen.
</p>
</div>
</li>
