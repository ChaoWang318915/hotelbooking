									<div class="col-md-8">
										<h3>Moskitos </h3>
										<p>
											Mückenstiche gehören in den Tropen zu den Gefahren, vor denen es sich zu schützen gilt.
											Malaria, Denguefieber, Chikungunya Fieber, Filariosen (Fadenwürmer) und andere Krankheiten werden von Mücken übertragen.
											Man sollte sich zu Tages- und Nachtzeiten vor Mückenstiche schützen.
											Da Sie sich in den Tropen befinden, sind Moskitos jedoch normal.

											In den Resorts gibt es nur wenig offenes Süßwasser, wo sich Moskitos vermehren können, weiterhin werden die meisten Inseln regelmäßig eingenebelt um Mücken und Mückenlarven abzutöten.
											Saubere Inseln haben erfahrungsgemäß weniger Mücken als verschmutzte Inseln. </p>


											<h3>Dengue </h3>
											<p>
											Das Denguefieber, eine Viruserkrankung, wird durch Stechmücken wie die Tigermücke oder der Gelbfiebermücke übertragen.
											Die Mücke ist tagaktiv, weiblich, mag keine direkte Sonne, sticht bevorzugt bei Sonnenaufgang oder Sonnenuntergang, mag stehendes, ruhiges Süßwasser/Brachwasser.

										  Vermehrt tritt das Denguefieber während der Regenzeit zwischen Mai und September auf, aber auch danach.
										  Eine vorbeugende Impfung gibt es nicht. Ein neuer Impfstoff ist vorrangig für Menschen die in einem endemischen Gebieten leben, zwischen 9 und 45 Jahre alt sind und bereits mindestens eine dokumentierte Dengue-Virusinfektion durchgemacht haben.
										  Die beste Schutzvorkehrung die Sie treffen können ist die Klimaanlage in ihrem Zimmer laufen zu lassen, helle Kleidung tragen und sich konstant mit Mückenschutz einreiben.
										</p>
											<h3>Malaria </h3>
											<p>
												Malaria ist auf den Malediven nicht weit verbreitet.
											</p>
											<h3>Chikungunya</h3>
											<p>
												Es gibt weitere Mückenarten die unter anderem Chikungunya Viren übertragen.
												Die Symptome sind der Malaria ähnlich.
												Es gibt keine Impfungen und keine wirksamen Medikamente gegen die Chikungunya Viren.
												Auch hier ist ein Mückenschutz dringend zu empfehlen.
												<br><br>
												Chikungunya wird wie Dengue von dem Stich tagaktiver Mücken übertragen.
												Der Krankheitsverlauf ist ähnlich dem der Malaria mit Kopf- und Gliederschmerzen sowie hohes Fieber.
												Es gelten die gleichen Vorsorgemaßnahmen wie beim Dengue Fieber.
												Die Inkubationszeit beträgt zwischen drei und sieben Tagen.
												<br><br>
												Nach einer überstandenen Erkrankung ist man sein Leben lang Immun.
												Todesfälle sind äußerst selten zu verzeichnen.
												Es gibt sogar Fälle, wo man eine Erkrankung nicht merkt oder diese mit einer Grippe verwechselt.
											</p>
											<p>
												In den Malediven ist das  Denguefieber weit verbreitet, die gemeldeten Fälle schwanken jedoch von Jahr zu Jahr und sind größtenteils auf die einheimischen Inseln und Malé begrenzt.
												Die Anzahl der erkrankten Touristen ist unbekannt oder ungenau, da die Inkubationszeit von drei bis zu 14 Tagen betragen kann und bis dahin sind viele der Touristen wieder zu Hause.
												<br><br>
												Man darf die Gefahr der durch Moskitos übertragenen Krankheiten, vor allem in den Tropen, keinesfalls vernachlässigen.
												Man sollte auch nichts auf Zahlen oder Erfahrungsberichte geben und sich auch nicht auf Aussagen von Anderen verlassen, sondern den normalen Verstand walten lassen.
												<br><br>
												 Wenn man mit Kindern auf die Malediven fliegt oder man schon einmal Dengue Fieber hatte, ist der Hausarzt der beste Ansprechpartner.
												 Als Mückenschutz haben sich Insektenabwehrmittel mit dem Wirkstoff Diethyltoluamid, besser bekannt als DEET, bewährt.
												 Dieser Wirkstoff wird in Deutschland von bekannten Firmen wie Autan oder Anti Brumm eingesetzt.
											  </p>
												<ul>
										    		<li>Benutzen Sie Moskitonetze, möglichst imprägniert, auch über Ihrem Bett</li>
										    		<li>Tragen Sie helle Kleidung, möglichst imprägniert, man kann auch Kleidungsstücke einsprühen</li>
										    		<li>Klimaanlage laufen lassen, Mücken hassen Kälte</li>
										    		<li>Konsequentes auftragen des Mückenschutzes mit dem Wirkstoff DEET</li>
										    		<li>Nachts Verdampfer, Sprays und Räucherspiralen zusätzlich benutzen</li>
												</ul>
									</div>
								</div>
