<header class="title-panel">
<h3>Impfungen</h3>
</header>
<div class="collapse">
<h4>Impfungen für die Einreise auf die Malediven</h4>
<p>
Für Reisende aus Deutschland, der Schweiz und Österreich besteht keine Impfpflicht bei der Einreise auf die Malediven.
Empfohlen werden Standardimpfungen für Kinder und Erwachsene <small>(Nachzulesen beim RKI)</small>.
Reisende aus Gelbfieber gefährdeten Ländern müssen eine gültige Gelbfieberimpfung nachweisen.
Was Impfungen betrifft, so sollte man immer den Hausarzt konsultieren.
</p>
