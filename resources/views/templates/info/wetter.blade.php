<h1>Das Wetter auf den Malediven</h1>
<p>
Das Wetterradar für die Malediven zeigt eigentlich nur eins: tropisch warmes und meist sonniges Wetter.
Die Maledivensind der Inbegriff für himmlischen Urlaub mit strahlend blauem Himmel und einem atemberaubenden Meer.
Viele Malediven Urlauber schätzen das unvergleichliche Insel-Flair bei ganzjährigen Durchschnittstemperaturen zwischen 29 und 32 Grad Celsius.
Dennoch gibt es im Inselparadies eine Haupt- und eine Nebensaison.</p>
<p>Die Malediven liegen am Äquator und haben zwei ausgeprägte Jahreszeiten, wo die Temperaturen kaum schwanken. </p>
<p>&nbsp;</p>
<div class="inform-block">
<div class="row">
<div class="col-md-5"><figure><img src="../../images/info/Wetter-Sued-West-Monsun.jpg" alt="#"></figure></div>
<div class="col-md-7">
<h3>Nordost-Monsun</h3>
<p>
Die Trockenzeit geht von November bis April und wird begleitet mit dem Nordost-Monsun. 
Der Nordost-Monsun ist eher freundlich, er ist ziemlich trocken und etwas kühler als der Südwest-Monsun.
Die Höchsttemperaturen liegen ganzjährig über 30 °C, die nächtlichen Tiefsttemperaturen unterschreiten selten 25 °C.
Tagsüber kühlt die Meeresbrise ein wenig, die Nächte können dagegen sehr schwül sein.  
</p>
</div></div></div>


<div class="inform-block">
<div class="row">
<div class="col-md-5"><figure><img src="../../images/info/Wetter-Nord-Ost-Monsun.jpg" alt="Bild: Blauer Himmer zur Trockenzeit"></figure></div>
<div class="col-md-7">
<h3>Südwest-Monsun</h3>
<p>Die Regenzeit mit dem Südwest-Monsun dauert in der Regel von Mitte Mai bis Ende Oktober.
In dieser Zeit fällt auf den Malediven der meiste Regen, oft auch sintflutartige Regenfälle.
Dies ist auch der Zeitraum für die meisten Stürme und weniger Sonnenschein.</p>
<p>Die durchschnittliche Niederschlagsmenge beträgt zwischen 1786 mm/Jahr im Norden, 2277 mm/Jahr im Süden und 1924 mm/Jahr in den zentralen Teilen des Landes.
Durch die Lage gibt es auf den Malediven das ganze Jahr über viel Sonnenschein, der Durchschnitt liegt bei ca. 7,5 Stunden pro Tag.
Wobei die Sonne in den zentralen Teilen des Landes ein wenig öfters scheint als in den südlichen Teilen.
<br><br>
Auf den Inseln ist es mit durchschnittlichen Temperaturen von 30,5° C tagsüber sehr heiß, oft aber mit einer angenehmen Meeresbrise die eine willkommene Abkühlung bietet.
Am Abend liegen die durchschnittlichen Temperaturen bei ca. 23° C.
Die tiefste Temperatur, die jemals auf den Malediven gemessen wurde, war 17,2 ° C , die höchste Temperatur betrug 36,8° C .
</p>

<ul class="accordion type2">
<li>
<header class="title-panel">
<h3>Die beste Reisezeit für die Malediven</h3>
</header>
<div class="collapse">
<p>Sucht man viel Sonnenschein, muss man die Malediven in der trockenen Jahreszeit von Dezember bis April besuchen.
Dies ist jedoch auch die <b>Hauptsaison für die Malediven</b>.
Resorts könnten oft ausgebucht und die Preise höher als im restlichen Jahr sein.
<br><br>
Der Zeitraum zwischen Weihnachten und Neujahr ist die beliebteste und teuerste Reisezeit in der <b>Hochsaison</b>, denn hier kommen noch oft Zuschläge hinzu.
Zwischen Mai und November ist es warm, aber der Himmel kann bewölkt sein.
Die Luftfeuchtigkeit ist höher und es kann zu Regen und tropischen Stürmen kommen.
Dies ist die Nebensaison – die Preise sind niedriger und es gibt weniger Touristen.
Einige Resorts bieten hier besonders attraktive Übernachtungspreise an.
<br><br>
In den Übergangsmonaten November und Mai finden Taucher klareres Wasser vor.
</p>

