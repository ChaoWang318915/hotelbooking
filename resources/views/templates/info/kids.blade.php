<h3>Gesundheit und Kinder</h3>
<p>
Die Malediven mit Kindern ist ein Thema, welches vor allem Eltern mit jungen Kindern betrifft.
Die Sorgen vor einer Reise in die Tropen sind groß.
Kann oder sollte man seinem Kind die Strapazen einer langen Reise antun?
Sind die gesundheitlichen Gefahren eventuell zu hoch oder kalkulierbar?
Muss ich mein Kind vorher impfen? Ist die Sonnenstrahlung zu stark?
Schließlich kann man nicht 24 Stunden ein Auge auf die Kleinen werfen.
Die Malediven sind ein relativ sicheres Reiseziel, da sich die Anzahl der Naturkatastrophen in Grenzen hält und gefährliche Tiere nur unter Wasser anzutreffen sind.
An Land stellen Moskitos und die Sonne die größte gesundheitliche Gefahr für Kinder dar.
Hier gilt es, sich besonders zu schützen <a href="https://www.my-maldives.com/informationen/gesundheit" target="_blank">(weitere Infos zur Gesundheit auf den Malediven).</a>
Generell spricht nichts gegen einen Urlaub mit Kindern auf den Malediven, ja selbst mit Babys ist das problemlos möglich.
</p>

</div></div></div>

<div class="inform-block">
<div class="row">
<div class="col-md-5"><figure><img src="../../images/content-images/mit-kindern-auf-die-malediven.jpg" alt="Bild: Mit Kleinkind auf die Malediven"></figure></div>
<div class="col-md-7">
<h3>Mit dem Kleinkind auf die Malediven</h3>
<p>
Wer mit einem Baby auf die Malediven reist, sollte einiges wissen.
Babynahrung, Windeln, evtl. Kinderwagen und alles Notwendige zur Versorgung müssen mitgebracht werden.
Weiterhin dürfen Sonnenschirme bzw. Sonnenschutz sowie der Mückenschutz nicht fehlen. 
<br><br>
Gesundheitliche Risiken sollten unbedingt vorher mit dem Hausarzt besprochen werden.
Unsere Empfehlung: Konsultieren Sie vor Reservierung Ihren Hausarzt.
Achten Sie bei Reservierung auf eine kindgerechte Ausstattung mit ausreichend Schatten. 
<br><br>
Selbstverständlich wird das Hotel die Mahlzeiten für Ihr Baby auch zubereiten.
Wenn Sie jedoch Fertiggerichte aus dem Glas wünschen, kann man diese auf den Malediven von der Firma Nestum (Nestlé) kaufen.
Windeln z. B. Drypers, werden von der schwedischen Firma SCA hergestellt, ein Weltmarktführer, die auch Zewa und Tempotaschentücher herstellen. 
<br><br>
Sie sollten aber bedenken, dass diese Produkte teurer sind als zu Hause.
Diese Artikel können dazu nur in Malé oder auf einer einheimischen Insel gekauft werden.
</p>
<p>
Achten Sie vor Buchung auf die Preise.
Nicht alle Hotels bieten günstige Preise für Kinder an.
</p>
