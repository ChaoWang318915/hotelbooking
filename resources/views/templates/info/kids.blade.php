<h1>Mit den Kindern auf die Malediven</h1>
<p>
Kinder sind in fast allen Hotels willkommen, nur einige Hotels auf den Malediven sind ausschließlich Erwachsenen zugänglich <small><b>(Adults only Hotels)</b></small>.
Aber in den Hotels sind Kinder unterschiedlich gut aufgehoben oder werden unterschiedlich betreut.
Einige Hotels haben einen <b>Kids-Club</b> für Kinder oder bieten <b>Babysitting</b> an. Auch die Hoteleinrichtung unterscheidet sich grundlegend.
<br><br>
Wäre es nicht wunderbar, wenn Eltern in aller Ruhe den Tag genießen können, sich im Spa verwöhnen lassen oder im Meer schwimmen, mit dem Wissen, dass die Kleinen gut versorgt und beschäftigt werden?
</p>
<p>&nbsp;</p>


<div class="inform-block">

<div class="row">
<div class="col-md-5"><figure><img src="../images/info/willkomen-kids.jpg" alt="Bild: Kids spielen am Strand"></figure></div>
<div class="col-md-7">
<h3>Willkommen Kids</h3>
<p>
Wer mit Kinder auf die Malediven reist, sollte seine Reise vorab gut planen.
Je nach Alter der Kinder muss man sich fragen, wie sollen die Kinder vor Ort betreut und unterhalten werden.
Bei <b>Kleinkindern</b> werden andere Hoteleinrichtungen erwartet als bei größeren Kindern.
Was muss ich mitnehmen, gibt es auf den Malediven Windeln oder Babynahrung?
</div></div>

<p>
Bei Kleinkindern oder <b>Babys</b> wäre sicherlich ein <b>Babysitter</b> von Vorteil.
Wenn die Kinder bereits gut alleine klarkommen, sollte man darauf achten, dass es im Hotel ausreichend Spiel- und Unterhaltungsmöglichkeiten gibt.
Das Hotel muss geschultes Personal haben, welches die Kinder betreuen kann und auch die Hotelausstattung sollte kinderfreundlich sein.   
<br><br>
Einige der Hotels haben ein richtig gutes <b>Programm für Kinder</b> entwickelt und stellen dazu ausgebildetes Personal und Animateure zur Verfügung.
Malen, Singen, Tanzen, Basteln, Schnorcheln und vieles mehr können die Kleinen erwarten.
Viele Hotels bieten einen <b>Kids-Club</b> für die Betreuung und Unterhaltung der Kinder an.
Jedes Hotel bietet selbstverständlich auch <b>Sport- und Freizeitaktivitäten für Kinder</b> an (gegen extra Gebühr), mit einer kindgerechten Auswahl, je nach Alter des Kindes.  
<br><br>
Bei der Auswahl des Hotels sollte man genau hinschauen, ob es zu den Kindern passt.
Denn wenn sich die Kinder langweilen, wird es sicherlich kein schöner Urlaub werden.   

<br><br>
Auch auf die Zimmerpreise sollte man achtgeben, da einige Hotels Kinder wie Erwachsene berechnen.
Generell übernachten Babys<sup>*</sup>in allen Hotels kostenfrei, bis auf die Zahlung der Green-Tax (Ökosteuer).
Einige Hotels verfügen über familienfreundliche Zimmerpreise oder bieten die Verpflegung kostenlos an, wenn die Kinder im Zimmer der Eltern übernachten.
Sollte man mit mehr als 2 Babys reisen, ist zu prüfen, ob für beide Babys ein Babybett zur Verfügung steht.
Einige Hotels berechnen nur 1 Baby kostenlos, das zweite Baby wird wie ein Kind berechnet.
</p>
<p>
Sie werden, wenn Sie bei einem Hotel die Anzahl der Reisenden angeben, wenig damit konfrontiert sein, da Sie die Regeln im Einzelnen nicht sehen, sondern nur einen Gesamtaufenthaltspreis.
Das allerdings erschwert auch die Vergleichsmöglichkeiten.
<p>
<sup>*</sup><small>*(Das Alter von Babys und Kindern wird unterschiedlich definiert, je nach Hotel.
	Als Faustregel kann man aber ansetzen, dass Babys von 0 bis 1.99 Jahre alt sein müssen, um als Babys zu gelten.
Mehr zum Thema Altersregeln <b>(Child Policy)</b> finden Sie <a href="https://www.my-maldives.com/informationen/altersregelungen">hier</a>)</small>
</p>
</div>

