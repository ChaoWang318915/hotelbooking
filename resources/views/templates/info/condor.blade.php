<h4>Flugvermittlung</h4>
<p>Auf Wunsch vermitteln wir dir den passenden Flug für deinen Urlaub auf den Malediven. 
  <br>
Für die Flugvermittlung berechnen wir <b>50 € pro Person (inkl. 19 % MwSt.)</b>. 
<br><br>
Wir übermitteln dir alle Flugzeitenänderungen, buchen den Flug 1 x für dich um (wenn nötig), wir stornieren den Flug für dich (wenn nötig) und beantragen die Rückzahlung von Steuern und Gebühren im Falle einer Stornierung.
Wir buchen für dich Speisen an Bord (wenn gewünscht – extra Gebühren können entstehen), wir melden dein Extra Gepäck an, wie Sportausrüstung und reservieren deine Sitzplätze (extra Kosten bei der Fluggesellschaft können entstehen.) 
<br><br>
Weitere Informationen über unsere Entgelte für unsere Dienstleistungen kannst du unserer <a href="https://www.my-maldives.com/entgelttabelle">Entgelttabelle</a> entnehmen.
Selbstverständlich buchen wir auf Wunsch jeden Flug für dich. </p>
