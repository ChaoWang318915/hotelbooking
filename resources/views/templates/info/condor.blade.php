<div class="block">
<h3>Ein paar Tipps für die Flugbuchung mit Condor! </h3>
<ul>
<li>Buche den Flug so früh wie möglich, vergiss aber nicht den Flug mit einer Versicherung abzusichern.</li>
<li>Alternativ und von uns empfohlen, buche den Flug im Tarif "N", die Flex-Option (Zur Zeit + 50 € pro Person).</li>
<li>Prüfe, ob es Angebote (Fliegen Tarife) gibt und ob Malé dort angeboten wird (Preisalarm)</li>
<li><a href="https://www.condor.com/tcibe/de/mybooking/login" title="Hier gehts zum Online Check-In von Condor">Online-Check-In</a> machen und sich Sitzplätze reservieren, wenn man diese nicht extra bezahlen will.</li>
<li><a href="https://www.condor.com/tcibe/de/mybooking/login" title="Hier gehts zum Online Check-In von Condor">Online-Check-In</a> machen um sich nicht am Check-in-Schalter am Flughafen anstellen zu müssen. („Baggage Drop-off“ für Reisende mit Gepäck am Abflugtag).</li>
<li><a href="https://www.condor.com/tcibe/de/mybooking/login" title="Hier gehts zum Online Check-In von Condor">Online-Check-In</a> machen, um sich die Boarding Card auf das Handy schicken zu lassen.</li>
<li>Wer seinen Flug nicht selber buchen möchte, kann das uns überlassen. Gern suchen wir dir den passenden Flug. Mehr Informationen findest du <a href="https://www.my-maldives.com/flugbuchung">hier.</a></li>
</ul>
