<div class="col-md-7">
<h3>Sitzplätze</h3>
<p>
Sitzplätze können bis 48 Stunden vor Abflug gebucht werden.
Für Gäste in der Premium- und Business-Class ist die Sitzplatzreservierung kostenfrei.
Für eine Sitzplatzreservierung in der Economy-Class  berechnet Condor eine Gebühr: Sitzplatz ab € 19,99 und XL-Seat ab € 99,99<sup>*</sup>.
<br><br>
<small><sup>*</sup> Es gibt Bestimmungen für die Buchung dieser Sitzplätze.</small>
<br><br>
In der Premium und Business-Class ist die Reservierung der Sitzplätze kostenlos. Sitzplatzreservierungen gelten nicht für den Zubringerflug. Preise pro Strecke und Person.
Bitte beachten Sie die Hinweise auf der Condor <a href="https://www.condor.com/de/fliegen-geniessen/an-bord-service/sitzplaetze.jsp">Webseite.</a>
Sollten Sie Ihren gebuchten Sitzplatz nicht erhalten haben, können Sie das bei Condor direkt auf der Webseite <a href="https://www.condor.com/de/fliegen-geniessen/an-bord/sitzplaetze/reklamationen.jsp">reklamieren.</a>
<br><br>
</p>
<small><p><sup>*</sup>Preisangaben sind unverbindlich und dienen nur Ihrer Information. Preise können sich ohne Ankündigung ändern, bitte erfragen Sie die aktuellen Preise per Telefon.</p></small>
