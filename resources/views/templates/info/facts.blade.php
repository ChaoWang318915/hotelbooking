<h2>Gesundheit</h2>
<figure><img src="../../images/content-images/gesundheit.jpg" alt="Symbolbild: Ärztliche Versorgung"></figure>
<h4>Krankenversicherung</h4>
<p>
Die medizinische Versorgung auf den Malediven entspricht in keiner Weise den europäischen Standards.
Generell sollte man für die Malediven einen entsprechenden Versicherungsschutz abschließen, dazu gehört eine Auslandskrankenversicherung mit Rückholservice.
<small>(Diese Versicherung sollte Standard sein bei jedem, der mindestens einmal im Jahr verreist.)</small>
</p>
<h4>Insel Arzt - Atoll Krankenhäuser</h4>
<p>Ernsthafte Erkrankungen können nur in Singapur, Dubai oder Indien behandelt werden.
Wobei Deutsche, Schweizer und Österreicher für die Einreise nach Indien ein Visum benötigen.
Leichtere Erkrankungen können entweder auf der Insel durch einen Arzt/Assistenzarzt behandelt werden, eventuell kommt eins der Atoll Krankenhäuser in Frage oder in einem der beiden Krankenhäuser in Malé.
</p>
<h4>Reiseapotheke</h4>
<p>

Medikamente gegen Durchfall, Kopf- und Zahnschmerzen etc. sollte man in der Reiseapotheke haben und die sollte auf Haltbarkeit und Vollständigkeit kontrolliert werden.
Eine Reiseapotheke für die Tropen sieht anders aus als für die Schweizer Berge.
Richten Sie die Reiseapotheke entsprechend dem Reiseziel aus.
Bei Reisen mit Kindern in die Tropen bedarf es einer besonderen Aufsicht.
</p>
