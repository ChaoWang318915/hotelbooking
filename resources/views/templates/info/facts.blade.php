<h3>Telekommunikation</h3>
</header>
<div class="collapse">
<h4>Mit dem Handy</h4>
<p>
Die telefonischen Verbindungen im Land sind von guter Qualität, sowohl von Deutschland nach den Malediven als auch umgekehrt.
<br>
Wenn Sie ein Handy besitzen das auf der Frequenz 900 MHz sendet (was heutzutage jedes normale Handy tut), können Sie dies auch in den Malediven benutzen.
Weiterhin werden Datenraten mit 4G anboten.
<br><br>
Die Gesprächsminuten sind relativ teuer sowohl von Deutschland als auch die Roaming-Gebühren aus den Malediven (3 € pro Minute und mehr sind keine Seltenheit.
Ankommende Gespräche kosten ebenfalls bis zu 1,80 €).
Vergessen können Sie mit dem Handy ins Internet zu gehen, wenn Sie Roaming nutzen. Hier sind rund 0,80 € / pro 50 KB angesagt.
Bei einem normalen Webseitenbesuch können da schnell einige Euros zusammenkommen. 
<br><br>
Das lässt sich umgehen, indem Sie sich Prepaid-Karten am Flughafen besorgen.
Mit denen können Sie sowohl Wi-Fi nutzen als auch telefonieren.
Die sind günstiger als Ihr Telefonanbieter und Sie haben die Kosten unter Kontrolle.
Die meisten Hotels bieten bereits kostenlos Wi-Fi an, allerdings oft langsam und nicht Insel abdeckend.
SIM-Karten von Ooredoo am Flughafen kosten 30 US-Dollar für 17 Gbyte oder 50 US-Dollar für 30 Gbyte. Schauen Sie in der Ankunftshalle nach einem Stand, dort werden diese angeboten.
</p>
<p>	Anbieter sind <a href="http://www.dhiraagu.com.mv/" target="_blank">Dhiraagu</a> oder <a href="https://www.ooredoo.mv/tourist-plan" target="_blank"> Ooredoo</a> mit speziellen Touristen SIM-Karten.
Welche wir empfehlen würden.</p>
<h4>Mit dem Laptop oder Handy ins Internet</h4>
<p>Es ist oft der Fall, das das Internet auf der Resortinsel vom Hotel zu langsam ist, da es sich viele Leute gleichzeitig teilen müssen.
   Abhilfe schaft auch hier eine Pre-Paid-Karte, die Sie am Flughafen kaufen können. Die Anbieter stellen 4G zur Verfügung.
</p>
