<h1>Malediven Fakten <br>Die wichtigsten Fakten zu den Malediven im Überblick.</h1>
<figure><img src="../../images/content-images/mosche-malediven.jpg" alt="Mosche Malediven"></figure>
<p>
Die Malediven befinden sich in Asien, ca. 428 km südwestlich von Indien mitten im Indischen Ozean. 
Die über 1190 Inseln der Malediven haben eine Nord-Südausdehnung von 870 km und eine West-Ostausdehnung von ca. 125 km. 
Die gesamte Landfläche beträgt nur 298 km². Nachbarländer sind Indien und Sri Lanka.
<br><br>
Die Malediven bestehen aus über 1.190 Koralleninseln, die sich in 26 (geografische) Atolle gruppieren.
Die Inseln verteilen sich auf eine Fläche von rund 90.000 km².
99 % der Landesfläche besteht nur aus Wasser und nur 1 % ist Landfläche und die hat eine maximale Bodenhöhe von knapp über 2 Meter.
Die Malediven sind somit das flachste Land der Erde.
</p>


