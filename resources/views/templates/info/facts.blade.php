<h4>Traveller Checks</h4>
<p>
Ja, die gibt es noch und die haben einige gute Vorteile.
Aber auf den Malediven sollten Sie jedoch keine Traveller Checks mitnehmen, da es immer weniger Hotels gibt, die Traveller Checks akzeptieren.
</p>

<h4>Steuern und Gebühren / Serviceentgelt</h4>
<p>
Die Tourismussteuer (TGST) von 12 Prozent wird auf alle Waren und Dienstleistungen in Tourismusbetrieben erhoben.
Es wird eine Ökosteuer in Höhe von 6 US-Dollar pro Person und Tag  erhoben für Gäste, die in touristischen Unterkünften übernachten.
In Gästehäusern beträgt die Ökosteuer 3 US-Dollar pro Person und Tag.
In den Resorts wird darüber hinaus noch ein Serviceentgelt in Höhe von 10 % berechnet.
