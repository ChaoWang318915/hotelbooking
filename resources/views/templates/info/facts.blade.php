<h2>Der Ramadan auf den Malediven</h2>
<div class="row pt-4">
<div class="col-md-4">
<figure>
<img src="../../images/content-images/ramadan.jpg" alt="Bild: Ramadan">
</figure>
</div>
<div class="col-md-8">
<h3>Ramadan</h3>
<p>
Ramadan ist der muslimische, heilige Fastenmonat. Normalerweise sollte Ramadan Ihren Urlaub auf den Malediven nicht beeinflussen.
Es kann aber zu Verschiebungen bei den Essenszeiten kommen, eventuell ist Ihr Lieblingskoch nicht vor Ort, es kann auch passieren, dass alles ein wenig länger dauert als sonst. 
Alles in allem ist es aber eher unwahrscheinlich, dass man auf den Resortinseln viel vom Ramadan mitbekommt.
Anders sieht es auf den Inseln der Einheimischen aus. Wenn man die Einheimischen-Inseln oder Malé besucht, sollte man vermeiden in der Öffentlichkeit zu trinken, zu rauchen oder zu essen.
Restaurants und Cafés haben am Tage komplett geschlossen und öffnen erst nach Sonnenuntergang. 
</p>
</div>
