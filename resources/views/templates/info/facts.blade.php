<h3>Das Wetter auf den Malediven</h3>
<p>
Die Malediven liegen am Äquator und haben zwei ausgeprägte Jahreszeiten in denen die Temperaturen kaum schwanken, so herrscht das ganze Jahr über ein tropisches Klima.
<br><br>
Die Höchsttemperatur liegt bei 31,5° C und die Tagestiefsttemperaturen bei 25,3° C. 
<br><br>
Von Dezember bis April geht die Trockenzeit, diese wird vom Nordost-Monsun <small>(auch Sommermonsun genannt, bei dem der Wind aus Richtung Indien kommt)</small> bestimmt.
Die Monate von Mai bis November werden vom Südwest-Monsun bestimmt <small>(Wintermonsun)</small> und gelten als Regenzeit.
</p>
