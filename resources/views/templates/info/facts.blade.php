<h3>Trinkgelder</h3>
</header>
<div class="collapse">
<h4>Trinkgelder auf der Urlaubsinsel</h4>
<p>
Trinkgelder sind zwar nicht üblich, werden aber fast überall erwartet.
Man sollte die Höhe der Trinkgelder von der Art der Unterkunft ausmachen (hohes Preisniveau, höheres Trinkgeld) und auch nur dann Trinkgeld geben, wenn man mit einer Leistung oder Service zufrieden war.
Ohne Zweifel geht mit Trinkgelder vieles einfacher.
Der Ober merkt sich, was Sie trinken möchten, extra Brot oder bevorzugte Bedienung sind die Folge von gutem Trinkgeld.
Das macht auch Ihren Urlaub viel angenehmer.
Die wichtigsten Personen sind der Ober, die Reinigungskraft die Ihr Zimmer sauber macht und der Butler oder Concierge.
<br><br>
Trinkgeldempfehlung für die Malediven:</p>
<ul>
<li> Kofferträger = 1 US-Dollar pro Gepäckstück</li>
<li> Zimmerservice = 2 bis 3 US-Dollar pro Bestellung</li>
<li> Reinigungskraft = 2 US-Dollar pro Tag</li>
<li> 	Thakaru = 10,- USD die Woche</li>
<li> Im Restaurant = 5% - 10% vom Rechnungsbetrag</li>
</ul>
