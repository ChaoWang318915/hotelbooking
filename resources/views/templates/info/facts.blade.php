<h3>Die Atolle  und die Inseln</h3>
<p>
Die Anzahl und die Formen der Inseln ändern sich ständig. Einige Inseln verschwinden und andere kommen neu hinzu.
Die Regierung der Malediven unterteilt die Inseln in 21 Verwaltungsbezirke (<a href="https://www.my-maldives.com/atolle"> 20 Atolle + Malé, die Hauptstadtinsel)</a>.
<br><br>
Touristen benutzen die Namen der Verwaltungsbezirke wie <a href="https://www.my-maldives.com/atolle/haa-alifu">Haa Alifu</a>, <a href="https://www.my-maldives.com/atolle/raa">Raa</a>, <a href="https://www.my-maldives.com/atolle/baa">Baa</a>, <a href="https://www.my-maldives.com/atolle/gaafu-alifu">Gaafu Alifu</a> oder <a href="https://www.my-maldives.com/atolle/meemu">Meemu</a>.
Die Namen sind Buchstaben aus dem Thaana Alphabet, das aus der Sprache der Malediver, Dhivehi, kommt.
<br><br>
Einige Buchstaben (Atoll Namen) wurden mit dem Zusatz Alifu <small>(= nördlich)</small> oder Dhaalu <small>(= südlich)</small> ergänzt. 
</p>
