<header class="title-panel"><h3>Währung</h3></header>
<div class="collapse">
<h4>Der Rufiyaa, die Landeswährung</h4>
<p>
1 maledivischer Rufiyaa (MRF) entspricht 100 Laaris.<br>
Die Ein- und Ausfuhr der Landeswährung ist nicht erlaubt. Es gibt keine Beschränkung für ausländische Währungen.
Der US-Dollar wird überall akzeptiert und Hotelrechnungen werden in US-Dollar ausgewiesen.
Hotels akzeptieren oft auch €, GBP und CHF, allerdings kann es zu hohen Wechselgebühren kommen.
Wir empfehlen, generell die Zahlung mit US-Dollar.
</p>
<p>
1 Rufiyaa = 0,056 € und 1 € = 17,84 Rufiyaa und 1 USD = 15,42 Rufiyaa.
Der Wechselkurs ist an diese Werte gekoppelt.
</p>
</div>
