<h3>Umweltschutz auf den Malediven </h3>
<p>
  Umweltschutz auf den Malediven hat oft mit Fehlentscheidungen und fehlendem Wissen zu tun.
  Wer auf den Malediven Urlaub macht, kann und macht kein Ökourlaub! 
  <br><br>
  Diese Zeiten sind leider schon lange vorbei.
  Man sollte sich darüber im Klaren sein, dass der immer weiter zunehmende Tourismus auf den Malediven, Auswirkungen auf die Natur hat. 
  <br><br>
  Ein Urlaub auf den Malediven hat nichts mit umweltgerechten Verhalten zu tun.
  Einige glauben, die teuren Hotels seien schuld, andere meinen, die billigen Hotels sind an den Umweltproblemen schuld. 
  <br><br>
  Viele Gäste vertreten den Standpunkt, wenn ein Hotel gewisse Annehmlichkeiten bietet, so möchte man diese auch in Anspruch nehmen.
  Bei den hohen gezahlten Zimmerpreisen sollte das Hotel über genügend Kapital verfügen, um entstehende Umweltprobleme zu lösen.
  Die Verantwortung liegt allein bei den Hotels. 
  <br><br>
  Wer für Luxus bezahlt, möchte auch Luxus haben.
  Hotelgäste zu bitten, Wasser zu sparen, so wenig Müll als möglich zu produzieren oder Klimaanlagen in Maßen zu nutzen, verläuft oft ungehört. 
  Warum in vielen Badezimmern der Bungalow eine 300 L Badewanne steht,  die mit heißem Wasser gefüllt wird, auf einer Insel ohne natürlicher Wasserversorgung, wo die Temperaturen fast nie unter 25 °C liegen,
  und ein kristallklarer Ozean direkt vor der Tür liegt, bleibt ein Rätsel.
 Man könnte die 10 Tage, die man auf der Insel ist, sich mit duschen zufriedengeben, oder nicht?
 Ein weiteres großes Problem sind die vielen Plastikflaschen. 
  <br><br>
  Jeder Urlauber auf den Malediven hinterlässt (ökologische) Fußspuren.
  Klimaanlagen, Müll, Whirlpools, Badewannen, Duschen, Minibars ..., um nur einige der eigenen Fußspuren zu nennen.
  Die Liste ist lang, denn auch Hotels und deren Angestellten, sie alle hinterlassen ihre Fußspuren und das seit Jahrzehnten.
  Wenn dann in Broschüren zu lesen sind wie diese: „... Unberührte Natur ...“ fragt man sich zu Recht, was der Verfasser wohl von seinen Lesern denkt? 
  <br><br>
  Es gibt private Initiativen, die versuchen den Gästen den Umweltschutz der Inseln näher zubringen und einige setzen sich schon seit Langem persönlich für den Erhalt der Natur auf den Malediven ein. 
  <br><br>
  Auch einige Hotels haben sich dem Thema Umweltschutz gewidmet und gute Programme zur Müllvermeidung, Stromsparen, Wasserverbrauch und zum Schutz der Korallen ins Leben gerufen. 
  <br><br>
  Gästen wird vor Ort  die Möglichkeit gegeben aktiv daran teilzunehmen.
  Erfolge sind vereinzelt hier und da zu verzeichnen, aber im Großen und Ganzen hat sich die Lage über die Jahre immer weiter verschlechtert. 
  <br><br>
  Auf den Malediven wird Strom mit Dieselgeneratoren erzeugt, Solarstrom steckt noch in den Kinderschuhen.
  Müll wird verbrannt.
  Hotels klären  heutzutage ihre Abwässer und leiten diese gefiltert ins Meer ab. 
  <br><br>
  Mit Müll wird z. B. die Lagune der Insel Thilafushi aufgeschüttet UND zur Neulandgewinnung genutzt, um dort Gewerbe anzusiedeln. 
  Restmüll wird verbrannt.
  Lediglich Plastikflaschen werden im etwas größeren Umfang recycelt.
  Die Verschmutzungen rund um die Einwohnerinseln sind oft besonders groß.
  Abfall wird oft achtlos entsorgt.  
</p>
