<h3>Inseln, die man meiden sollte</h3>
<p>
Schon seit Jahren kann man bei vielen Inseln erkennen, dass man sich gegen Bodenerosion und dem Anstieg des Meeresspiegels schützen muss.
Es wird versucht, die Inseln mit allen zur Verfügung stehenden Mitteln zu schützen.
Entweder durch Beton als Brandungsschutz, im Sand platzierte Sandsäcke, Wellenbrecher in Form von Steinen oder neu aufgeschütteten Sand.
<br><br>
Einige Maßnahmen sind gut gemacht und beeinträchtigen den Urlauber kaum, andere wiederum werden in der Pflege vernachlässigt und machen diese teilweise unansehnlich.
Auf Fotos sind diese Umstände selten zu sehen.
</p>
<h3>Bewohnte Nachbarinseln und Hotels in Flughafennähe sind oft problematisch</h3>
<p>
Je größer die Nachbarinsel, desto größer ist der Einfluss auf Ihre Resortinsel.
Liegt Ihr Hotel in Flughafennähe, wird man zusätzlich mit Fluglärm konfrontiert.
Es gibt nur einige Hotels, wo man aufgrund der Lage und des Engagements des Hotels, wenig oder fast keine störende Einflüsse bemerkt.
Unserer Erfahrung nach lohnen sich Inseln im Umkreis von 5 km des Flughafens nur für 1 bis 2 Tage, wenn man ein Hotel in der Nähe von Malé benötigt.
</p>
