<p>
  In China selbst kennt man das Problem, welch schlechtes Image chinesische Touristen im Ausland teilweise haben und geht bereits dagegen vor.
  Reisebüros sind verpflichtet, Kunden über das Zielland und seine Kultur vor Reiseantritt zu informieren.
  Das Verhalten von Touristen im Ausland ist ein Beurteilungsfaktor für Reisebüros und Agenturen in China und lässt auf Besserung hoffen und es ist bereits viel besser geworden.
  <br><br>
  Dennoch, <b>meiden Sie Resorts mit hoher Fluktuation</b>. Das Urlaubsgefühl kann durch viele An- und Abreisen mit Boot oder Flugzeug leiden.
  Reisende, die viel erleben und ausprobieren wollen, wechseln die Hotels innerhalb eines kurzen Zeitfensters von 2 bis 3 Tagen.
  Für Urlauber, die länger auf der Insel bleiben, könnte das ständige an- und abreisen von Hotelgästen lästig sein.
  Hier zu erwähnen sind vorwiegend Hotels, wo man mit Wasserflugzeugen anreist.
  Viele, oft laute, Starts und Landungen werden von vielen Gästen als Belästigung angesehen.
  <br><br>
  Asiatische Gäste sind 24/7 im Internet unterwegs, alles wird geteilt und dokumentiert. Man selbst hat kaum Chancen auf ein
  flüssiges Internet während der normalen Tageszeiten.
</p>
