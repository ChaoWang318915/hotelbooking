<h1>Andere Kulturen, andere Sitten</h1>
<p>
  Bis Ende September 2019 kamen 18,4 % aller Besucher auf den Malediven aus China!
  Die Malediven liegen in Asien und da ist es nur naheliegend, dass auf den Malediven viele asiatische Gäste anzutreffen sind.
  Spitzenreiter sind mit Abstand die Chinesen, gefolgt von indischen Gästen.
  <br><br>
  Zwischen Januar und September 2019 besuchten insgesamt 230.349 Chinesen die Malediven.
  In keinem anderen Urlaubsland der Welt sind die Chinesen so präsent wie hier und die Malediven gehören auch in China zu den Top-Reisezielen dieser Welt.
  China hat sich in den letzten Jahren zum größten Touristikmarkt der Welt entwickelt.
  <br><br>
  Auf den Malediven sind die Chinesen manchmal auffällig, da sich erstens alles auf einer winzigen Insel abspielt und zweitens die Sitten und Gebräuche sich von Europäischen sehr unterscheiden.
  Chinesen sind oft laut, rücksichtslos und hinterlassen oft eine Spur der Empörung hinter sich.
  Das gilt hauptsächlich, wenn sie in Gruppen reisen und nur 2 bis 3 Tage bleiben, bevor es in ein anderes Hotel oder Land weitergeht.
  <br><br>
  Probleme, die unmittelbar das Hotel und die Gäste betreffen, sind hier vorprogrammiert.
  Das Hotel muss sich entsprechend einrichten, im Restaurant muss die Küche selbstverständlich an die Gästestruktur angepasst werden, und durch kurze Aufenthaltsdauer entstehen hohe Fluktuationen und somit Unruhe im Hotel.
  <br><br>
  Wenn chinesische Gäste im Restaurant sind, ist es meist laut, Gespräche werden über 3 Tische geführt, Schmatzen und Rülpsen gehört zum guten Ton und Rücksichtnahme ist oft ein Fremdwort.
  Als Europäer muss man sehr tolerant sein und viel Geduld und Verständnis aufbringen.
</p>
<p>&nbsp;</p>

