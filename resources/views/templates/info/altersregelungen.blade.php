<h1>Ab welchem Alter ist ein Baby ein Kind und ab wann ist ein Kind ein Erwachsener?</h1>
<p>
Jedes Hotel auf den Malediven hat eine sogenannte <b>Child-Policy</b>. Diese Child Policy bestimmt ab welchem Alter ein Baby ein Kind ist und ab wann ein Kind ein Erwachsener ist.
Nicht jedes Hotel hat die gleiche Altersregelung. Einige Hotels haben für Babys oder Kinder jeweils ein unterschiedliches Alter. Das macht sich im Aufenthaltspreis bemerkbar.
Die normale Regelung sieht wie folgt aus:</p>
<ul>
<li> 0 - 1.99  Jahre = Baby</li>
<li> 2 - 12.99 Jahre = Kind</li>
<li> ab 13 Jahre = Erwachsener</li>
</ul>
