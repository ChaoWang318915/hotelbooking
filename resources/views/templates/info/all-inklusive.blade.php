<h3>All-Inklusive ist nicht immer All-Inklusive</h3>
<p>
  Man darf <b>All-Inklusive Resorts</b> nicht verwechseln mit den Hotels, die neben Frühstück, Halbpension und/oder Vollpension auch Upgrades auf ein oder mehrere <b>All-inclusive-Pakete</b> haben. Was viele Hotels anbieten.  
<br><br>
  Die meisten Hotels mit <b>Upgrades</b> auf All-inclusive-Paketen unterliegen Regeln, die besonders zu beachten sind. Die Texte stehen meistens im Kleingedruckten.
  Selbstverständlich haben auch die wirklichen All-Inklusive Resorts Regeln, jedoch nicht so umfangreich wie bei den normalen Resorts.
<br><br>
Was die Verpflegungspakete genau beinhalten und welche Regeln existieren, darüber sollte man sich vorher informieren und kann dann vergleichen.
So vermeidet man Enttäuschungen oder weitere Kosten vor Ort.  
<br><br>
  Einige Malediven Urlauber, die ein <b>All-Inklusive Upgrade</b> gebucht haben, beklagten vor Ort Gäste 2. Klasse gewesen zu sein: 
<br><br>
  <i>Das bestellte Essen oder die Getränke ließen auf sich warten, der Ober kam nicht regelmäßig vorbei, räumte den Tisch nicht ab und fragte auch nicht, ob man noch etwas haben möchte.
    Man konnte jedoch beobachten, dass es bei anderen Gästen im Hotel anders lief.</i>
<br><br>
  Der Service wurde offensichtlich in unterschiedlicher Qualität ausgeführt.
  Ein Grund dafür könnte sein, dass hier das Geld bereits verdient wurde.
  All-Inklusive Reisende geben in der Regel weniger <b>Trinkgeld</b> als Individualreisende, denn oft ist das Trinkgeld bereits im Aufenthaltspreis enthalten und so sind All-inclusive Gäste weniger attraktiv für Mitarbeiter. 
<br><br>
  Die Motivation einer Servicekraft, dessen Aussichten auf ein Trinkgeld gering sind, ist nicht besonders hoch.
  Sind dann noch andere Gäste vor Ort, die alles extra zahlen und womöglich großzügig Trinkgeld geben, könnte es gewisse Vorzüge bei der Bedienung geben, auch wenn das nicht so sein sollte. 
</p>
</div></div></div>


