<p>&nbsp;</p>
<p>&nbsp;</p>

<h2>Zusammenfassung</h2>
<p>
Wer alle die Ratschläge berücksichtigt und erst mal System in das ganze „sammeln“ reinbekommen hat,
der kommt innerhalb eines Jahres durchaus auf so zwischen 400 und 500 €, die man den Flugpreis reduzieren kann.
Je nach Konsum- und Einkaufsverhalten.
Wer darüber hinaus noch die Ratschläge befolgt, die wir oben gemacht haben, bekommt seinen Flug zu einem guten Preis.
</p>
<ul>
	<li>Flug weit im Voraus buchen</li>
	<li>Als Flugzeit Juli, August oder September wählen</li>
	<li>Nach Angeboten und Upgrades Ausschau halten</li>
	<li>Flüge mit Zwischenstopp buchen</li>
	<li>Miles & More Meilen sammeln</li>
	<li>Payback Punkte sammeln und umtauschen in Meilen</li>
	<li>Meilen bei Star Aliance Mitgliedern sammeln</li>
</ul>
