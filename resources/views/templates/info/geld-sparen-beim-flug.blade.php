<h1>Richtig planen heißt: Geld sparen, <br>so sparst du bei deinem Flug!</h1>

<p>
Bei einem Malediven Urlaub ist der Flug der unangenehme Teil.
Da sind nicht nur die rund 10 Stunden Flug, die es irgendwie zu überwinden geht, sondern auch der hohe Flugpreis.
Die Zeit vergeht schneller, wenn man in der Business- oder First-Class fliegt, mit rundum Verköstigung, angenehmer Unterhaltung und viel Platz.  
<br><br>
Die Preise sind oft genauso hoch oder sogar höher als der Hotelaufenthalt.
Einen Flug in der Business-Class gibt es ab circa 2.000 €, ein First-Class Flug kosten bereits zwischen 4.500 € und 6.500 €, das nur bestenfalls und pro Person.
Als Alternative bleibt oft nur die Premium-Economy mit rund 1.200 €.
Da ist die Economy-Class mit Preisen von teilweise unter 800 € sehr preiswert.
Wem 10 Stunden Flug am Stück zu viel sind, der macht Urlaub mit Zwischenstopp.
Zur Auswahl stehen Städte wie Dubai, Abu Dhabi, Doha oder Istanbul.
</p>


<div class="banner"><img src="../../images/content-images/sparen-beim-flug.jpg" alt="Bild: Sparen beim Flug"></div>
<h2>Kein Geheimnis, rechtzeitig buchen!</h2>
<p>
Wer seinen Flug rechtzeitig bucht, spart am meisten.
Die Fluggesellschaften werden nicht billiger, sondern immer teurer!
Es empfiehlt sich daher den Flug bereits 4 - 6 Monate im Voraus zu buchen, umso früher umso besser.
Aber daran denken, ein Flug der so lange im Voraus gebucht wurde, gehört abgesichert.
Deswegen auch immer eine Reiserücktrittsversicherung oder aber einen Flug im Flex Tarif buchen, der wiederum ist aber wieder ein paar Hundert Euro teurer.
Aber man kann ihn umbuchen und meistens auch kostenlos stornieren. Was bei der Reiserücktrittsversicherung nur bedingt möglich ist. 
</p>
<p>&nbsp;</p>

<h2>Flüge mit Zwischenstopp buchen</h2>
<p>
Die Flüge mit Zwischenstopp sind preiswerter. Allerdings verlängert man seine Reisezeit erheblich. Wem das nichts ausmacht, spart hier weitere Euros.
</p>
<p>&nbsp;</p>

<h2>Frühzeitig nach Upgrades oder Angebote schauen</h2>
<p>
Wer aber nur auf die Malediven will und das so schnell wie möglich, dem empfehlen wir Condor.
Die Flüge sind von Frankfurt direkt und die Preise und Service akzeptabel. Allerdings gibt es wenig Upgrade-Möglichkeiten.
Das Kabinenpersonal fragt ab und an, ob jemand upgraden will in die Business-Class, ein richtiges „Schnäppchen“ macht man allerdings nicht wirklich. 
<br><br>
Bei Condor sollte man auf die Fliegenangebote warten und hoffen das Malé mit in den Angeboten ist.
Aber auch so früh wie möglich zu buchen ist bei Condor wichtig, um preiswerte Flüge zu bekommen. Besser ist da Lufthansa und die Star-Allianz.
</p>
<p>&nbsp;</p>

<h2>In der preiswertesten Reisezeit buchen</h2>
<p>
	Wer flexibel in der Reisezeit ist, kann in der preiswertesten Reisezeit seinen Urlaub auf den Malediven buchen.
Die Monate Juli, August und September sind nicht nur für Flüge gute Zeiten, sondern auch für die Hotels auf den Malediven ist diese Reisezeit die beste Zeit mit den besten Angeboten und Hotelpreisen.
	Es variiert ein wenig von Hotel zu Hotel und von Monat zu Monat. Wichtig ist, es darf nicht in unseren Schulferienzeiten sein.
	Da werden die Flugpreise generell höher angeboten.
</p>
<p>&nbsp;</p>

<h2>Lufthansa Upgrades + Miles & More </h2>
<p>
Lufthansa hat da ein paar nette Features, die wir sehr mögen.
Da ist zum Beispiel das Sammeln von Meilen mit Miles & More, was Condor leider seit Juni 2021 nicht mehr anbietet.
Die Meilen kann man für Flüge, Sitzplatzupgrades oder anderes (z. Bsp. Lounge Zugang) verwenden. 
<br><br>
Richtig cool aber ist das Bieten auf Upgrades bei Lufthansa:
Nachdem du deinen Flug gebucht hast, machst du Lufthansa ein Angebot, wie viel du bereit bist mehr zu bezahlen, wenn du in eine höhere Sitzplatzkategorie upgradest.
Du kannst, wie bei einer Auktion, einen Preis abgeben, den du bereit bist für eine Sitzplatzklasse der höheren Kategorie zu bezahlen.
Rund 36 - 24 Stunden vor Abflug erfährst du ob Lufthansa das Angebot von dir annimmt. 
<br><br>
Hier kann man richtig sparen, zwar ohne Garantie auf Erfolg, aber man kann Lufthansa immerhin ein Angebot machen und den Preis selbst bestimmen, den man bereit ist, zu zahlen.
Manchmal klappt es, manchmal nicht.
Lufthansa hat auch am Schalter am Flughafen oft einen Hinweis auf Upgrade-Möglichkeiten, allerdings nicht immer preiswerter.
</p>
<p><b>Unser Tipp: </b> Die Premium-Economy buchen und auf die Business-Class bieten.
Natürlich kann man auch die Economy-Class buchen und auf Premium-Economy oder Business-Class bieten.
Übrigens, der Rückflug nach Deutschland dauert länger. Wer also nur eine Strecke upgraden will, sollte den Rückflug wählen.
</p>
<p>&nbsp;</p>

<h2>Die goldene Miles & More Kreditkarte</h2>
<p>
Wenn du dir eine Miles & More Kreditkarte zulegst, sammelst du automatisch Meilen mit jedem Euro Umsatz, den du mit der Karte machst. 
Bei der goldenen Miles & More Kreditkarte verfallen diese Meilen auch nicht.
Die Karte ist kostenpflichtig, dafür bekommst du schon bei der Beantragung 4000 Premiummeilen gutgeschrieben und hast einige nützliche Versicherungen enthalten.
Weiterhin gibt es während des Jahres etliche Aktionen wo du deine Meilen noch verdoppeln oder vervielfachen kannst. So kommen noch schneller Premiummeilen auf dein Konto.
(Anbieter: https://www.miles-and-more-kreditkarte.com)
<br><br>
All die gesammelten Meilen kann man bei Lufthansa gegen Rabatt auf den Flugpreis eintauschen oder bei Miles and More gegen Upgrades.
</p>
<p>&nbsp;</p>

<h2>Star Aliance</h2>
<p>
Wer mehrmals im Jahr mit dem Flieger unterwegs ist, sollte prüfen, ob die Möglichkeit besteht mit Lufthansa oder einem Partner aus der Star Aliance zu fliegen.
Denn bei allen Partnern der Star Aliance sammelt man ebenfalls (Status) Meilen für seine Flüge.
Vorher immer prüfen, ob es eine Aktion gibt, die Meilen verdoppeln oder vervielfachen, zu einigen muss man sich extra anmelden, um davon zu profitieren.
</p>
<p>&nbsp;</p>


<h2>Payback</h2>
<p>
Eine weitere Möglichkeit Meilen zu sammeln ist über den „Umweg“ von Payback Punkte zu sammeln.
Wer kein Problem damit hat, kann bei vielen Geschäften für seine Einkäufe Payback Punkte sammeln.
Auch hier gibt es die Möglichkeit sich über E-Mail oder APP Angebote einzuholen, mit denen man seine Punkte vervielfachen kann.
Aufs Jahr gesehen kommen etliche Tausend Payback Punkte zusammen.
Man kann diese automatisch, wenn man mind. 2.500 Punkte erhalten hat, alle paar Monate auf sein Miles & More Konto in Form von Premium Meilen umschreiben lassen.
</p>
<p>&nbsp;</p>


<h2>Meilen Optimieren</h2>
<p>
	Wenn du weiter in das Thema einsteigen möchtest, mehr Meilen sammeln willst oder wenn du wissen willst was du für deine Meilen bekommen kannst,
	dann ist die Seite <b>meilenoptimieren</b>, eine weitere Anlaufstelle.
	Hier holst du noch mehr aus deinen Meilen raus und kannst noch mehr Meilen sammeln für deinen reduzierten oder sogar kostenlosen Business-Class Flug.
	Selbstverständlich findet Ihr auch auf der Miles and More Webseite Anregungen wie du deine Meilen vervielfältigen kannst.
</p>


