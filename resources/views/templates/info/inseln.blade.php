<h1>Die Inseln der Malediven</h1>
<figure><img src="../../images/content-images/mosche-malediven.jpg" alt="Mosche Malediven"></figure>
<p>
Die Malediven bestehen aus über 1.190 Inseln.<br>
Diese Zahl ändert sich ständig. Einige kommen neu hinzu, andere verschwinden von heut auf morgen wieder.
<br><br>
Knapp 200 Inseln werden von Einheimischen bewohnt und etwa 200 Inseln sind sogenannte Resortinseln. 
Eine Insel, ein Resort! 
Dieses Prinzip gilt bei annähernd allen Resorts auf den Malediven. 
In der letzten Zeit sind immer mehr Gästehäuser entstanden (knapp 500 bis jetzt). 
Diese befinden sich meist auf einheimischen Inseln und werden oft auch von einheimischen Familien betrieben. 
Einige haben bereits mehrere Gästehäuser.
<br><br>
Auf den Malediven befinden sich einige der besten und teuersten Hotels der Welt. 
Der Tourismus ist auch die größte Einnahmequelle. 
Industrie gibt es so gut wie keine, fast alle Erzeugnisse werden aus Europa, Sri Lanka oder Indien importiert.
</p>

<h2>Inseln und Strände</h2>
<div class="row pt-4">
<div class="col-md-4"><figure><img src="images/img-29.jpg" alt="#"></figure></div>
<div class="col-md-8">
<h3>Die Strände</h3>
<p>
Die Strände um eine Insel sind in ständiger Bewegung, die Gezeiten und Windverhältnisse verändern die Form der Insel manchmal über Nacht. 
Der Einfluss des Monsuns kann die ganze Insel innerhalb von 6 Monaten komplett verändern. 
Wenn man also aus dem letzten Urlaub noch den Traumstrand in Erinnerung hat, kann es diesmal ein enttäuschender Strand sein. 
Auch das Zimmer was man letztes Jahr noch hatte, vor dem wunderschönen Strandabschnitt, kann heute voll mit Sandsäcken sein. 
Bilder auf Webseiten, Ansichten in Google Maps oder Fotos müssen nicht der Realität entsprechen. 
Das Klima verändert die Insel und das kann ziemlich schnell passieren. 
</p>
</div></div>
<p>&nbsp;</p>

<div class="row pt-4">
<div class="col-md-4"><figure><img src="images/img-29.jpg" alt="#"></figure></div>
<div class="col-md-8">
<h3>Erosionen</h3>
<p>
Man kennt es vielleicht, die häßlichen Mauern um die Insel herum, Sandsäcke am Strand oder Betonmauern die als Wellenschutz fungieren.
Um Erosion entgegenzuwirken haben viele Inseln auf den Malediven ein Küstenschutzprogramm implementiert.
Das ist vielleicht nicht jedermanns Vorstellung von einem Tropenparadies aber es ist unvermeidlich.
Nur so läßt sich die Insel und das Hotel schützen.
Die getroffenen Maßnahmen sind unterschiedlich, einige sind gut gemacht, einige soweit das es möglichst umweltverträglich ist,
bei anderen einfach nur billig und ohne Rücksicht auf Gäste oder Umweltverträglichkeit.
Am meisten zu bemängeln wäre aber die fehlende Kommunikation gegenüber den Gästen in Form einer ehrlichen (besser vollständigen) Hotelbeschreibung.
</p>
</div></div>
<p>&nbsp;</p>

<div class="row pt-4">
<div class="col-md-4"><figure><img src="images/img-29.jpg" alt="#"></figure></div>
<div class="col-md-8">
<h3>Korallen & Riffe</h3>
<p>
Korallen sehen über dem Wasser aus wie graue oder schwarze Steine am Meeresgrund.
Die Realität ist aber, dass es sich um eine Ansammlung von Millionen von Organismen handelt.
Diese Organismen spielen eine Große Rolle im Schutz der Insel.
Sie sind es die die Wellen brechen lassen, sie wachsen mit dem Meeresspiegel immer dem Licht nach, steigt der Meeresspiegel wachsen auch die Korallen, sie bieten dadurch der Insel einen Schutz vor Wellen und geben Ihr Stabilität.
Fast 1/4 der Meeresbewohner finden in Korallen Schutz und Nahrungsquellen. Ohne Korallen/Korallenriffe keine Insel! Korallen kann man leicht beschädigen, filigrane Verästelungen brechen einfach ab.
Wenn man auf Korallen rauftritt hat man die Koralle zerstört und sie stirbt ab.
Jahrelanger Wachstum ist nötig um nur 1 cm zu wachsen, mit dem zerstören von Korallen zerstört man ein über Jahrzehnte gewachsenes Ökosystem.
Am Urlaubsort können Korallen ziemlich gefährlich sein. Korallen sind sehr scharf und es kann zu Schnittverletzungen kommen die sich entzünden können.
Weiterhin verstecken sich Meeresbewohner zwischen den Korallen, einige von Ihnen sind giftig oder "bissig" wenn man Ihnen zu nahe kommt.
</p>
</div></div>
