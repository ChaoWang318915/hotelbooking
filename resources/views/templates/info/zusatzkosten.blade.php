<h1>Zusatzkosten</h1>

<p>
  Wir würden mal behaupten, was die Berechnung von Zusatzkosten angeht, sind die maledivischen Hotels wahre Meister.
  Diese noch zu verstecken ist die Paradedisziplin vieler Hotels.
  Dieses Schreckgespenst namens Zusatzkosten sorgt für Ärger und Enttäuschung bei Urlaubern.
  Es gibt Zusatzkosten, von denen wusste man nichts und es gibt Zusatzkosten, von denen man eigentlich auch nichts wissen will.
  Ein Urlaub auf den Malediven ohne Zusatzkosten ist fast unmöglich, irgendetwas kommt immer hinzu, garantiert.
</p>
<p>&nbsp;</p>
<div class="inform-block">
<div class="row">
<div class="col-md-5"><figure><img src="../../images/content-images/zusatzkosten-tax.jpg" alt="Bild: Steuern und Gebühren"></figure></div>

<div class="col-md-7">
<h3>Oft vergessen, 10 % Service-Charge und 12 % T-GST</h3>
<p>
Schnelleres  Wi-Fi,  zusätzliche Speisen und Getränke, die nicht in der gebuchten Verpflegung enthalten sind, Wassersport, Sport- und Freizeitaktivitäten oder Ausflüge und Serviceleistungen; vieles wird extra berechnet.
Das hört sich erst einmal normal an.
Viele dieser Dinge werden auch in anderen Hotels dieser Welt extra berechnet. 
In den Hotels werden die Preise meist sehr transparent dargestellt. 
</p>
</div></div></div>

<p>
Will man die Preise aber vor Anreise erfahren, um seine Urlaubskosten zu planen, könnte das vielleicht ein Problem werden.
Nicht alle Hotels veröffentlichen die Menükarten mit Preisen.
Die Wassersportzentren werden meistens von Drittanbietern betrieben.
Auch hier findet man normalerweise keine Preisliste bevor man im Hotel ist.
<br><br>
Viele Urlauber buchen deshalb auf den Malediven ein <b>All-Inklusive Urlaub</b> Nur von den „wahren“ <b>All-Inklusive Resorts</b> gibt es sehr viele, auch wenn in den letzten Jahren einige dazu gekommen sind  <small>(siehe dazu <a href="https://www.my-maldives.com/informationen/verpflegungsarten">Verpflegung</a>)</small>.
Hotels, die ein All-Inklusive-Verpflegungspaket anbieten, gibt es, hingegen viele. Doch zwischen All-Inklusive-Resort und All-Inklusive-Verpflegungspaket gibt es massive Unterschiede. 
<br><br>
Viele Urlauber vergessen aber oder wissen es nicht: Auf alle Preise kommt auf den Malediven noch eine 10 % Servicegebühr und eine 12 % Touristensteuer hinzu. Die angezeigten Preise beinhalten diese „Steuern und Gebühren“ <b><u>nicht.</u></b>
Das bedeutet, dass alle Preise die Sie im Hotel sehen, in Wirklichkeit um 23,2 % höher sind. (Ja, auf die 10 % Servicegebühren kommen ebenfalls 12 % Steuern!). Das bedeutet, alles ist in Wirklichkeit um fast ein Viertel teurer.
</p>

<div class="inform-block">
<div class="row">
<div class="col-md-5"><figure><img src="../../images/content-images/zusatzkosten-tennis.jpg" alt="Bild: Tennis"></figure></div>
<div class="col-md-7">
<h3>Da fehlt doch was ... </h3>
<p>
Verrückt wird es, wenn es so kommt: Wer an „kostenlos Tennis“ denkt, der kommt einfach nicht darauf, dass Tennisbälle 5 US-Dollar kosten und weil das Tennis ja „kostenlos“ ist, bringt natürlich auch keiner Bälle mit.
Es ist wichtig, sich zu vergewissern, was im Preis enthalten ist und viel wichtiger: Was muss man noch bezahlen, welche Kosten kommen auf einen noch hinzu.
</p>
 </div></div></div>

 <div class="inform-block">
 <div class="row">
 <div class="col-md-5"><figure><img src="../../images/content-images/zusatzkosten-zimmer.jpg" alt="Bild: Zimmer"></figure></div>
 <div class="col-md-7">
<h3>Doofe Tricks denken Sie? <br>Was halten Sie dann von diesem?</h3>
<p>
Sie buchen ein Zimmer, nicht das teure, das preiswertere, am Strand, nennen wir es Strandbungalow.
<br><br>
Stellen Sie sich vor, Sie kommen in ihr Zimmer und nichts ist so wie Sie sich es vorgestellt haben.
Die Matratzen sind durchgelegen, der Kaffee fehlt.
Es gibt nur ein oder zwei Handtücher, nebenan wird gebaut und der Strand ist hier auch nicht so schön wie bei den anderen Zimmern.
Sie sind unzufrieden. Was machen Sie, Sie gehen an die Rezeption und beschweren sich.
<br><br>
Hier hat man auch gleich die Lösung für Sie: Ein viel besseres Zimmer, natürlich doppelt so teuer. Sie müssen zuzahlen.
Selbstverständlich nimmt man das in Kauf, man will ja nur raus aus dem schlechten Zimmer und dass man zuzahlen muss leuchtet vielleicht sogar noch ein, es ist ja schließlich ein höherwertiges Zimmer.
Also zahlt man, vor allem wenn einem noch dazu ein großzügiger Rabatt für die Unannehmlichkeiten eingeräumt wird.
Und schon sind alle wieder glücklich!
<br><br>
Was, aber wenn ich Ihnen erzähle, dass dies nichts weiter als ein ausgeklügelter Plan war, Ihnen einfach noch mehr Geld abzunehmen? <br>
Geben Sie „unserer kleinen Geschichte“ ein passendes Ende.
</p>
 </div></div></div>

 <div class="inform-block">
 <div class="row">
 <div class="col-md-5"><figure><img src="../../images/content-images/zusatzkosten-verpflegung.jpg" alt="Bild: Verpflegung"></figure></div>
 <div class="col-md-7">
<h3>Die Sache mit der Verpflegung</h3>
<p>
Wer All-Inclusive bucht denkt nicht daran, das man seine Speisen immer nur in einem Restaurant, dem Hauptrestaurant, zu festen Essenszeiten zu sich nehmen muss.
Jedes Essen in einem anderen Restaurant kostet extra Geld. Dort bekommt man bestenfalls einen Rabatt, weil man nicht im Hauptrestaurant speist.
<br><br>
Gehen Sie auch davon aus, dass wenn Sie <b>All-Inclusive buchen</b>, ihre Getränke inklusive sind?
Sind sie auch, nur von 10 Uhr morgens bis 22 Uhr abends <small>(Zeiten variieren je nach Hotel)</small>. Jedes Getränk ausserhalb dieser Zeiten kostet extra.
<br><br>
Der <b>Zimmerservice</b> könnte ebenfalls kostenpflichtig sein und sowohl Service als auch Speisen und Getränke trotz "All-Inclusive" extra berechnet werden.
Die Minibar ist ein weiterer Punkt. Hier lauern die versteckten Kosten.
</p>
</div></div></div>

<div class="inform-block">
<div class="row">
<div class="col-md-5"><figure><img src="../../images/content-images/zusatzkosten-wifi.jpg" alt="Bild: Wi-Fi"></figure></div>
<div class="col-md-7">
<h3>Wi-Fi kostenlos </h3>
<p>
Wenn Sie lesen: "Wi-Fi kostenlos", dann ist dem sicher auch so, aber wenn das laden einer E-Mail Ewigkeiten zu dauern scheint, ist man nur noch genervt.
Oft liegt das an der Gesamtanzahl der Gäste, die meist alle zur gleichen Zeit ins Internet gehen.
Streaming oder Videos sind vielerorts garnicht oder nur gedrosselt möglich.
Oft regeln die Hotels die Nutzung und begrenzen den Zugang auf E-Mails und normale Nutzung des Internets.
Weiterhin geben einige Hotels nur ein bestimmtes Datenvolumen pro Aufenthalt kostenlos. Wer mehr möchte muss bezahlen.
<br><br>
Einige Hotels offerieren natürlich ein Upgrade auf <b>Highspeed-Internet</b>, das allerdings wird man extra bezahlen.
Hier lauern die versteckten Kosten.
Zu allen Kosten kommen immer noch 10 % Service Gebühren und 12 % Steuern (T-GST)
</p>
</div></div></div>

<div class="inform-block">
<div class="row">
<div class="col-md-5"><figure><img src="../../images/content-images/zusatzkosten-weihnachten.jpg" alt="Bild: Weihnachten"></figure></div>
<div class="col-md-7">
<h3> Weihnachten und Neujahr </h3>
<p>
Einige Gäste wollen eigentlich dem Weihnachtsstress und den traditionellen Zeremonien in Deutschland entfliehen.
Leider holt einen auf den Malediven bei vielen Hotels die Heimat wieder ein.
Denn viele Hotels auf den Malediven bieten den Gästen zu Weihnachten ein Gala-Dinner und eine Silvesterfeier an. 
<br><br>
Das Gala-Dinner ist für alle Gäste obligatorisch und wird in den meisten Hotels teuer berechnet.
Welches Verpflegungspaket Sie auch immer haben, an Weihnachten und Neujahr berechnen viele Hotels das Weihnachts- oder Neujahr Gala-Dinner extra.
Ob man will oder nicht. 
<br><br>
Auch unser Neujahr oder das Chinesische Neujahr wird zum Anlass genutzt ein Gala-Dinner zu veranstalten.
Alles Tage an dem sich der Aufenthaltspreis manchmal fast verdoppelt.
Einige Hotels berechnen das Gala-Dinner zum chinesische Weihnachtsfest und Neujahr nur für chinesische Gäste, andere Hotels für alle Gäste. 
<br><br>
An diesen Tagen gilt das gebuchtes Verpflegungspaket nicht, man muss es auch nicht bezahlen, da man ja das Gala-Dinner bezahlt.
Auch beim Gala-Dinner gelten unterschiedliche Regeln.
Bei einigen ist eine Flasche Wein, Sekt oder gar Champagner enthalten, bei anderen ist nur das Dinner enthalten. Getränke müssen extra bezahlt werden.
</p>
</div></div></div>

