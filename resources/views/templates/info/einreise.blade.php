<h1>Malediven <br>Einreisebestimmungen und Visum</h1>
<figure><img src="../../images/content-images/mosche-malediven.jpg" alt="Mosche Malediven"></figure>


<div class="row">
<div class="col-lg-9">
<div class="box">
<p><b>Wichtige Informationen</b></p>
<p>
Wir machen darauf aufmerksam, dass die folgenden Informationen ausschließlich für Touristen gelten, die die Malediven besuchen möchten.
Jede andere Art des Aufenthalts (Arbeitsvisa, Einreise für Geschäftsleute etc.) können andere Einreisebestimmungen mit sich bringen, die hier nicht aufgeführt werden.
Bitte informieren Sie sich bei den entsprechenden Stellen.<br><br>
<b>Beachten Sie bitte:</b> Es handelt sich um Einreisebedingungen, die während der Covid-19 Pandemie gelten und sich jederzeit ändern können.
</p>
</div></div></div>


<h2>Einreisebestimmungen und Visum</h2>
<div class="row pt-4">
<div class="col-md-4">
<figure><img src="../../images/content-images/Einreise-Visa.jpg" alt="Bild: Einreise Pass"></figure>
</div>
<div class="col-md-8">
<h3>Einreisebestimmungen</h3>
<p>
Die Einreise in die Malediven ist für Deutsche, Schweizer und österreichische Staatsbürger mit einem Reisepass möglich, der bei Einreise noch 1 Monat Gültigkeit haben muss.
Kinder im Reisepass der Eltern sind seit dem 26.06.2012 nicht mehr zulässig.
Jedes Kind benötigt einen eigenen Reisepass.
</p>
<ul>
<li>Der Reisepass muss eine maschinenlesbare Zone (MRZ) haben.</li>
<br>
<li>Eine im Voraus bezahlte Hotelbuchung muss bei Aufforderung bei Einreise belegbar sein.</li>
<br>
<li>	Es müssen ausreichende Geldmittel für die geplante Aufenthaltsdauer auf den Malediven mitgeführt werden.</li>
<br>
<li>	Ein bestätigtes Weiterreise- / Rückreiseticket in das Heimatland oder in das Land des Wohnsitzes muss vorhanden sein.</li>
<br>
<li>	Sollte für die Weiterreise in ein anderes Land ein Visum benötigt werden, ist dieses ebenfalls nach Aufforderung vorzuzeigen.</li>
<br>
<li>	Wenn Sie aus einem Gelbfiebergebiet kommen, benötigen Sie eine Gelbfieber-Impfbescheinigung.
<small>(Klicken Sie<a href="https://immigration.gov.mv/wp-content/uploads/2021/06/YELLOW-FEVER-COUNTRIES-2.pdf"> hier</a>, um die Gelbfieber-Endemiegebiete zu überprüfen.)</small></li>
</div></div>
<p>&nbsp;</p>
<p>&nbsp;</p>

<div class="block">
<h3>Visum</h3>
<p>
	Die Einreise wird für max. 30 Tage gewährt. 
	Der Besucher hat keinen Anspruch auf diese Aufenthaltsdauer, es liegt im Ermessen der maledivischen Einwanderungsbehörde, eine beliebige Anzahl von Tagen zu gewähren, die kleiner oder gleich 30 ist. 
<br><br>
Die bewilligte Aufenthaltsdauer ist im Reisepass vermerkt.
Besuchern wird empfohlen, ihren Reisepass auf den Ankunftsvermerk zu überprüfen und die Anzahl der gewährten Aufenthaltstage auf den Malediven zu notieren.
Nach der Ankunft als Tourist ist es dem Besucher nicht gestattet, während seines Aufenthalts auf den Malediven irgendeiner Form von Geschäfte, Beschäftigungen, einem Beruf oder bezahlter Arbeit nachzugehen.
Alle Reisenden werden auch daran erinnert, dass es eine strafbare Handlung ist, sich länger als die angegebene Anzahl von Tagen auf den Malediven aufzuhalten.
</p>
<p>Ein Visum wird direkt bei Einreise erteilt. </p>
</div>

<div class="block">
<h3>Covid-19</h3>
<p>
<b>Für die Einreise auf die Malediven benötigen Sie einen negativen PCR-Test, der nicht älter als 96 Stunden ist.</b>
<br><br>
2020 haben die Einwanderungsbehörden der Malediven die „Gesundheitserklärung für Reisende“ <small>(Traveller Health Declaration, THD)</small> eingeführt.
Der Prozess der Gesundheitsinformationen für die Ein- und Ausreise soll gemäß den Gesundheitsanforderungen der Malediven so beschleunigt werden.  
<br><br>
Alle Reisenden, die auf die Malediven einreisen oder von den Malediven abreisen, müssen innerhalb von 48 Stunden vor ihrer Ein- und Ausreise eine selbsterklärende Gesundheitserklärung abgeben. 
Die Gesundheitserklärung für Reisende muss über das Online-Portal der maledivischen Einwanderungsbehörde, <a href="https://imuga.immigration.gov.mv/ethd/create"> "Imuga",</a> ausgefüllt und eingereicht werden. 
<br><br>
Sie kann auch auf der offiziellen Website der <a href="https://www.immigration.gov.mv">Einwanderungsbehörde der Malediven</a>, eingesehen werden, indem man in der Menüleiste „Imuga“ auswählt. 
Alle Reisenden, einschließlich Touristen, müssen die Gesundheitserklärung für Reisende abgeben.
Hauptziel ist es, gesundheitliche Risiken zu minimieren und das Einwanderungsverfahren zu erleichtern. 
<br><br>
Sie benötigen folgende Unterlagen:</p>
<ul>
<li>Ein Foto von sich (Gesicht und Teil der Schulter)</li>
<li>Eine Kopie des negativen PCR-Ergebnisses des COVID-19-Tests, nicht älter als 96 Stunden</li>
<li>Impfnachweis, wenn Sie geimpft sind (erste und zweite Impfung)</li>
</ul>
<p>
Der Antrag ist in englischer Sprache. Sie erhalten ein QR-Code nach Prüfung durch die IMUGA per E-Mail zugeschickt.
Drucken Sie sich dieses Schreiben aus oder speichern Sie es auf Ihrem Handy.
</p>

<h4>Mund- und Nasenschutz</h4>
<p>Alle Touristen sind verpflichtet, am Flughafen, bei Inlandsreisen und in allen geschlossenen öffentlichen Räumen einen Mundschutz zu tragen.</p>
<h4>Abstandsregeln</h4>
<p>Alle Abstandsrichtlinien sind in den Passagierterminals durch sichtbare Abstandsangaben und Bodenmarkierungen deutlich gekennzeichnet. Händedesinfektions- und Hygieneeinrichtungen sind ebenfalls im gesamten Flughafen vorhanden.</p>
<p>&nbsp;</p>
<p><b>Bitte beachten Sie, das sämtliche Informationen sich ohne Ankündigung jederzeit ändern können. Achten Sie daher auf die Angaben der Maledivischen Einreisebehörden oder rufen Sie uns an, gern stehen wir Ihnen bei Ihnen Reiseplanungen zur Verfügung.</b></p>
</div>

<div class="row">
<div class="col-lg-9">
<div class="box">
<p><b>Wir kümmern uns um die Gesundheitserklärung</b></p>
<p>
Auf Wunsch und gegen Gebühr kümmern wir uns um die Angaben der Gesundheitserklärung.
Wir fragen die Daten bei Ihnen telefonisch ab und schicken diese an die IMUGA sowohl für Ihre <u>Einreise</u> als auch für die <u>Rückreise</u>.
Sie brauchen sich um nichts zu kümmern.
Sie erhalten alle Unterlagen per E-Mail direkt zugeschickt.
Wir berechnen für die Antragsstellung der Ein- und der Ausreise <b>20 € (inkl. 19 % MwSt) pro Person</b>.
Den Service buchen und weitere Informationen finden Sie <a href="https://wwww.my-maldives.com/gesundheitserklaerung-buchen">hier.</a>
</p>
</div></div></div>




<h2>Zollvorschriften</h2>
<figure><img src="../../images/content-images/Einreise-Zoll.jpg" alt="Symbolbild: Zoll"></figure>
<p>
Die Einfuhr von pornografischem Material (oder auch nur erotisches Material = leicht bekleidete Personen) und Alkohol ist nicht erlaubt und wird bei der Einreise eingezogen und erst bei der Ausreise (wenn überhaupt) wieder ausgehändigt.
In letzter Zeit ist man dazu übergegangen diese Sachen gar nicht mehr auszuhändigen, sondern gleich zu vernichten.
<br><br>
Generell ist darauf zu verzichten, da das Prozedere anstrengend und aufwendig ist.
Neuerdings werden diese Materialien nicht nur eingezogen, sondern auch vernichtet, das heißt es gibt diese bei Ausreise nicht mehr zurück.
Die folgenden Sachen sind von der Einfuhr, Herstellung, Handhabung, Verkauf, Vertrieb oder als Geschenk auf den Malediven verboten:
</p>
<ul>
<li>Religiöse Materialien, egal welcher Art</li>
<li>Idole (für Anbetungen)</li>
<li>Pornografisches Material</li>
<li>Betäubungsmittel oder Drogen</li>
<li>Lebende Schweine</li>
</ul>
<p>
Die Einfuhr und der Besitz von Drogen steht unter Strafe, das kann auch bei Ausländern lebenslänglich Gefängnis bedeuten.
Import beschränkte Artikel (gilt nicht für Regierungsbehörden) sind unter anderem:
</p>
<ul>
<li>Waffen und Munition</li>
<li>Alkohol und Spirituosen</li>
<li>Schweinefleisch und seine Nebenprodukte</li>
<li> Hunde und gefährliche Tiere</li>
</ul>
<p>Artikel, die mit entsprechender Bescheinigung importiert werden können:</p>
<ul>
<li>Lebende Pflanzen und Tiere mit einem gültigen Pflanzengesundheitszeugnis</li>
</ul>
</p>
