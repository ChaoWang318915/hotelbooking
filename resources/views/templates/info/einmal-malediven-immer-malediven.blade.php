<h1>Einmal Malediven, immer Malediven</h1>
<p>
  Urlauber, die einmal auf die Malediven kommen, werden in der Regel zu Wiederholungstäter und kommen immer wieder zurück auf die Malediven.
  Andere Gäste möchten den Urlaub vielleicht in mehr als einem Hotel verbringen.
  Wenn man schon einmal die Malediven besucht, dann will man mehr sehen und ausprobieren.
  <br><br>
  Sicherlich ist es reizvoll zwei oder drei Hotels zu besuchen, wenn die Zeit dafür ausreicht.
  Gäste, die nur 2 bis 3 Tage bleiben, erhalten vielleicht nicht das schönste Zimmer im Hotel.
  Andere Hotels jedoch nutzen die Gelegenheit und bieten ein kostenloses Upgrade auf eine höherwertige Zimmerkategorie an, um so den Gast für einen nächsten Besuch zu gewinnen.
   <br><br>
  Einige Verpflegungsangebote wie All-Inklusive gelten oft nur in Verbindung mit einem Mindestaufenthalt.
  Auch Rabattaktionen gelten oft erst ab einer bestimmten Anzahl von Übernachtungen.
  <br><br>
  Je nachdem wie es arrangiert wird und von welchem Hotel zu welchem Hotel man möchte, führt der Transfer immer erst wieder zurück nach Malé.
  Andernfalls muss man einen Privattransfer bezahlen.
  Liegen die Hotels nicht weit auseinander, kann es sein, dass man einen Transfer direkt vom Hotel zu Hotel arrangieren kann.
  <br><br>
  Generell ist Island Hopping, also von Insel zu Insel springen, relativ teuer.
  Ein zusammenhängender Aufenthalt in einem Hotel verspricht mehr Komfort, ist preiswerter und man bekommt alle Rabatte <small>(soweit es welche gibt zu der Reisezeit). </small>
</p>
