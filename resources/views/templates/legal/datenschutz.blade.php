<h3>Datenschutzhinweise</h3>
<p>
Wir nehmen den Schutz Ihrer persönlichen Daten sehr ernst und verarbeiten diese Daten daher im Einklang mit den gesetzlichen Datenschutzgesetzen gemäß der EU-DSGVO.
Mit den nachfolgenden Informationen zum Datenschutz möchten wir Sie darüber informieren, wie wir Ihre persönlichen Daten verwenden.
Insbesondere möchten wir, dass Sie wissen, wie wir Ihre Daten weiterverarbeiten, auf welche Weise und wofür wir Ihre Daten nutzen.
</p>
<h3>Was sind persönliche Daten</h3>
<p>
Persönliche Daten sind Informationen, die einen Bezug auf Ihre Person aufweisen können, durch die ein Rückschluss auf Ihre Identität möglich ist.
</p>
<h3>Grundsätze für die Verarbeitung personenbezogener Daten</h3>
<p>
Wir verarbeiten keine ganz oder teilweise automatisierten personenbezogenen Daten von Ihnen.
<br>
Unsere Datenschutzhinweise gelten daher für die nicht automatisierte Verarbeitung personenbezogener Daten, die wir in unseren Dateisystemen speichern bzw. speichern werden.
Wir versichern Ihnen, dass wir Ihre personenbezogenen Daten auf rechtmäßige und in einer für Sie nachvollziehbaren Weise verarbeiten werden.
Ihre personenbezogenen Daten nutzen wir nur für festgelegte und eindeutige Zwecke und verwenden diese nicht in einer mit diesen Zwecken nicht zu vereinbarenden Weise.
Weiterhin beschränken wir die Verarbeitung Ihrer personenbezogenen Daten auf das notwendige Maß.
Ihre personenbezogenen Daten sind vor unbefugter, unrechtmäßiger Verarbeitung und vor Verlust geschützt.
</p>
<h3>Rechtmäßigkeit der Verarbeitung</h3>
<p>
	Die Verarbeitung Ihrer Daten ist rechtmäßig, wenn Sie uns Ihre Einwilligung zu der Verarbeitung Ihrer personenbezogenen Daten für einen oder mehrere bestimmte Zwecke gegeben haben.
	Weiterhin ist die Verarbeitung Ihrer Daten rechtmäßig, wenn diese für die Erfüllung eines Vertrags erforderlich ist, dessen Vertragspartei Sie sind, oder zur Durchführung vorvertraglicher Maßnahmen, die auf Anfrage von Ihnen erfolgt.
	Die Angaben ihrer personenbezogenen Daten sind an verschiedenen Stellen für die Erbringung einer oder mehrerer Leistungen erforderlich.
	Wir speichern Ihre Daten, wenn Sie auf unseren Webseiten eine Buchung vornehmen oder eine Buchungsanfrage an uns richten.
	Wir speichern Ihre Daten auch, wenn Sie uns telefonisch damit beauftragen für Sie eine Leistung zu buchen bzw. wenn Sie telefonisch eine Buchungsanfrage stellen.
	Ohne diese Daten werden wir in der Regel den Abschluss des Vertrages oder die Ausführung des Auftrages ablehnen müssen oder einen bestehenden Vertrag nicht mehr durchführen können und ggf. beenden müssen. 
	<br><br>
	Im Rahmen unserer Geschäftsbeziehung müssen Sie nur die personenbezogenen Daten bereitstellen, die für die Begründung,
	Durchführung und Beendigung einer Geschäftsbeziehung erforderlich sind oder zu deren Erhebung wir gesetzlich verpflichtet sind.
	<br><br>
	Die Verarbeitung Ihrer personenbezogenen Daten ist ebenfalls zur Erfüllung einer rechtlichen Verpflichtung erforderlich, der wir unterliegen.
</p>
<h3>Bedingungen für Ihre Einwilligung</h3>
<p>
Entsprechend Artikel 7 EU-DSGVO ist Ihre Einwilligung für die Verarbeitung, Speicherung und Weitergabe Ihrer persönlichen Daten notwendig. Diese Einwilligung erteilen Sie uns für die festgelegten Zwecke, wenn Sie uns Ihre Daten übermitteln. Sie können Einwilligungen jederzeit mit Wirkung für die Zukunft widerrufen. Dies gilt auch für Einwilligungserklärungen, die Sie uns gegenüber vor der Geltung der EU-DSGVO, also vor dem 25. Mai 2018, erteilt haben. Der Widerruf wirkt nur für zukünftige Verarbeitungen, nicht für bereits erfolgte. Sie sind berechtigt jederzeit und kostenlos Auskunft gemäß Artikel 15 EU-DSGVO und § 34 Bundesdatenschutzgesetz (BDSG), über Ihre, bei uns gespeicherten, personenbezogenen Daten zu erhalten. Darüber hinaus haben Sie jederzeit das Recht, die Löschung gemäß Artikel 17 EU-DSGVO bzw. § 35 BDSG, die Sperrung oder Berichtigung gemäß Artikel 16 EU-DSGVO, die Einschränkung der Verarbeitung gemäß Artikel 18 EU-DSGVO sowie die Datenübertragbarkeit gemäß Artikel 20 EU-DSGVO, Ihrer Daten zu verlangen.
	<br><br>
	Außerdem haben Sie ein Beschwerderecht bei einer Datenschutzaufsichtsbehörde gemäß Artikel 77 EU-DSGVO bzw. § 19 BDSG.
	Davon ausgenommen sind Daten die einer gesetzlichen Bestimmung entgegenstehen, insbesondere abrechnungstechnische oder buchhalterische Daten.
	Sie versichern uns gegenüber, sollten Sie Daten eines weiteren Reisenden übermitteln, das Sie die ausdrückliche Genehmigung der weiteren Reisenden haben,
	uns die persönlichen Daten zu übermitteln.
</p>
<h3>Verarbeitung besonderer Kategorien</h3>
<p>
	Wir verarbeiten und speichern keine persönlichen Daten zu rassische und ethnischer Herkunft, politischer Meinungen, religiöse oder weltanschauliche Überzeugungen,
	genetische Daten, Gesundheitsdaten, Daten zum Sexualleben oder sexuellen Orientierungen.
</p>
<h3>Welche Ihrer persönlichen Daten benötigen und welche speichern wir</h3>
<p>
Im Falle einer Buchung fragen und speichern wir folgende Stammdaten ab: Ihren Vor- und Nachnamen, Ihre Anschrift, Ihre E-Mail-Adresse und Ihre Telefonnummer. Im Falle einer Buchung können wir weiterhin Gesundheitsdaten zur Vorbeugung von Unfällen und zum Schutz des oder der Reisenden abfragen, die wir jedoch nicht speichern: z. B. Grad körperlicher Behinderung, Schwerbehindertenausweis, Lebensmittelunverträglichkeit, Allergien, Schwangerschaft. Im Falle einer Buchungsanfrage speichern und verarbeiten wir Ihren Vor- und Nachnamen, Ihre E-Mail-Adresse und Ihre Telefonnummer. Wenn Sie uns damit beauftragen einen Flug für Sie zu vermitteln und Sie in ein Land reisen, dessen Bestimmungen die Angabe von erweiterten Passagierdaten (API) vorschreibt, benötigen wir weiterhin von Ihnen Ihre Vor-, Mittel- und Nachnamen wie diese im Reisepass aufgeführt sind, Ihre Passnummer, Gültigkeit des Reisepasses, Ausstellerland, Ihre Nationalität und Ihr Geburtsdatum. 
	<br><br>
	Folgende Länder verlangen zur Zeit die Reisepassdaten von jedem Reisenden vor Reiseantritt: Australien, Irland, Neuseeland, Bahrain, Italien, Schweiz, Bangladesch, Japan, Spanien, Brasilien,
	Kanada, Sri Lanka, China, Katar, Südafrika, Deutschland, Korea, Tschechische Republik, Großbritannien, Malta, USA, Indien, Niederlande, Zypern, Indonesien, Österreich.
	<br><br>
Nach amerikanischen Gesetz werden dem US Department of Homeland Security (DHS) Reise und Buchungsdaten, sogenannte Passengers Name Records, (PNR) von Fluggesellschaften vor Reiseantritt übermittelt. Dies gilt für Flüge, die in, aus oder über die USA durchgeführt werden. Diese Daten dienen in erster Linie dazu den Terrorismus und andere schwere Verbrechen zu bekämpfen. 
	<br><br>
Diese Daten können mit weiteren Daten abgeglichen werden. Die PNR′s werden 3 Jahre und 6 Monate gespeichert und können an andere Behörden weitergegeben werden. Zu diesen Daten kommen die Daten aus Ihrem Reisepass, die Advanced Passenger Information (API). Eine ausführliche Erklärung zur Verwendung von Ihren Daten können Sie in der Entscheidung 2004/535/EG der EU-Kommission nachlesen. 
	<br><br>
Fluggesellschaften geben ab dem 25. Mai 2018 Fluggastdaten an die Fluggastdatenzentralstelle des BKA weiter. Die Daten werden fünf Jahre vom BKA gespeichert. Unter anderem werden Name, Sitzplatz, E-Mail, Telefonnummern, die Reiseroute, Hotels, Mietwagen, Kreditkartennummer, Anzahl der Gepäckstücke, Mitgliedschaft an einem Vielfliegerprogramm, Reisebegleiter, Sonderwünsche beim Essen und weitere Angaben gespeichert, analysiert und weitergegeben an andere Mitgliedstaaten. Das gilt für Flüge außerhalb der EU, in Deutschland auch für Flüge innerhalb der EU und Deutschlands.
</p>
<h3>Automatisierte Entscheidungsfindung</h3>
<p>
	Zur Begründung und Durchführung der Geschäftsbeziehung nutzen wir gemäß Artikel 22 EU-DSGVO grundsätzlich keine automatisierte Entscheidungsfindung oder ein Profiling.
</p>
<h3>Weitergabe Ihrer Daten an Dritte</h3>
<p>
	Zur Erbringung einer Leistung geben wir Ihre persönlichen Daten an Dritte weiter.
	Dazu gehören, Fluggesellschaften, Mietwagen Vermieter, Hotels, Ferienwohnungsanbieter, Versicherungen, Touren und Transfergesellschaften sowie Kreditkartengesellschaften.
	Wir versichern Ihnen, dass wir ausschließlich die Daten an Dritte weiterreichen, die zur Erbringung einer Leistung notwendig sind. Auch übermitteln wir Ihre Daten nur an uns bekannte Firmen.
	Weiterhin werden wir Ihre Bankverbindung und/oder Kreditkarten Daten abfragen, die von uns jedoch nie gespeichert werden.
	Sollte es eine Buchung auf unseren Webseiten oder eine telefonische Buchung notwendig machen, das Sie Ihre gebuchte Leistung sofort bezahlen oder anzahlen müssen,
	werden Sie an einen externen Zahlungsabwickler weitergeleitet.
	<br><br>
	Fluggesellschaften / Consolidatoren übermitteln wir Ihre Vor- und Nachnamen, Ihre Anschrift, Ihre Geburtsdaten, Telefonnummer, sowie Ihre Bank- oder Kreditkartendaten.
	Mietwagen Vermieter übermitteln wir Ihren Vor- und Nachnamen, sowie Ihr Geburtsdatum, Hotels, Ferienwohnungsanbieter,
	erhalten von uns Ihre Vor- und Nachnamen Tourenanbieter und Transfergesellschaften, erhalten von uns Ihre Vor- und Nachnamen sowie Telefonnummer.
	Versicherungen erhalten von uns Ihre Vor- und Nachnamen, Adresse, E-Mail Adresse sowie Geburtstag. Kreditkartengesellschaften übermitteln wir Ihre Kreditkartendaten, Ihren Vor- und Nachnamen.
</p>
<h3>Weitergabe Ihrer persönlichen Daten in Drittländer</h3>
<p>
	Wir übermitteln Ihre Daten in Staaten außerhalb der Europäischen Union nur,
	soweit dies zur Ausführung und Abwicklung der beauftragten Leistungen erforderlich oder gesetzlich vorgeschrieben ist oder Sie uns Ihre Einwilligung erteilt haben (z. B. bei Fernreisen).
</p>
<h3>Wie lange werden Ihre personenbezogenen Daten gespeichert?</h3>
<p>
	Ihre persönlichen Daten aus Buchungsanfragen, telefonisch, per E-Mail oder über unsere Webseite werden nach 6 Monaten komplett gelöscht.
	Dazu zählen Ihr Vor- und Nachname, Ihre E-Mail Adresse, Ihre Telefonnummer, alle Angaben zum 2. Reisenden, sämtlicher E-Mail Schriftverkehr.
	<br><br>
	Alle weiteren Daten werden entsprechend der gesetzlich vorgeschriebenen Aufbewahrungsfristen gespeichert und anschließend gelöscht.
	Wir werden Sie in jedem Fall schriftlich über die Berichtigung oder Löschung Ihrer persönlichen Daten informieren.
</p>
<h3>Gesetzliche Vorgaben </h3>
<p>
	Wir unterliegen verschiedenen rechtlichen Verpflichtungen wie den handels- und steuerrechtlichen Aufbewahrungsfristen nach dem Handelsgesetzbuch.
	Gemäß § 257 HGB sind wir verpflichtet die gespeicherten Daten zu Ihrer Buchung aufzubewahren um vollständige Handelsbücher führen zu können und zudem alle für unsere Besteuerung
	relevanten Angaben gemäß §147 AO machen zu können.
</p>
