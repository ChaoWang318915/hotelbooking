<h1>Agolhitheemu <small>(Raa Atoll)</small></h1>
<p>
  <b>Agolhitheemu</b> <small>(auch An'golhitheemu)</small> ist eine bewohnte Insel im <b>Raa Atoll</b>. 
  Auf der Insel lebten die ersten Siedler der Malediven. 
  Historische Artefakte wurden auf der ganzen Insel gefunden, darunter alte Relikte.
  2003 wurden große kunstvoll geschnitzte Korallensteine entdeckt und beim Bau der Inselschule im Jahr 2006 wurde eine kleine Statue Buddhas gefunden.
  Diese wurde nach einer Schulinspektion gestohlen, andere Reliquien wurden damals von Inselbewohnern zerstört, die nicht die historische Bedeutung kannten.
  Ende 2002 wurden 7 Friedhöfe unter der Insel entdeckt.
  Die Regierung plant zukünftig den Kulturtourismus zu fördern, um den Touristen einen besseren Einblick in das maledivische Erbe zu geben.
 <br><br>
 Die Entfernung von Agolhitheemu nach <b>Malé</b> beträgt ca. 187 km.
 Die Insel ist ca. 906 x 529 m groß und verfügt über eine Fläche von ca. 29,5 ha.
 Auf der Insel wohnen 272 Einwohner.

</p>
