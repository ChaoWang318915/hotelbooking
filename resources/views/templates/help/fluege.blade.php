<header class="heading-block">
<h1>Fragen und Antworten zu den (internationalen) Flügen</h1>
<p>
Bitte nehmen Sie zur Kenntnis, das wir Ihnen als Condor Agentur vorrangig Condorflüge anbieten werden.
Die Flüge gehen von Frankfurt direkt nach Malé.
Informationen zu Condor finden Sie <a href="https://www.my-maldives.com/condor">hier</a>.
<br><br>
Gern vermitteln wir Ihnen jedoch jeden anderen, gewünschten Flug.
Wir können Ihnen nur einen Flug vermitteln, wenn Sie auch das Hotel bei uns gebucht haben.
Wir vermitteln keine Nur-Flugsegmente. Wenn Sie einen Flug wünschen, teilen Sie uns das bitte bei der Hotelbuchung mit oder rufen Sie uns einfach an.
</p>
</header>

<ul class="accordion mt-0">
<li class="open">
<header class="title-panel">
<h3>Wie kann ich einen Flug buchen?</h3>
</header>
<div class="collapse">
<p>
Um einen Flug zu buchen, schicken Sie uns bitte eine E-Mail. Wir benötigen die Angaben aus Ihrem Reisepass.
Dazu gehören Ihr kompletter Name inklusive aller Mittelnamen wie diese im Reisepass aufgeführt sind. Weiterhin teilen Sie bitte mit, wie Sie den Flug bezahlen möchten (Kreditkarte oder Überweisung).
Wenn Sie als Zahlungsmethode Überweisung wünschen, benötigen wir Ihre Kontoverbindung.
Dazu gehört die IBAN Nummer bzw. die BIC und der Kontoinhaber.
<br><br>
Bitte nehmen Sie zur Kenntnis, dass wir keine Flugbuchung vornehmen, wenn die Zahlung durch einen Dritten, nicht Reisenden, durchgeführt werden soll.
Das gilt auch für die Hotelbuchung <small>(siehe FAQ Hotelbuchungen)</small>. 
<b>Achtung:</b> Bitte nehmen Sie zur Kenntnis, das wir Bankdetails von unseren Kunden nicht speichern!
</p>
</div>
</li>

<li>
<header class="title-panel">
<h3>Was kostet es einen Flug zu buchen?</h3>
</header>
<div class="collapse">
<h4>Flugpreise</h4>
<p>
Die Preise für Flüge ändern sich ständig. Schon nach wenigen Minuten kann sich ein Flugpreis bei einer Airline ändern.
Der Grund sind Währungskurse, Kerosinpreise und die Auslastung in den angebotenen Flugklassen.
Wenn Sie einen Flug anfragen, senden wir Ihnen ein Angebot per E-Mail zu.
Der angebotene Preis kann sich wie beschrieben jederzeit ändern.
Der Flugpreis richtet sich entsprechend dem Buchungszeitraum und der Reisezeit.
</p>

<h4>Bearbeitungsentgelt</h4>
<p>
Wenn wir Ihnen einen Flug vermitteln, berechnen wir Ihnen 50 € (inkl. 19 % MwSt.) pro Person Bearbeitungsentgelt.
Dieses beinhaltet die Vermittlung des Flugtickets, Abrechnung mit der Fluggesellschaft im Direktinkasso, Zusendung des Flugtickets, Unterrichtung bei Flugzeitenänderungen und wenn nötig die Stornierung des Fluges bei der Fluggesellschaft.
Des Weiteren buchen wir für Sie Sportgepäck, Sitzplätze oder Speisen bei der Fluggesellschaft.
<br><br>
Wenn Sie diese Leistungen jedoch zu einem späteren Zeitpunkt dazu buchen möchten, berechnen wir Ihnen ein Bearbeitungsentgelt.
Die entsprechenden Kosten entnehmen Sie bitte der Entgelttabelle.
Das Bearbeitungsentgelt ist vorab zahlbar, eine entsprechende Rechnung geht Ihnen vorab zu mit dem Link um den Rechnungsbetrag Online mit Kreditkarte oder EC-Karte zu bezahlen.
Das Bearbeitungsentgelt ist nicht rückerstattbar.
<br><br>
Die Höhe des Bearbeitungsentgelts für einen kostenpflichtigen Service entnehmen Sie bitte unserer <a href="https://www.my-maldives.com/entgelttabelle">Entgelttabelle.</a>
</p>
<h4>Flüge auf Option</h4>
<p>
Auf Wunsch und gegen eine Gebühr von 10 € pro Person (inkl. MwSt.) können wir Ihnen einen Condorflug für 3 Tage auf Option setzen.
Auf diese Weise ist der Flug unverbindlich gebucht und Sie haben 3 Tage Zeit (bis 17 Uhr des 3. Tages) sich zu entscheiden, ob Sie diesen Flug buchen möchten.
Sollten Sie sich entscheiden den Flug nicht zu buchen brauchen Sie nichts tun, die Option erlischt automatisch am 3. Tag um 17 Uhr (CET).
Wenn Sie den Flug verbindlich buchen möchten, rufen Sie uns bitte an oder senden Sie uns eine E-Mail.
Wir buchen den Flug für Sie. Sie erhalten umgehend Ihre Flugtickets von Condor zugeschickt.
</p>


</div>
</li>
<li>
<header class="title-panel">
<h3>Kann man den Flug selber buchen?</h3>
</header>
<div class="collapse">
<p>
Selbstverständlich.
Wenn Sie vorher die Verfügbarkeit des Hotelzimmers wissen möchten, sprechen Sie uns bitte an.
Gern prüfen wir die Verfügbarkeit für Sie.
Buchen Sie keinen Flug wenn Sie keine Hotelbestätigung von uns erhalten haben. </p>
</div>
</li>


<li>
<header class="title-panel">
<h3>Kann man einen Flug umbuchen?</h3>
</header>
<div class="collapse">
<p>
Eine Umbuchung ist vom gebuchten Tarif abhängig.
Wenn laut Tarifbestimmungen eine Umbuchung möglich ist, ist diese meist mit Kosten verbunden oder Sie haben ein Ticket im "Flex-Tarif" gebucht, dann ist eine (oder mehrere) Umbuchung(en) kostenlos möglich.
Bei den meisten Standard Tarifen ist eine Umbuchung nicht möglich und kommt einer Stornierung gleich.
Die Kosten können bis zu 100 % des Flugpreises ausmachen.
Bitte beachten Sie, dass wir im Falle einer Umbuchung ein Bearbeitungsentgelt berechnen.
Die entsprechenden Kosten entnehmen Sie bitte der <a href="https://www.my-maldives.com/entgelttabelle">Entgelttabelle.</a>
<br><br>
<b>Achtung:</b> Aus Sicherheitsgründen ist es nicht möglich, einen Condorflug den Sie bei uns gebucht haben, selbst umzubuchen oder zu stornieren.
</p>
</div>
</li>


<li>
<header class="title-panel">
<h3>Wie bezahlt man sein Flugticket?</h3>
</header>
<div class="collapse">
<p>
Der gebuchte Flug wird per Direktinkasso über die Fluggesellschaft sofort bezahlt.
Hierzu ist Ihre Kreditkarte notwendig. Das Flugticket kann per Überweisung direkt an Condor bezahlt werden. Hierfür benötigen wir Ihre Bankdetails, die wir an Condor weiterleiten.
Sollten Sie keinen Condorflug buchen, erhalten Sie von uns die Rechnung, die sofort nach Rechnungserhalt bezahlt werden muss.
Hierfür können Sie einfach und sicher Online bezahlen mit Ihrer Kreditkarte, einen entsprechenden Link schicken wir Ihnen per E-Mail zu.
</p>

</div>
</li>
<li>
<header class="title-panel">
<h3>Flugstornierungen</h3>
</header>
<div class="collapse">
<p>
Ein Flug kann jederzeit vor Abreise storniert werden.
Die Kosten richten sich nach dem Tarif des gebuchten Flugtickets und der AGB der jeweiligen Fluggesellschaft und sind bitte dort zu entnehmen.
Flugreservierungen sollten schriftlich storniert werden, bei telefonischer Stornierung (nur bei uns bekannten Kunden möglich) halten Sie bitte das E-Ticket oder Ihre Reservierungsnummer bereit.
Bei Fragen steht Ihnen auch gerne unser Kundenservice zur Verfügung.
<br><br>
Wenn wir für Sie einen Flug stornieren entstehen Ihnen keine zusätzlichen Kosten, diese Leistung ist in dem Bearbeitungsentgelt enthalten welches Sie bezahlt haben als wir Ihnen den Flug vermittelt haben.
</p>
</div>
</li>


<li>
<header class="title-panel">
<h3>Namensänderungen </h3>
</header>
<div class="collapse">
<p>Namensänderungen bei Flugreservierungen sind nur mit dem entsprechenden Tarif möglich.
Bei vielen Tarifen kommen Namensänderungen einer Stornierung gleich.
Bitte sprechen Sie uns an, gern prüfen wir ob in Ihrem Tarif eine Namensänderung möglich ist.
<br><br>
Wenn der Name falsch geschrieben ist, sind nur Änderungen bei max. 3 Buchstaben möglich. Ansonsten wird die Fluggesellschaft das als Namensänderung bewerten.
Es ist deshalb wichtig, dass Sie Ihren Namen inklusive aller Mittelnamen, wie diese im Reisepass angegeben sind, bei der Reservierung richtig mit angeben.
Bitte achten Sie auf korrekte Schreibweise.
</p>
</div>
</li>


<li>
<header class="title-panel">
<h3>Low-Cost Airlines </h3>
</header>
<div class="collapse">
<p>
Leider vermitteln wir Ihnen keine Low-Cost Airlines. Natürlich steht es Ihnen frei eine Low-Cost Airline zu Ihrem Hotelaufenthalt selbst zu buchen.
</p>
</div>
</li>

<li>
<header class="title-panel">
<h3>Wie erhalte ich meine Flugtickets? </h3>
</header>
<div class="collapse">
<p>
Ihr Flugticket wird sofort ausgestellt und Ihnen via E-Mail zugesendet.
Alle Daten sind im System der Airline gespeichert, ein Papier-Ticket ist nicht mehr notwendig.
Sie benötigen nur noch Ihren gültigen Ausweis/Reisepass.
Sie erhalten von uns direkt nach Buchung des Fluges eine Buchungsbestätigung/ETIX, wo Sie nochmals alle Details Ihres Fluges entnehmen können.
Auf Wunsche senden wir Ihnen die Buchungsbestätigung/ETIX auch per Fax zu.
</p>
</div>
</li>

<li>
<header class="title-panel">
<h3>Sitzplatzreservierungen </h3>
</header>
<div class="collapse">
<p>
Sitzplatz Reservierungen nehmen wir bei Flugreservierung gerne entgegen. Nicht bei allen Tarifen ist eine kostenlose Sitzplatzreservierung möglich.
Bei Sitzplatzreservierungen können extra Gebühren gemäß den Tarifbedingungen der Fluggesellschaft entstehen. Entsprechende Auskunft erhalten Sie bei Buchung.
Wenn Sie die Sitzplatzreservierung nach Ihrer Flugbuchung vornehmen, berechnen wir eine Bearbeitungsgebühr. Während des Buchungsprozeßes Ihres Fluges ist kein Bearbeitungsgeld zu zahlen.
<br><br>
Die entsprechenden Kosten entnehmen Sie bitte der <a href="https://www.my-maldives.com/entgelttabele">Entgelttabelle</a>.
Das Bearbeitungsentgelt ist vorab zahlbar eine entsprechende Rechnung geht Ihnen vorab zu mit dem Link um den Rechnungsbetrag Online mit Kreditkarte oder EC-Karte zu bezahlen.
Das Bearbeitungsentgelt ist nicht rückerstattbar.
</p>
</div>
</li>

<li>
<header class="title-panel">
<h3>Rail & Fly </h3>
</header>
<div class="collapse">
<p>
Bei vielen Flugangeboten können wir Ihnen ein Rail & Fly von der Deutschen Bahn anbieten.
Rail & Fly bringt Sie von jedem Bahnhof der Deutschen Bahn (www.bahn.de) auf direktem Weg zum Flughafen zum reduzierten Preis.
Rail & Fly kann bequem bei der Flugreservierung mit reserviert werden. Das Bahnticket erhalten Sie von uns gesondert als PDF Fahrschein und sollte am Abreisetag mitgeführt werden.
Die Bahntickets sind einen Tag vor Abreise und einen Tag nach Ankunft des Fluges gültig. Eine Rückerstattung, auch für nicht in Anspruch genommene Strecken, kann nicht erfolgen.
Bitte beachten Sie, dass Fluggesellschaften nicht verantwortlich bei Bahnverspätungen sind. Planen Sie für Ihre Anreise genügend Zeit ein.
Seitens der Fluggesellschaft besteht keine Beförderungspflicht, wenn Sie den Flug verspätet antreten. 
<br><br>
Auf Wunsch können wir Ihnen einen Mietwagen anbieten, den Sie am Flughafen wieder abgeben können.
Bitte rufen Sie uns an, wenn Sie einen Mietwagen wünschen, unser Kundenservice steht Ihnen gern zur Verfügung. 
<br><br>
Wenn Sie die Sitzplatzreservierung nach Ihrer Flugbuchung vornehmen, berechnen wir eine Bearbeitungsgebühr.
Während des Buchungsprozesses Ihres Fluges ist kein Bearbeitungsgeld zu zahlen. 
<br><br>
Die entsprechenden Kosten entnehmen Sie bitte der <a href="https://www.my-maldives.com/entgelttabele">Entgelttabelle</a>.
Das Bearbeitungsentgelt ist vorab zahlbar, eine entsprechende Rechnung, mit dem Link um den Rechnungsbetrag Online mit Kreditkarte oder EC-Karte zu bezahlen, geht Ihnen vorab zu.
Das Bearbeitungsentgelt ist nicht rückerstattbar.
</p>
</div>
</li>

