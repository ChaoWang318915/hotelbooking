								<h2><a href="#">Flugzeiten</a></h2>
								<p>Der Hoteltransfer mit dem Wasserflugzeug wird ausschließlich bei Tageslicht durchgeführt. Die regulären Flugzeiten sind zwischen <strong>06:00 Uhr und 18:00 Uhr</strong>. Spätestens um <strong>15:00 Uhr / 15:30 Uhr</strong> ist das Erscheinen am <strong>Gate</strong> der Wasserfluggesellschaft notwendig, um den Transfer wahrnehmen zu können.</p>
								<p>Sollte das nicht möglich sein, muss man eine Nacht in Malé oder Umgebung übernachten. In den letzten Jahren sind die  Fluggesellschaften immer mehr dazu übergegangen Internationale Regeln zu übernehmen.</p>
								<p>Während es in den Anfangszeiten noch üblich war auf die Reisenden zu warten, zum Beispiel bei Flugverspätungen des internationalen Fluges, wird schon seit einiger Zeit nicht mehr gewartet und es gibt ein “no-Show”.</p>
								<p>Wer Glück hat kommt dann erst am nächsten Tag ins Hotel oder am Abend. Wer Pech hat muss 50% Stornokosten zahlen.</p>
