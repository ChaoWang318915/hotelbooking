								<h2><a href="#">Gepäckregeln</a></h2>
								<p>Wasserflugzeuge erlauben nur 20 kg Gepäck p. Person, hinzu kommen maximal 5 kg Handgepäck jeweils pro Person. Wenn bei einem internationalen Premium-/ oder Business Class Flug Mehrgepäck erlaubt ist (zb. 30 kg),  bedeutet dies nicht, dass man das Gepäck auch im Wasserflugzeug kostenlos mitnehmen kann.</p>
								<p>In diesem Fall kommt es zu Übergepäck, das muss nicht nur extra bezahlt werden, sondern kann auch oft nicht mit dem Wasserflugzeug zusammen transportiert werden. Es wird in der Regel mit dem Boot zum Hotel gebracht, so das man es am selben Tag noch zur Verfügung steht.</p>
								<p>Mehrgepäck muss als Übergepäck  bezahlt werden. Achten sollte man auch auf das Gewicht vom  Handgepäck. Der Preis für Übergepäck beträgt zwischen 2 USD/kg und 8 USD/pro kg zzgl. 12 % GST, je nach Hotel. Handgepäck sollte die Maße <strong>50 cm x 30 cm x 17 cm</strong> nicht überschreiten. Koffer dürfen nicht mehr wiegen als <strong>32 kg/p. Koffer</strong></p>
